      <b>
        WOLFGANG AMADEUS MOZART: OVERTURE TO <i>THE MARRIAGE OF FIGARO</i> (LE NOZZE DI FIGARO)
      </b>
      </p>
      <p>
      Wolfgang Amadeus Mozart (1756-1791) composed his 4 act opera <i>The Marriage of Figaro</i> (Le nozze di Figaro) in 1786. The Marriage of Figaro is known as an <b>opera buffa</b> (comic opera) based on a stage comedy of the time.
      </p>
      <p>
      The opera&rsquo;s premiere was a notable success and <i>The Marriage of Figaro</i> remains one of the most performed operas of all time. You may have heard part of this overture used in different films, for example the movie Trading Places. 
      </p>
      <p>
      <i>The Marriage of Figaro</i> is set in Spain, in the castle of Count Almaviva. The Count&rsquo;s valet and former barber, Figaro is to marry Susanna, the Countess&rsquo;s maid. However, the Count is planning to seduce Susanna, while the Countess is pursued by Cherubino, a young Page.  The opera features a tangled web of comedy, romance, misadventure, and mistaken identity as the relationships and action of the opera unfold. 
      </p>
      <p>
      As was typical for the time, <i>The Marriage of Figaro</i> opens with an instrumental overture. Mozart famously composed the overture just a few hours before the opera&rsquo;s premiere. The approximately 4-5 minute overture that you will hear is an arrangement of Mozart&rsquo;s symphonic overture and is played by a wind octet of oboes, clarinets, bassoons, and horns.  
      </p>
