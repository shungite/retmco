      <span class="badge">1</span> Form in music refers to the musical architecture or the way the music is structured. The form of the Octet in E flat major is typical for classical string quartets and symphonies, although the treatment of the menuetto as more like that of a scherzo was certainly forward looking. The Octet in E flat major is structured in four movements:
      </p>

      <ol>
        <li>Allegro (fast and lively)</li>
        <li>Andante (moderately slowly, walking pace);</li>
        <li>Menuetto and Trio (A minuet, in this case, more like a scherzo which is also in &frac34; time but more lively and playful in nature than a minuet); 
          IV. Presto (very fast)</li>
      </ol>

      <p>
      Each of the four sections or movements also has a particular form.  The first movement Allegro is written in sonata form.  Sonata form is made up of three sections: a first section called <b>exposition</b> that presents 2 themes; a middle <b>development</b> section where the themes are varied and developed; and a final <b>recapitulation</b> section that re-states the main themes. 
      </p>

      <p>
      The first and second themes are not highly contrasting but can be distinguished by instrumentation. Can you hear the lively theme 1 in the oboe part and a second theme stated in the bassoon and clarinet sections? 
      </p>

      <p>
      Can you hear how Beethoven explores many ways to vary these themes in the development section?  And can you hear a new motive played by the clarinet in this section? Can you hear how all the instruments restate the two main themes in the recapitulation section? 
      </p>

      <p>
      The Andante movement is in a song-form or ABA structure where the first and second parts of the movement are the same or similar, and the middle part is a contrasting section. Can you hear the beautiful main theme expressed first in the oboe part? This andante could also be described as a duet.  Can you hear the main theme sung between the oboe and bassoon to create the effect of an operatic duet between soprano and baritone? 
      </p>

      <p>
      The form of the Minuetto and Trio is also an ABA structure.  However, this particular minute was innovative for its time, since it is one of Beethoven&rsquo;s first scherzos.  The scherzo is more lively and playful than the stately  minuet court dance.  Can you hear the dynamic contrasts of the scherzo? In the middle trio can you hear a dialogue between the clarinet and horns?  
      </p>

      <p>
      The Presto finale is a rousing finish to this octet. It takes the form of a rondo-sonata. In rondo form, a main theme is repeated before and after contrasting sections.  The repeated theme is called the A section.  The contrasting sections are called B, C, D etc., depending on how many different sections the composer decides to compose. Sonata form features the statement, development and re-statement of two main themes. In this Presto, what does the main theme sound like to you?  Does it have fanfare qualities? Can you hear the constant variations of the main theme throughout the Presto?  Does the second theme remind you of any other melodyperhaps Beethoven&rsquo;s <i>Hymn of Joy</i>? Can you hear a coda or conclusion that features virtuosic writing for the horns? Can you hear special improvisatory parts for the oboe and clarinet? 
