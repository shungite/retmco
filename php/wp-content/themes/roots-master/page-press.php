					<p>
						<strong>Karl Stobbe, Concertmaster</strong> 
					</p>
					<p>
						Karl Stobbe is one of Canada&rsquo;s most accomplished violinists, known for his dedication to excellence on the violin and classical music in all its forms. As a concertmaster, soloist, and chamber musician, Karl has been an audience favourite in small settings and large venues. His diverse career has included performances of all six Ysa&yuml;e sonatas for solo violin, all 16 Beethoven string quartets, and all ten Mahler symphonies. Noted for his generous, rich sound, and long, poignant phrasing, he is described by the <em>San Francisco Classical Voice</em> as &ldquo;an artist with soulful musicianship,&rdquo; and by London&rsquo;s <em>Sunday Times</em> as &ldquo;a master soloist, recalling the golden age of violin playing&thinsp;&hellip;&thinsp;producing a breathtaking range of tone colours.&rdquo; 
					</p>
					<p>
						Karl has performed in many of North America&rsquo;s most famous concert venues, including Carnegie Hall, Jordan Hall, the National Arts Centre, Roy Thompson Hall, Segerstrom Hall, and the Chan Centre. As a chamber musician and soloist, he has shared the stage with many of the most important and eclectic violinists of our day, from James Ehnes to Mark O&rsquo;Connor. Avie Records&rsquo; 2014 recording of Karl performing Ysa&yuml;e&rsquo;s solo violin sonatas received worldwide attention, including that of <em>Gramophone</em> magazine, which hailed it as &ldquo;full of spirit and energy&thinsp;&hellip;&thinsp;exciting, fearless&thinsp;&hellip;&rdquo; It was nominated for a 2015 JUNO Award for best Classical Album: Solo or Chamber Ensemble, and was the winner of the 2015 Western Canadian Music Award for Classical Album of the Year. Karl frequently performs and records new music and has been involved in numerous commissions and world premieres. 2014 marked the release of a live recording of Karl, joined by Jonathan Crow and the National Arts Centre Orchestra in the title track of <em>Cobalt</em>, a CD of the music of Jocelyn Morlock. <em>Cobalt</em> also won a 2015 Western Canadian Music Award for best Classical Composition of the Year. 
					</p>
					<p>
						A lover of all things violin, Karl completed a minor in Violin Repair and Construction while completing his Master&rsquo;s of Music at Indiana University. His passion for the construction and mechanics of the violin is an important part of his professional musical life and continues to influence his performances and teaching. Karl never misses an opportunity to see and play exceptional violins and bows. He has given multiple presentations on the history of the violin family, violin building and repair, and has organized showings and lecture recitals on rare, fine instruments at various concert halls, art galleries, universities, and conservatories. 
					</p>
