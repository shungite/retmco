					<p>
						<strong>Anne Manson, Music Director and Conductor</strong> 
					</p>
					<p>
						Conductor Anne Manson has served as Music Director of the Manitoba Chamber Orchestra since 2008 and recently renewed her contract through 2020. Manson&rsquo;s strong commitment to contemporary music has led to numerous commissions and recordings with the MCO. Among them are Philip Glass&rsquo; <em>Symphony No. 3</em> and <em>Piano Concerto</em>, and the Juno and Western Canada Music award nominated <em>Troubadour &amp; the Nightingale</em> with soprano Isabel Bayrakdarian. This season Manson gives the world premiere of <em>Nanabush and the Drum&thinsp;/&thinsp;Nanabozho et le tambour</em>, a new bilingual music theater work by composer Michael Oesterle and writer Rh&eacute;al Cenerini in collaboration with Th&eacute;&acirc;tre Cercle Moli&egrave;re. A recording of Baroque concerti with percussionist Evelyn Glennie and the MCO will be released in spring 2017. 
					</p>
					<p>
						As a guest conductor, she has performed with the Los Angeles Philharmonic, Houston and Indianapolis Symphonies, the Residentie Orchestra of the Hague, the St. Paul Chamber Orchestra, London Philharmonic, the Royal Scottish National Orchestra, Bournemouth Sinfonietta, the Swedish Chamber Orchestra, Orquesta de Extremadura, BBC Proms, and she has guest conducted regularly with many orchestras in Spain. 
					</p>
					<p>
						As a renowned conductor of opera, Manson has led at houses around the globe: San Francisco Opera (<em>Cos&igrave; fan tutte</em>), New York City Opera (Barber&rsquo;s <em>Vanessa</em>), Grand Th&eacute;&acirc;tre de Gen&egrave;ve (Floyd&rsquo;s <em>Susanna</em>), Royal Opera Stockholm (Donizetti <em>Viva la mamma</em>), Spoleto Festival (Jan&aacute;&#x10D;ek&rsquo;s <em>Kat&rsquo;a Kabanova</em>), Minnesota Opera (US premiere of Jonathan Dove&rsquo;s <em>The Adventures of Pinnochio</em>, Donizetti&rsquo;s <em>Maria Stuarda</em>, Puccini&rsquo;s <em>Tosca</em>, Humperdinck&rsquo;s <em>H&auml;nsel und Gretel</em>), Portland Opera (Phillip Glass&rsquo; <em>Orph&eacute;e</em> and <em>Galileo Galilei</em>&mdash;released on CD in 2013, Puccini&rsquo;s <em>Madama Butterfly</em>), Virginia Opera (Bizet&rsquo;s <em>Les P&ecirc;cheurs de Perles</em>, Offenbach&rsquo;s <em>Orph&eacute;e aux enfers</em>). At the Washington National Opera, Manson has conducted numerous works including Douglas Pew&rsquo;s <em>Penny</em>, Barber&rsquo;s <em>Vanessa</em>, Conrad Susa&rsquo;s <em>Dangerous Liasons</em>, and Scott Wheeler&rsquo;s <em>Democracy</em> and has served as a mentor for the American Opera Initiative commissions. She received Canada&rsquo;s DORA Award for outstanding musical direction for her work with the Canadian Opera Company on Benjamin Britten&rsquo;s <em>A Midsummer Night&rsquo;s Dream</em>. At the Juilliard School, she has conducted Jan&aacute;&#x10D;ek&rsquo;s <em>The Cunning Little Vixen</em>, Offenbach&rsquo;s <em>Orph&eacute;e aux enfers</em>, Poulenc&rsquo;s <em>Dialogues des Carm&eacute;lites</em>, Ned Rorem&rsquo;s <em>Our Town</em>, and Peter Maxwell Davies&rsquo; <em>Kommilitonen!</em>. 
					</p>
					<p>
						Upcoming engagements include a return to The Juilliard School to conduct Jan&aacute;&#x10D;ek&rsquo;s <em>Kat&rsquo;a Kabanova</em>, Manson&rsquo;s third appearance at Spoleto Festival for the season opening concert, San Francisco Opera&rsquo;s Schwabacher Summer Concert, performances with the Swedish Chamber Orchestra, and a tour of Philip Glass&rsquo; new piano concerto with Simone Dinnerstein and the MCO. 
					</p>
					<p>
						In addition to recordings with Manitoba Chamber Orchestra and Portland Opera, Manson has recorded with the Residentie Orchestra of The Hague, the BBC Scottish Symphony, the Iceland Symphony, the Singapore Symphony, and the Swedish Chamber orchestra. 
					</p>
					<p>
						Before serving as the MCO&rsquo;s Music Director, Manson was Music Director of London&rsquo;s Mecklenburgh Opera (1987-96) and the Kansas City Symphony (1999-2004). She was the first woman to conduct the Vienna Philharmonic at the Salzburg Festival in 1994 (Mussorgsky&rsquo;s <em>Boris Godunov</em>). 
					</p>
