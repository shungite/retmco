							<h4>
								Review:<br>Children&rsquo;s opera packs emotional wallop 
							</h4>
							<p class="fineprintdark">
								<a href="http://www.winnipegfreepress.com/">Winnipeg Free Press</a>, by Holly Harris<br>Manitoba Chamber Orchestra; Earl Stafford, guest conductor<br>Pembina Trails Voices; Ruth Wiwchar, Director<br>Westminster United Church; Tue, Mar 12<br>Attendance: 764<br>Four stars out of five<br>&nbsp; 
							</p>
							<p>
								Out of the inferno of the Holocaust have come many inspiring stories of courage, strength, survival, and defiance. <i>The Diary of Anne Frank</i>, published in 1947, still resonates as an extraordinary tale of resilience. 
							</p>
							<p>
								The Manitoba Chamber Orchestra presented the seemingly innocent <i>Brundib&aacute;r</i> (The Bumblebee) Tuesday night, a poignant children&rsquo;s opera penned by Jewish Czech composer Hans Kr&aacute;sa that is a charming fairy tale and ethical manifesto dealing with the horrors of fascism. 
							</p>
							<p>
								Winnipeg&rsquo;s premi&egrave;re children/youth vocal ensemble, Pembina Trails Voices, performed the 40-minute work with youthful energy, impeccably prepared by artistic director Ruth Wiwchar with efficient stage direction by Donna Fletcher. Notably, Wiwchar also led the orchestra situated in front of the stage area, stepping in for MCO&rsquo;s Anne Manson, who had become ill at the last minute. 
							</p>
							<p>
								The story, based on Adolf Hoffmeister&rsquo;s libretto, is simple. First performed in its orchestral version in 1943 at the Theresienstadt concentration camp, it tells the tale of young Aninka and Pepicek, whose ailing mother needs milk to get well. Wicked organ grinder Brundib&aacute;r bullies them after they imitate his singing to raise money. Their fantastical friends, Dog, Cat, and Sparrow, joined by Ice Cream Seller, Baker and Milkman offer to help by singing a lullaby that earns them donations from passers-by. After Brundib&aacute;r steals their money, the children chase him down before reuniting to sing a victory song that celebrates the bonds of friendship in staring down adversity. 
							</p>
							<p>
								This moving opera has been performed in the city before; last seen as a touring production by Saskatoon Children&rsquo;s Choir in June 2006. But whether one sees the opera for the first time, or many times, it&rsquo;s safe to say it will always pack an emotional wallop of sheer magnitude, literally giving voice to the ghetto&rsquo;s children who were murdered days after each of the original 55 performances, as well as serving as a living testament to the power of art. Even its unusual orchestration reflects harsh origins: scored for the 12 musicians available &mdash; and still alive &mdash; in the camp. 
							</p>
							<p>
								Sung in English, all 78 children, and especially the 10 leads, performed with a sense of conviction their crystal-clear diction ensuring every word was heard. Their sweetly harmonized duets, trios, quartets and quintets interspersed throughout the show proved particularly heart-warming. 
							</p>
							<p>
								The youthful cast is also to be commended for carrying on like true, seasoned pros when several sound issues inadvertently arose. Bravo to all, and especially to the tireless Wiwchar for bringing this important work to life, which elicited both laughter and tears in the audience of 764. 
							</p>
							<p>
								The concert also included Stravinsky&rsquo;s <i>L&rsquo;Histoire Du Soldat</i> (The Soldier&rsquo;s Tale) featuring Winnipeg actor Arne MacPherson, who spun his Faustian tale as a master storyteller. Guest conductor Earl Stafford &mdash; again, filling in on scant notice &mdash; led the seven-piece ensemble through Stravinsky&rsquo;s rhythmically treacherous, episodic score with precision and fearless pluck. 
							</p>
							<p>
								Kudos to MCO concertmaster Karl Stobbe for his solo passages packed with double and triple stops, especially during <i>Three Dances</i>, which morphs into a tango, waltz, and ragtime dance. Other highlights included <i>Royal March</i> with trumpeter Guy Few tossing off complex riffs like child&rsquo;s play, and MacPherson&rsquo;s animated, biting delivery during <i>The Devil&rsquo;s Song</i>. 
							</p>
