							<h4>
								Review:<br />
								Light &amp; bright 
							</h4>
							<p class="fineprintdark">
								<a href="http://www.winnipegfreepress.com/">Winnipeg Free Press</a>, by Gwenda Nemerofsky<br />
								Manitoba Chamber Orchestra; Anne Manson, conductor; Janina Fialkowska, piano<br />
								Westminster United Church; Tue, Feb 19<br />
								Attendance: 672<br />
								Four stars out of five<br />
								&nbsp; 
							</p>
							<p>
								She&rsquo;s given performances the world over with the most famous orchestras, she&rsquo;s recorded more than 15 CDs and she recently received the Governor General&rsquo;s Performing Arts Award for lifetime achievement in classical music. But when renowned Montreal-born pianist Janina Fialkowska takes the stage, there is an honest demureness that endears her to her audience. 
							</p>
							<p>
								On Feb. 19, she performed Mozart&rsquo;s Piano Concerto No. 12 in A major, K414 with the Manitoba Chamber Orchestra under the baton of Anne Manson. 
							</p>
							<p>
								This is a lovely, lyrical concerto and Fialkowska introduced the opening melody with a refreshingly natural simplicity, well enunciated, with limpid ornaments. She made phrases sing and burble with uplifting joyfulness. Her fingers seemed to barely alight on the keys, as she maintained a light brightness throughout the Allegro. 
							</p>
							<p>
								Fialkowska approached the stately beginning of the Andante with thoughtful patience. Ideas rolled out clearly in her distinct, deliberate style &mdash; not overdone, which is true to the score. Echoes of the Allegro wove in and out. 
							</p>
							<p>
								The Allegretto was gently playful and understated. Fialkowska&rsquo;s pure artistry made it all seem so easy. She brought Mozart&rsquo;s voice to our ears through the magic of her hands. The audience provided rousing applause, persuading Fialkowska to play a stunning Chopin ringer as an encore. 
							</p>
							<p>
								Gary Kulesha&rsquo;s 1985 <i>Serenade for String Orchestra</i>, written for the MCO, marked a departure from his usual atonal compositional style. Four movements long, it opened with dense, intense harmonies, then mellowed into pleasing tunefulness, rich with assertive cello and bass lines. The Scherzo was spirited, syncopated and dance-like and the Romance was true to its name, albeit a bittersweet take. Smooth, long phrases were highlights, while the high-pitched insect-like violin section was a little hard on the ears. Manson imbued a sense of power and authority to the majestic Finale. 
							</p>
							<p>
								The evening began with the charming <i>Born by the River</i> by Winnipeg-born composer, cellist and university professor Karen Sunabacka. She introduced the piece, explaining the influence and inspiration she gained from her M&eacute;tis grandmother. Strains of the fiddle tune <i>The Old French</i> were evident immediately, as the work captured its liveliness and humour. Use of momentary silences and pizzicato grabbed our attention. 
							</p>
							<p>
								The night wrapped up with an energetic reading of Haydn&rsquo;s Symphony No. 82 in C Major &mdash; &lsquo;The Bear.&rsquo; After a brief audience lesson from Manson in the construction of the first movement, the MCO, bolstered by woodwinds and timpani, launched into the Vivace with plenty of oomph. The French horns had a few difficulties with their upper notes and the Menuetto and Trio was a bit heavy-handed, but you couldn&rsquo;t help but warm to the sprightly finale with its unmistakable bagpipe-like drone in the cellos and basses. A great effort all-round. 
							</p>
