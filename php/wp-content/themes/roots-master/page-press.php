							<h4>
								Review:<br />
								Pianist&rsquo;s age belies his virtuosity<br>in MCO season opener 
							</h4>
							<p class="fineprintdark">
								<a href="http://www.winnipegfreepress.com/">Winnipeg Free Press</a>, by Gwenda Nemerofsky<br />
								Manitoba Chamber Orchestra; Jan Lisiecki, piano<br />
								Westminster United Church; Wed, Sep 11<br />
								Attendance: 810<br />
								Four stars out of five<br>&nbsp; 
							</p>
							<p>
								If you&rsquo;d closed your eyes during the perform&shy;ance of Grieg&rsquo;s Piano Concerto in A Minor at the Manitoba Chamber Orchestra&rsquo;s season-opening concert Wednesday night, experience and conventional wisdom would have made you picture the soloist as a middle-aged piano virtuoso. Such confidence, attack, technical fluidity and enchanting sensitivity could only come from a decades-seasoned performer. 
							</p>
							<p>
								Instead, the guest artist was a blond-haired, fresh-faced lanky young man. 
							</p>
							<p>
								But appear&shy;ances don&rsquo;t tell all. Eighteen-year-old Calgary native Jan Lisiecki has been playing with sym&shy;phony orches&shy;tras since he was 10 and gives 100 con&shy;certs around the world each year. 
							</p>
							<p>
								He had the audience in the palms of his long-fingered hands from Grieg&rsquo;s familiar dramatic opening. Runs rippled from his fingers and the first movement&rsquo;s lengthy cadenza grew from pensive to theatrical in its building excitement, showing Lisiecki&rsquo;s extraordinary power and commitment. 
							</p>
							<p>
								He puts his heart into his playing and while he could have approached some early passages with more patience, one can chalk it up to youthful enthusiasm. 
							</p>
							<p>
								The MCO, under the direction of Anne Manson, was playing an arrangement for smaller orchestra by Canadian composer Michael Oesterle. This left many sections rather exposed &mdash; with mixed results. Unchanged was the lovely horn solo at the beginning of the second movement, played to perfection by principal horn Patricia Evans. 
							</p>
							<p>
								Lisiecki&rsquo;s feather-light touch blossomed into the simply pretty theme of the adagio, ruffles here and running lines there, before the sturdy melody emerged, played with assertive pomp. 
							</p>
							<p>
								He began the final movement playfully, but with a serious intensity, a little military flair fused with folk-tune touches. We glimpsed Lisiecki&rsquo;s gentler side in the lyrical middle section, to which he gave a romantic flow, singing with lovely understatement. This was pristinely and impressively sensitive playing. 
							</p>
							<p>
								And then, we were back to the folk dance, skipping and hopping, until Lisiecki pulled out all the dramatic stops, milking Grieg&rsquo;s music to the hilt for an audience-stirring ending. 
							</p>
							<p>
								A unanimous standing ovation brought him back for an encore: Grieg&rsquo;s delicate Arietta from his Lyric Pieces. We stood and clapped again &mdash; not just for the evening&rsquo;s inspiring performance but for the many performances to come by this special young artist. 
							</p>
							<p>
								The evening started with Bartok&rsquo;s Rumanian Folk Dances, seven quick little spirited pieces that brought the countryside right into the church hall. Concertmaster Karl Stobbe was especially nimble in his many solo sections. 
							</p>
							<p>
								The MCO reprised its popular recording of Philip Glass works with a performance of Symphony No. 3. The highlight of this was the third movement, which opened with a delicious sense of anticipation from the viola, cello and bass lines. It was nice to see Theodore Chan, who played on the recording and who is now with the Toronto Symphony Orchestra, back to handle the bass solos full of gorgeous growls. 
							</p>
							<p>
								Stobbe&rsquo;s impassioned violin solo was picked up seamlessly, first by Kerry DuWors, who kept the feeling alive with lovely legato, and then by Elation Pauls, who carried well. The blend of all the voices in the orchestra was transfixing. 
							</p>
							<p>
								A solid start to the new season. 
							</p>
