							<h4>
								Singer Bayrakdarian illustrates<br />
								how music transcends language 
							</h4>
							<p class="fineprintdark">
								<a href="http://www.winnipegfreepress.com/">Winnipeg Free Press</a>, by Gwenda Nemerofsky<br />
								Manitoba Chamber Orchestra; Isabel Bayrakdarian, soprano<br />
								Westminster United church; Wed, Sep 12<br />
								Attendance: 604<br />
								Four stars out of five<br />
								&nbsp; 
							</p>
							<p>
								If you ever doubted that music could transcend language, Wednesday night&rsquo;s Manitoba Chamber Orchestra (MCO) concert would have changed your mind. 
							</p>
							<p>
								Guest artist, spectacular Armenian-Canadian soprano Isabel Bayrakdarian sang in Greek and Armenian. Audience members were provided lyric sheets with translations, but who wanted to read fussy bits of paper when Bayrakdarian was onstage? This musical treasure told us everything we needed to know with her limpid voice, impassioned facial expressions and graceful gestures. No words necessary. 
							</p>
							<p>
								The concert was introduced by the always debonair CBC Radio 2 personality, Bill Richardson. Conductor Anne Manson was at the helm for this 40th anniversary season opener. 
							</p>
							<p>
								<strong>Gourmet candy</strong> 
							</p>
							<p>
								Bayrakdarian began with <em>Five Greek Songs</em> by Maurice Ravel, arranged by her husband Serouj Kradjian. It was evident from the singer&rsquo;s appearance that the couple is expecting an addition to their family soon, but this did not affect her gorgeous voice. The song cycle resembled a box of gourmet candy, each piece more deliciously tempting than the last. 
							</p>
							<p>
								<em>Song of the Bride</em> was immediately lilting, full of colour and power. <em>Yonder, Near the Church</em> had a somewhat melancholy tenor, rendered with long seamless phrases that were satisfyingly assertive. Bayrakdarian is so communicative, possessing a unique style that makes you feel that she is singing right to you. The soulful <em>Song of the Lentisk Gatherers</em> displayed her purity of tone, full of emotion and subtle inflection. And <em>All Merry!</em> was a dance in song form, bursting with playfulness. 
							</p>
							<p>
								The addition of the separate Ravel song, <em>Tripatos</em> was joyous fun, Bayrakdarian almost laughing as she sang. 
							</p>
							<p>
								She also performed 18th-century Armenian composer Sayat-Nova&rsquo;s <em>Four Songs</em>, arranged by Kradjian. One couldn&rsquo;t help but be struck by the modern tunefulness of the poignant first song <em>Kani Voor Janim</em>, Bayrakdarian&rsquo;s voice carrying effortlessly through the hall. Versatile concertmaster Karl Stobbe matched her emotion in his solo violin passages in <em>Blbooli Hit</em>, which was steeped in sorrow, his sensitive playing well-controlled. We felt we had travelled back in time to an earlier Armenia. The spirited Kamancha was an ode to the Armenian bowed instrument. Bayrakdarian&rsquo;s weighty voice crafted this beautifully, deftly shaping phrase endings, imbuing it with personality. You really couldn&rsquo;t ask for more. 
							</p>
							<p>
								The concert ended with Manson and the MCO whisking us off to Dvo&#345;&aacute;k's Czech countryside in a rousing performance of his elegantly substantial <em>Serenade for Strings</em>. 
							</p>
							<p>
								This is a work that shows the composer's love of the instruments &mdash; and he gives everyone a chance to shine. Overall, the musicians made the most of the opportunity, sweeping us up in the introductory melody with smooth bowing and effective nuance. The tempo di valse was wonderfully textured, only marred by a few stray violins letting their colleagues down with squeals and untidy notes. But the scherzo brimmed with life, violins scampering brightly and the finale was suitably upright and urgent. A nice start to the season. 
							</p>
