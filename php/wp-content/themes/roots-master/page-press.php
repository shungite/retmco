							<h4>
								Pianist Hamelin<br />
								like fine wine in MCO concert 
							</h4>
							<p class="fineprintdark">
								<a href="http://www.winnipegfreepress.com/">Winnipeg Free Press</a>, by Holly Harris<br />
								Manitoba Chamber Orchestra; Anne Manson, conductor; Marc-Andr&eacute; Hamelin, piano<br />
								Westminster United church; Wed, Jan 9<br />
								Attendance: 765<br />
								Four and a half stars out of five<br />
								&nbsp; 
							</p>
							<p>
								Many talented young artists abound but there is nothing like hearing well-honed artistry aged like fine wine. 
							</p>
							<p>
								World-class pianist Marc Andr&eacute; Hamelin wowed the crowd Wednesday night as the featured guest soloist for the Manitoba Chamber Orchestra&rsquo;s latest concert. The program led by MCO music director/conductor Anne Manson included, unusually, not one, but two piano concertos, as well as a pair of contemporary works. 
							</p>
							<p>
								Now based in Boston, the acclaimed Montreal-born musician marked his debut on the MCO stage in 1991, with his last appearance here in 2010. His rapport with both the orchestra and its loyal audience is palpable. Every time the Juno-winning artist comes to town, he&rsquo;s welcomed to the stage like a rock star, eliciting loud cheers even before he&rsquo;s tickled a single ivory. 
							</p>
							<p>
								Shostakovich&rsquo;s Concerto in C Minor for Piano, Trumpet and String Orchestra, Op. 35 is one of those great works one never tires of hearing. Hamelin pounced on his keyboard during the first movement allegretto's declamatory opening, followed by a solemn opening theme. He established at the onset this would be a take-no-prisoners performance, with his virtuosic technique easily handling the sparkling runs that erupt into brilliant octave passages. His cadenzas, courtesy of fabled Polish pianist Wanda Landowska, were effective, although one longed to hear how Hamelin &mdash; himself a composer &mdash; would have treated these showcase moments. 
							</p>
							<p>
								Canadian guest trumpeter Guy Few, poised standing at the opposite side of the stage, interpolated his own sardonic commentary, spinning long sotto voce phrases with an impressive breath control and nerves of steel. His muted solo during the world-weary Lento proved to be particular highlight. 
							</p>
							<p>
								After the ever-curiously short moderato that segues into the allegro con brio, Hamelin unleashed the fireworks, driving his piano &mdash; and the orchestra &mdash; forward like a runaway train. Always in supreme control, the soloist's vigorous finale including whipping glissandi and vaulting chords made this a performance that won&rsquo;t soon be forgotten. 
							</p>
							<p>
								As expected, the crowd of 765 leapt to their feet, demanding three curtain calls from the two guest artists. In response, Hamelin delivered a witty encore of Chopin's Minute Waltz Op.64 No.1 that begins traditionally but morphs into a dissonant variant. 
							</p>
							<p>
								Hamelin also performed Haydn&rsquo;s Piano Concerto in D Major, which brims with sunny optimism, if not overly technical demands. Still, you could sense the pleasure that he gets from playing this light-hearted music. 
							</p>
							<p>
								The concert opened with the world premi&egrave;re of <em>Virtuosities</em> (2012) by Vancouver-based composer Dorothy Chang, who was in attendance. The work was commissioned by the MCO as part of its ongoing 40th anniversary celebrations. Its three movements &mdash; <em>To dream, perchance to fly</em>, <em>Souvenir</em> and <em>Mechanica</em> &mdash; displayed Chang's ability to craft clearly structured, intensely focused works. 
							</p>
							<p>
								Spanish composer Jos&eacute; Evangelista&rsquo;s Concerto Con Brio teemed with baroque influences, with its florid central movement <em>M&eacute;lismatique</em> an evocative highlight. 
							</p>
