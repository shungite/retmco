      <p>
      <b>ANTONIN DVO&#344;&Aacute;K: SEXTET IN A MAJOR, OP. 48</b>
      </p>

      <p>
      Antonin Dvo&#345;&aacute;k (1841-1904) composed the Sextet in A major, Op. 48 in 1878. It was premiered as a private performance at the home of the renowned violinist Joseph Joachim. This marked the first time that Dvo&#345;&aacute;k&rsquo;s work had been heard outside of Bohemia (now Czechoslovakia), his home. The work was extremely well received and was the first of his compositions to enjoy international success.
      </p>
      <p>
      The sextet was written during a period of composition that is known as Dvo&#345;&aacute;k&rsquo;s Slavic period. During this time, Dvo&#345;&aacute;k&rsquo;s writing featured Slavic folk elements which can be heard throughout the four movements of the Sextet.
      </p>

      <div class="well">
        <p><b>FOR EDUCATORS</b></p>
