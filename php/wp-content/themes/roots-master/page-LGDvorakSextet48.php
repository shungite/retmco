      <p>
      <b> The following questions can help you listen to this work: </b>
      </p>

      <p>
      <span class="badge">1</span> <b>Form</b> in music refers to the musical architecture or the way the music is structured. Can you hear that the Sextet in A major is structured in four movements?  They are:
      </p>
      <ol>
        <li>Allegro moderato (moderately lively and quickly).</li>
        <li>Dumka. Poco allegretto (A Dumka folk song played slightly moderately fast).</li>
        <li>Furiant. Presto (An extremely fast Bohemian dance).</li>
        <li>Finale. Tema con variazioni. Allegretto grazioso, quasi andantino (Theme and Variations, fairly fast and gracefully, almost at an andantino tempo).</li>
      </ol>

      <p>
      Each of the four sections or movements also has a particular form. The form of the Allegro moderato first movement of the Sextet is the classic <b>sonata form</b> structure that contains: a first section called <b>exposition</b> that presents the main themes (in this example, 3 distinct theme groups can be heard); a middle <b>development</b> section where the themes are varied and developed; and a final <b>recapitulation</b> section that re-states the main themes.
      </p>
      <p>
      The first theme is presented right at the beginning of the first movement. Can you hear a quiet, gentle theme marked at the outset by repeated notes?  The second theme can be identified by a melodic leap followed by an off-beat three note rhythmic figure that creates an accent on beat 4, next followed by four notes walking upwards and then turning downwards for two notes.  A short closing theme occurs at a section marked molto tranquillo (very calmly) that contains two long notes followed by a descending scale passage.
      </p>

      <p>
      In the development section where Dvo&#345;&aacute;k develops the main melodies, can you hear the three note off-beat rhythmic pattern of the second theme throughout this section?
      </p>

      <p>
      Can you hear the main themes return again in the recapitulation section? Can you hear how the second theme is varied and moves through many different tonalities so that it sounds like another development section contained within the recapitulation?
      </p>

      <p>
      Dvo&#345;&aacute;k used a ballad type folk song form called &ldquo;Dumka&rdquo; for his second movement.  Dvo&#345;&aacute;k used this song-form in other compositions he wrote and generally the dumkas featured contrasts between melancholy themes and more joyous ones. In this work you will hear that the opening main subject or theme is melancholy.  Can you hear that the second theme, identified by contrasting Hungarian melodic sounds, is still a melancholy theme?
      </p>

      <p>
      The third movement is marked &ldquo;Furiant&rdquo;. A Furiant is a very fast and fiery Bohemian/Czech folk dance with shifting accents and alternating between 2/4 and 3/4 time. However this particular version of &ldquo;Furiant&rdquo; does not feature the alternating 2 and 3 beat rhythms and it also lacks a fiery nature. Can you hear that the structure of this movement is an A B A scherzo and trio form where the outer sections are the same or similar and the middle section is a contrasting one?
      </p>

      <p>
      The fourth movement takes the form of a theme and variations.  After the theme is stated in the opening of the fourth movement, can you hear how it is varied throughout this movement?  Dvo&#345;&aacute;k numbers the different variations in the music &mdash; can you hear how many different variations of the main theme Dvo&#345;&aacute;k composed for this movement?
      </p>

      <p>
      <span class="badge">2</span> The Sextet Op. 48 was written for 2 violins, 2 violas, and 2 cellos. Can you distinguish between these instruments as you hear them playing? Can you hear the instruments trading melodies back and forth? This style of music featuring independent melodic lines relating with each other is called <b>contrapuntal</b>. When do you hear individual instruments or sections and when do you hear the sextet all playing together? What instruments are given predominant motifs heard in this work?
      </p>

      <p>
      <span class="badge">3</span> Dvo&#345;&aacute;k uses <b>expressive elements</b> (musical elements that express certain feelings or dispositions), varying tempos (the speed of the music), and <b>Dynamics</b> (the volume of the music) to create interest.  Which movements use louder music?  Which movements use quieter music?
      </p>

      <p>
      When do the dynamics change dramatically and what effect does that create?  When does the music get suddenly louder (crescendo) or quieter (decrescendo), for example, in the Furiant?  Also in the Furiant can you hear loud accents that contrast with very quiet sections?  How does that affect the mood of the work?
      </p>

      <p>
      Which movements are faster or slower in tempo than others? Which movement has the fastest tempo of all? Can you hear contrasting fast and slow tempos in the Dumka second movement?
      </p>

      <p>
      Can you hear contrasts between staccato (short sounds) and legato sections (flowing, connected sounds)? Can you hear places where the players pluck the strings (pizzicato)?
      </p>

      <p>
      <span class="badge">4</span> How is <b>rhythm</b> used in this Sextet? Many of the rhythms of Bohemian/Czech folk dances and songs are heard in this work and there is considerable rhythmic variation.  Do you hear dotted note patterns (long then short sounds)?  Do you hear off beat rhythms that create the effect of an accent on beat four?  Can you pick out the three note off-beat rhythmic pattern of the second theme heard throughout the development section of the first movement?  Do you hear even rhythmic patterns?  Uneven long short long short patterns?
      </p>

      <p>
      <span class="badge">5</span> How is <b>melody</b> or <b>pitch</b> used in this Sextet?  Many of the wealth of melodies featured in this work are taken from Bohemian or Czech folk tunes &mdash; can you hear those qualities in the melodies in this work? Dvo&#345;&aacute;k is known for his beautiful melodies. Can you pick out parts of these melodies that can be easily sung or hummed?
      </p>

      <p>
      Can you hear melodies and melody fragments repeated and combined in different ways? Do you hear high sounds or pitches?  Do you hear low sounds or pitches? Can you hear when the melody notes are repeated or moving up or down in scale like patterns?  Can you hear when melody notes jump higher or lower in pitch?
      </p>

      <p>
      Examples of different melodic treatment can be heard in the three themes. The first four melodic notes of theme 1 following an initial upbeat note are repeated.  By contrast, the first two notes of the second theme are a leap of a fifth.  The second theme continues with other leaps and then in the development section these leaps become even larger &mdash; can you hear large 8 notes melodic leaps?  An 8 note interval is called an octave.  The first two long notes of the third theme are a rising skip apart (an interval of a third) followed by a descending scale pattern ending in a leap down.
      </p>

      <p>
      <span class="badge">6</span> What different kinds of <b>textures</b> do you hear in this work?  Do you hear how Dvo&#345;&aacute;k creates rich sonorities with the instruments of the sextet?  By adding a second viola and cello to the traditional string quartet configuration, Dvo&#345;&aacute;k created rich tone colours and colourful harmonies, borrowing from sounds found in Slavic dances and folk-songs.
      </p>

      <p>
      <span class="badge">7</span> What kinds of instrumental <b>timbre</b> do you hear? <b>Timbre</b> is the different qualities of sound that can be heard, for example the kinds of sounds that the string instruments make when they play together.  When does the timbre of the music change because certain instruments are added or taken away?  How is the timbre of this work where all the instruments are string instruments contrast to timbres in the Beethoven octet where the instruments are oboes, clarinets, bassoons, and horns?
      </p>

