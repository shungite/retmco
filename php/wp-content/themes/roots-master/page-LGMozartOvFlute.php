      <p>
      <b>
        The following ideas and questions can help you listen to this work:
      </b>
      </p>

      <p>
      <span class="badge">1</span> <b>Form</b> in music refers to the musical architecture or the way the music is structured. The opera <i>The Magic Flute</i> is structured in 2 acts with an opening, introductory, instrumental <b>overture</b>. The <b>overture</b> played before the curtain opens sets the stage for the action and music to follow and helps the audience emotionally prepare for the opera. In Mozart&rsquo;s time the overture did not always contain reference to melodic themes heard in the rest of the opera but the slow introduction to this overture does contains three chords that are heard again in the opera at significant points.
      </p>
      <p>
      The <b>form</b> of the <b>Overture</b> to <i>The Magic Flute</i> is the classic three section sonata form structure: a first section called <b>exposition</b> that presents 2 themes; a middle <b>development</b> section where the themes are varied and developed; and a final <b>recapitulation</b> section that re-states the main themes.
      </p>
      <p>
      Can you hear the long, slow stately, introduction that Mozart uses to preface the exposition of the overture? This introduction foreshadows the priest&rsquo;s solemn processional and prayer to come later in the opera.
      </p>
      <p>
      After the slow introduction can you hear a fast (allegro) theme that begins quietly and then builds in intensity, suggesting the playful and light-hearted comedic aspects of the opera? Then can you hear a second, contrasting and complementary theme in a new key (B flat major) introduced by a series of ascending scales and played out as call and answer between the woodwinds?
      </p>

      <p>
      Can you identify when the development section of this movement begins? There are musical clues that can help you. The beginning of the development is marked by variations of the three slow, solemn chords heard at the opening of the slow introduction to this overture.  After the slow, stately chords, listen to how Mozart develops the main themes in different ways and through various keys and modulations throughout the middle development section of this movement.  Can you hear when Mozart restates the main themes from the exposition with some variation in the recapitulation section at the end of the movement?
      </p>

      <p>
      This kind of compositional development is characteristic of sonata form, which was typically used by Mozart for his later overtures.
      </p>

      <p>
      <span class="badge">2</span> What <b>Instruments</b> can you hear in this arrangement of Mozart&rsquo;s overture to <i>The Magic Flute</i>? Mozart originally scored this overture for: strings; two each of flutes, oboes, clarinets, bassoons, horns and trumpets; three trombones; and timpani.  In this arrangement for wind octet can you hear two each of oboes, clarinets, bassoons, and horns?
      </p>
      <p>
      Can you hear the wind instruments trading parts and calling and answering to each other throughout the overture?
      </p>
      <p>
      <span class="badge">3</span> What <b>tempos</b> (the speed of the music) can you identify in this work? Can you hear a slow tempo at the beginning of the overture followed by an energetic Allegro (fast and lively)?  What <b>dynamics</b> (the volume of the music), and <b>expression</b> (musical elements that express certain feelings or dispositions) are used throughout this overture? Does the overture begin with loud or quiet dynamics?
      </p>

      <p>
      What is the overall mood of this overture and how does Mozart achieve this mood?  Do you hear the contrasts between the serious, solemn mood at the beginning and middle of the overture and the light-hearted, playful qualities of the Allegro?  How does Mozart achieve this contrast of mood?
      </p>

      <p>
      Was the music played at all the same volume (dynamics)?  When do you hear music played loudly? Quietly?  When do the dynamics change and what effect does that create?  When does the music get louder (crescendo) or get quieter (decrescendo)?
      </p>

      <p>
      Do you hear musical sounds that are short sounding (staccato) or very smooth sounding (legato)?  Which movement features legato sounds? Where do you hear suddenly loud sounds (accents)?
      </p>

      <p>
      <span class="badge">4</span> How is <b>rhythm</b> used in this overture? Do you hear sections that have a steady rhythm or beat that you could tap to? Did any particular rhythmic pattern stand out for you that you could listen for in the work?  Was there a particular pattern that you could hear repeated anywhere in the work?
      </p>

      <p>
      Can you hear three different heavy, solemn chords that begin the slow introduction to the overture? The first chord is a long sound (half note) followed by a rest (no sound). Then a second chord is heard twice in a short long pattern followed by a rest. Finally a third chord is heard also heard twice in a short long pattern followed by a rest.
      </p>

      <p>
      The number three has special significance in this overture and opera and is one of several Masonic references that various musicologists have identified in this work. Mozart was a member of the order of Masons and Masonic influences and ideas may be heard in his later works. The number three has mystical properties for the order of Masons and references to that ritualistic number abound in <i>The Magic Flute</i>, for example the three chords at the beginning of the overture, the three temples, ladies, and boys in the opera, and the three flats in the key signature (E flat major).
      </p>
      <p>
      Some analysts have compared the sounds of the opening three chords of the overture to the sounds of Masonic candidates who knock three times at the door in Masonic initiation ceremonies. However, there is nothing in Mozart&rsquo;s writings or correspondence that either confirm or deny this possibility. Whether or not they are Masonic references, these sounds certainly create a feeling of importance and create a serious beginning and sense of anticipation for this opera. Can you hear the repeated 3 chords from the beginning of the overture anywhere else in this opening work (listen around the middle of the overture just before the development section)?
      </p>
      <p>
      After the slow introduction, the first theme contains a repeated pattern. Pat a beat and say the words &ldquo;running, running, running everyone is running, running, running, everyone is running, etc,&rdquo; and be sure to say the words &ldquo;everyone is&rdquo; twice as fast as you say the words &ldquo;running.&rdquo; As you pat four steady beats you should be able to say &ldquo;running&rdquo; to each of the first three beats and then say &ldquo;everyone is&rdquo; while you pat the fourth beat. Can you find the sounds of those words in the overture and traded back and forth between instruments?
      </p>
      <p>
      The second theme is a contrasting rhythmic pattern that sounds like &ldquo;don&rsquo;t make me have to run so fast don&rsquo;t make me have to run so fast&rdquo; also traded between the instruments.  Where can you hear this rhythmic pattern?
      </p>
      <p>
      Can you hear any other repeated rhythmic patterns in the overture?
      </p>

      <p>
      <span class="badge">5</span> How is <b>melody</b> or <b>pitch</b> used in this overture?
      </p>

      <p>
      Do you hear high sounds or pitches?  Do you hear low sounds or pitches? Can you pick out a particular melody played by certain woodwind instruments? Can you hum or sing that melody? Could you draw the shape of the melody in the air?  When does it move up higher and when does it move lower?
      </p>

      <p>
      Can you hear that each of the opening three chords are played higher than the previous chord?  Can you hear the repeated notes in the &ldquo;running running running&rdquo; pattern of the first theme?  The same note is heard 6 times in a row before it moves up and down on the &ldquo;everyone is&rdquo; part of the rhythmic pattern. Can you hear the same pattern played at a higher register?
      </p>

      <p>
      Can you hear these repeated notes traded back and forth between instruments?  Can you hear parts of the melody introduced in one instrument (called voice) and then imitated at different pitches and heard in other voices or instruments?  This is called contrapuntal writing and is used in a musical form called a <b>fugue</b>.  Mozart uses fugal writing in this overture when the theme is interwoven between parts so that the same short melody or phrase is imitated by different voices or instruments or heard coming at different points by different instruments. The word fugue comes from the Latin word fuga meaning chase or flight.  Can you hear how different voices or instruments chase or run from each other?
      </p>

      <p>
      Can you hear that the second theme is not made up of repeated notes but instead, is a melody that includes ascending and descending scale like passages?
      </p>

      <p>
      <span class="badge">6</span> Can you hear different kinds of <b>texture</b> in this work?  Where do you hear all the instruments playing together so that the sound is thick? (Think of the opening chords of the overture).  The three slow opening chords of the overture introduce the key of the piece and consist of the main tonic chord (I), subdominant (IV), and dominant (V), chords of the key of E flat major.  They are referred to elsewhere in the opera, particularly related to the temple and Sarastro, the High Priest.
      </p>

      <p>
      Where do you hear a thinner texture where just a few instruments playing or taking turns?  Can you tell which instruments are playing?
      </p>

      <p>
      <span class="badge">7</span> What kinds of instrumental <b>timbre</b> do you hear? <b>Timbre</b> is the different qualities of sound that can be heard. For example, the timbre of the oboe is different to the timbre of the clarinet, bassoon, or horn.  Can you pick out each of those instruments when they are playing just by listening to their individual timbres?
      </p>


