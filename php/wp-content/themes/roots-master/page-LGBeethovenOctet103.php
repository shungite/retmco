      <p>
      <b>
        The following questions can help you listen to this work:
      </b>
      </p>

      <p>
      <span class="badge">1</span> Form in music refers to the musical architecture or the way the music is structured. The form of the Octet in E flat major is typical for classical string quartets and symphonies, although the treatment of the menuetto as more like that of a scherzo was certainly forward looking. The Octet in E flat major is structured in four movements:
      </p>

      <ol>
        <li>Allegro (fast and lively)</li>
        <li>Andante (moderately slowly, walking pace);</li>
        <li>Menuetto and Trio (A minuet, in this case, more like a scherzo which is also in &frac34; time but more lively and playful in nature than a minuet);
          IV. Presto (very fast)</li>
      </ol>

      <p>
      Each of the four sections or movements also has a particular form.  The first movement Allegro is written in sonata form.  Sonata form is made up of three sections: a first section called <b>exposition</b> that presents 2 themes; a middle <b>development</b> section where the themes are varied and developed; and a final <b>recapitulation</b> section that re-states the main themes.
      </p>

      <p>
      The first and second themes are not highly contrasting but can be distinguished by instrumentation. Can you hear the lively theme 1 in the oboe part and a second theme stated in the bassoon and clarinet sections?
      </p>

      <p>
      Can you hear how Beethoven explores many ways to vary these themes in the development section?  And can you hear a new motive played by the clarinet in this section? Can you hear how all the instruments restate the two main themes in the recapitulation section?
      </p>

      <p>
      The Andante movement is in a song-form or ABA structure where the first and second parts of the movement are the same or similar, and the middle part is a contrasting section. Can you hear the beautiful main theme expressed first in the oboe part? This andante could also be described as a duet.  Can you hear the main theme sung between the oboe and bassoon to create the effect of an operatic duet between soprano and baritone?
      </p>

      <p>
      The form of the Minuetto and Trio is also an ABA structure.  However, this particular minute was innovative for its time, since it is one of Beethoven&rsquo;s first scherzos.  The scherzo is more lively and playful than the stately ¾ minuet court dance.  Can you hear the dynamic contrasts of the scherzo? In the middle trio can you hear a dialogue between the clarinet and horns?
      </p>

      <p>
      The Presto finale is a rousing finish to this octet. It takes the form of a rondo-sonata. In rondo form, a main theme is repeated before and after contrasting sections.  The repeated theme is called the A section.  The contrasting sections are called B, C, D etc., depending on how many different sections the composer decides to compose. Sonata form features the statement, development and re-statement of two main themes. In this Presto, what does the main theme sound like to you?  Does it have fanfare qualities? Can you hear the constant variations of the main theme throughout the Presto?  Does the second theme remind you of any other melody—perhaps Beethoven&rsquo;s <i>Hymn of Joy</i>? Can you hear a coda or conclusion that features virtuosic writing for the horns? Can you hear special improvisatory parts for the oboe and clarinet?
      </p>

      <p>
      <span class="badge">2</span> The Octet is written for 2 each of oboes, clarinets, bassoons, and horns. Can you distinguish between these instruments as you hear them playing? Listen for the oboe playing the first theme in the opening Allegro.  Can you hear the oboe&rsquo;s theme passed all around the different instruments of the ensemble?   Can you hear the oboe playing a soloistic part at the beginning of the second movement and the answering duet by the bassoon? Can you hear the instruments trading melodies back and forth? When do you hear individual instruments or sections and when do you hear the octet playing together? What other instruments are given predominant motifs heard in this work?
      </p>

      <p>
      <span class="badge">3</span> Beethoven uses <b>expressive elements</b> (musical elements that express certain feelings or dispositions), varying <b>tempos</b> (the speed of the music), and <b>Dynamics</b> (the volume of the music) to create interest.  Which movements use louder music?  Which movements use quieter music?  Which movements are faster or slower than others? Which movement has the fastest tempo of all?
      </p>

      <p>
      When do the dynamics change dramatically and what effect does that create?  When does the music get suddenly louder (crescendo) or quieter (decrescendo)?
      </p>

      <p>
      <span class="badge">4</span> How is <b>rhythm</b> used in this octet?  Are the rhythms lively or stately?  Which movement could you easily tap a steady rhythm or beat to? The rhythmic treatment in this octet mostly follows the traditions of the time. Did any particular rhythmic pattern stand out for you anywhere that you could listen for in the work?  Was there a particular pattern that you could hear repeated anywhere in the work?  Can you hear how the rhythm of the oboe theme in the first movement is repeated throughout that entire movement? Did you hear any instances of syncopation (accent on off beat) in the first movement and in the final Presto movement?  Did you hear a repeated rhythmic pattern in the bassoon part in the first movement?  This is called an <b>Alberti bass</b>.
      </p>

      <p>
      How are beats grouped in each movement?  Can you hear how the beats are grouped in twos (count 1,2,1,2,1,2) in the first movement and grouped in threes (count 1,2,3,1,2,3) in the second movement?
      </p>

      <p>
      <span class="badge">5</span> How is <b>melody</b> or <b>pitch</b> used in this octet?  Do you hear high sounds or pitches?  Do you hear low sounds or pitches? Can you pick out a particular melody in each of the four movements?  Can you hum or sing that melody? Could you draw the shape of the melody in the air?  When does it move up higher and when does it move lower?
      </p>

      <p>
      Do you hear a melody in a certain instrument that is then played by a different instrument?  This is characteristic of music of this time. How does the shape of the melody played by the oboe in the first and second movements differ?
      </p>

      <p>
      <span class="badge">6</span> What different kinds of <b>textures</b> do you hear in this work?  Do you hear how Beethoven uses traditional approaches to harmony and instrumental texture in this work? Listen for how Beethoven creates the effect of steady accompaniment in the lower parts as for example the Alberti and arpeggiated bass parts in the bassoon in the first movement, while the higher instruments play the melodies above. The harmonies in this work heard usually in lower instruments function to support and accompany melodies often heard in the higher sounding instruments.
      </p>

      <p>
      <span class="badge">7</span> What kinds of instrumental <b>timbre</b> do you hear? <b>Timbre</b> is the different qualities of sound that can be heard, for example the kinds of sounds played by different instruments.  What is the difference between an oboe and a horn in terms of timbre?  When does the timbre of the music change because certain instruments are added or taken away? Can you hear different qualities of sound created by different instruments when instruments call and respond to each other?
      </p>

      <p>
      Can you hear a full, rich sound in the first chord at the outset of the octet created by all the instruments playing together followed by a lighter sound and contrasting timbre as the oboe and clarinets play together? Can you hear where Beethoven creates new timbre or tone color by layering instruments in or out, and by altering low and high instrument ranges?
      </p>

      <p>
      Can you hear the contrast in characteristic timbre and color between the clarinet and horns in the trio section of the octet in the third movement? Can you hear how the final movement features instruments in soloistic ways and also combines all the instrumental timbres to create a weave of colour?
      </p>

      <p>
      Listen for how often Beethoven give the soprano line to the oboe, the alto part to the clarinet, the tenor to the horns, and the bass part to the bassoons throughout the octet.
      </p>

