      <p><b>WOLFGANG AMADEUS MOZART: PIANO CONCERTO NO. 26 IN D MAJOR (K 537) &ldquo;CORONATION&rdquo;</b></p>

      <p>
      Wolfgang Amadeus Mozart (1756-1791) finished his second last piano concerto, the D major concerto No. 26 known as the &ldquo;Coronation&rdquo; concerto, in 1788. It was Mozart&rsquo;s publisher, rather than the composer himself who provided the subtitle for this piano concerto. The concerto was possibly titled &ldquo;Coronation&rdquo; as a marketing tool since Mozart performed the concerto around the same time or as part of festivities celebrating the Coronation of Leopold II in Frankfurt on October 15, 1790.
      </p>

      <p>
      The Piano concerto No. 26 is unique among the 27 piano concertos that Mozart wrote. Mozart authorities hold very differing views of this work. It is criticized by some as not having the same quality as previous concertos and in fact as being regressive. However, other authorities view this concert as innovative and looking ahead to the romantic piano concerto, due to the profusion of virtuosic solo writing, stylistic experimentation and innovation, and the loose harmonic and rhythmic structure as compared to the earlier concertos.
      </p>

      <p>
      The Piano Concerto No. 26 in D major was well received during Mozart&rsquo;s time and remains an enduring staple of the Mozart Piano concerto repertoire.
      </p>

