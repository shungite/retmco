      <h4>About the Manitoba Chamber Orchestra</h4>

      <p>The Manitoba Chamber Orchestra&rsquo;s ten-year vision has remained constant: it strives to be recognized as the premier general repertoire chamber orchestra in Canada, admired for its musical excellence, creative audience development and committed, balanced management.</p>
      <p>The Manitoba Chamber Orchestra was established in 1972, and each year presents a nine-concert subscription series in Winnipeg, Manitoba. The Orchestra has earned an excellent reputation for its unique artistic programming, exploring repertoire rarely performed by large symphony orchestras. Their performances have delighted audiences far beyond the Manitoba provincial borders, with over four million listeners each season through the CBC Radio (a partner since 1984) and Classic 107 broadcasts, and national and international audiences for the touring performances.</p>
      <p>The MCO embraces an ambitious touring schedule. Since 2008, the MCO has performed in New York, Boston, San Francisco, Orange County, Vancouver, Toronto, Edmonton, Guelph, Banff, Waterloo, Ottawa, Elora, nine smaller centres in BC, Prince Edward Island, and Brandon. The concerts received enthusiastic responses from audiences and critics alike.</p>
      <p>In November 2008 the MCO appointed Anne Manson as Music Director. Ms Manson is an internationally acclaimed conductor with high artistic standards, who quickly proved to be an excellent leader for the orchestra.</p>
      <p>The MCO has a strong commitment to profiling Canada&rsquo;s up and coming classical stars, both performers and composers. Some of these stars have included: the now internationally acclaimed soprano Measha Brueggergosman, who made her first professional CD with the MCO and is now a recording artist for the prestigious Deutsche Gramophone label; violinist James Ehnes, who also made one of his first orchestral appearances with the MCO and now regularly performs on international stages; and last season, the incredible 20-year-old pianist, Jan Lisiecki, a native and resident of Calgary, who has already made his New York Carnegie Hall debut. These artists, and many more, grace the MCO stages, having a great affinity for the orchestra and its audiences. The MCO has premiered over 100 works by Canadian composers.</p>
      <p>In 2013, the Manitoba Chamber Orchestra launched MCOrecords with the release of the JUNO-nominated <i>Troubadour &amp; the Nightingale</i>, featuring soprano Isabel Bayrakdarian.</p>
      <p>The MCO&rsquo;s core orchestra consists of 32 musicians, and the management team employs two full-time and five part-time individuals. The Orchestra operates on an annual budget of approximately $900,000, and is currently governed by a 19-member Board of Directors. The board and management have worked hard to maintain balanced budgets for the past 17 consecutive years, in addition to building an endowment for the future. This is an extraordinary feat given that in addition to this, the MCO has nearly doubled its budget in the past few years, for its international touring performances and a growing youth program for economically challenged children.</p>
      <p>Many concerts are sold-out, and the Orchestra boasts an average attendance of 80% of capacity.</p>

