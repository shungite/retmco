		<h4>Celebrating our<br />fortieth anniversary!</h4>
		<p>The Manitoba Chamber Orchestra, &ldquo;Canada&rsquo;s tiny, perfect, 22-string chamber orchestra&rdquo; (Toronto Star), has appeared in concert halls throughout the US, Europe and Canada. The Orchestra regularly tours, and frequently records for CBC Radio. Known for its original and fearless programming, encompassing a broad range of repertoire and diverse commissioning, it has been called &ldquo;an excellent string ensemble conducted with crispness and verve by Anne Manson&rdquo; (San Francisco Chronicle). It has been praised for its &ldquo;satiny sound, ... dynamic subtlety, and an impeccable sense of ensemble&rdquo; (Ottawa Citizen). The Manitoba Chamber Orchestra toured major North American centers with soprano Isabel Bayrakdarian in 2008, garnering three curtain calls at Carnegie Hall; Western Canada with Dame Evelyn Glennie in 2009; and Ontario with Michael Riesman in 2011. The Orchestra&rsquo;s recording with Measha Brueggergosman was nominated for a Juno in 2005, and the MCO performed on the live broadcast of the Junos that year.</p>
		<p>The Manitoba Chamber Orchestra explores the section of the repertoire rarely touched by large symphony orchestras. Established in 1972, the MCO presents an annual 9-concert subscription series in Winnipeg. Co-presentations with CBC Radio began in 1984, and Canadians across the country (roughly 3.7 million annually) and fans around the world enjoy the Orchestra in frequent broadcasts and online.</p>
		<p>In 1995, the Manitoba Chamber Orchestra released its first compact disc on Sweden&rsquo;s BIS label; <i>Canadian Music for Chamber Orchestra</i> (CD 698) marked the first recording by a North American orchestra on the prestigious label and the disc was distributed in over 45 countries. There have been five subsequent recordings, four of them with CBC Records. The most recent was <i>So Much To Tell</i>, with Measha Brueggergosman.</p>
