      <p>
      <b>
        EUG&Egrave;NE YSA&Yuml;E: SONATA IN E MAJOR, OP. 27, NO. 6
      </b>
      </p>
      Beryl Peters, Ph.D.

      <p>
      Eug&egrave;ne Ysa&yuml;e (1858-1931), was one of the most renowned violin virtuosos of his time. He was also a highly regarded teacher, conductor and composer. Ysa&yuml;e was born in Belgium but was an important part of musical communities around the world. Many composers associated with C&eacute;sar Franck&rsquo;s circle of composers wrote and dedicated works to Ysa&yuml;e, for example Saint-Saens, d&rsquo;Indy, Debussy, and Franck. Ysa&yuml;e was a founder-member of the Berlin Philharmonic Orchestra.
      </p>
      <p>
      Ysa&yuml;e composed the sixth in a set of six sonatas for unaccompanied solo violin in 1923. Ysa&yuml;e composed each of the six sonatas in Op. 27 to reflect a different character, style, and personality associated with famous contemporaries of his time. The sonatas were also conceived as Ysa&yuml;e&rsquo;s response to his fascination with the music of J. S. Bach, in particular J.S. Bach&rsquo;s Six Unaccompanied Violin Sonatas and Partitas.The sixth sonata was an homage to the great Spanish violinist Manuel Quiroga and has less Bach influences than the other sonatas of Op. 27.
      </p>

      <p>
      <b>
        When you listen to this Violin Sonata by Ysa&yuml;e, you will hear a work that incorporates the Spanish habanera.
      </b>
      </p>

