      <p>
      <b>
        The following questions can help you listen to this work.
      </b>
      </p>

      <p>
      <span class="badge">1</span> <b>Form</b> in music refers to the musical architecture or the way the music is structured. This work for solo violin was composed as a single movement structure. The form can be described as a free rhapsody that includes a <b>cadenza</b> (a showy, difficult, virtuosic section) introduction and various contrasting sections.  Can you hear a section that sounds very lyrical with Spanish influences?  It occurs just after the opening cadenza.  Can you hear a section with very demanding and complex technical passages?  Can you hear a section that sounds like a tango?
      </p>

      <p>
      <span class="badge">2</span> What <b>Instruments</b> do you hear playing this work? Is the violin playing by itself or do you hear any other instruments accompanying the violin?  Ysa&yuml;e specifically wrote all six of the sonatas in Op. 27 to be unaccompanied violin sonatas.
      </p>
      <p>
      <span class="badge">3</span> What <b>tempo</b> (the speed of the music) would you mainly associate with this work?  The composer marked this work as &ldquo;Allegro giusto non troppo vivo&rdquo; meaning lively and quickly with consistent speed but not too fast.
      </p>

      <p>
      What <b>dynamics</b> (the volume of the music), and <b>expression</b> (musical elements that express certain feelings or dispositions) are used throughout this sonata? Can you hear where the composer creates the effect of rubato (expressive and rhythmic freedom)?
      </p>

      <p>
      Was the music played at all the same volume (dynamics)?  When do you hear music played loudly? Quietly?  When do the dynamics change and what effect does that create?  When does the music get louder (crescendo) or get quieter (decrescendo)?
      </p>

      <p>
      Do you hear musical sounds that are short sounding (staccato) or very smooth sounding (legato)? Can you hear a section where there are sudden accents (sforzandos) on every other up bow?
      </p>

      <p>
      Ysa&yuml;e uses expressive elements to capture the character of Spanish music, including the tango and haberna rhythm. Ysa&yuml;e also uses expressive elements and special effects to create different rich textures throughout the work. These effects include highly demanding virtuoso bow and left hand techniques such as slurred and fast double stops, broken octaves, descending chromatic thirds, arpeggiated chords, difficult string crossing passages, grace notes, trills, jazz-like harmonies, difficult scale passages, and chromatic playing, etc.
      </p>

      <p>
      <span class="badge">4</span> How is <b>rhythm</b> used in this sonata? Is this the kind of music that you could keep a steady rhythm or beat to? Or do the rhythms have a more rhapsodic, virtuoso and improvisatory quality?
      </p>

      <p>
      Can you hear a brief silence that Ysa&yuml;e marks as a &ldquo;grand pause&rdquo; in the music about halfway through the approximately 7 minute sonata?
      </p>

      <p>
      Do you hear the habanera rhythm of the tango following the grand pause?  Listen for a rhythm that sounds &ldquo;long short even&rdquo; with an accent occurring on the &ldquo;e&rdquo; of evenâ€”the upbeat in the middle of the bar.  If you have ever heard Bizet&rsquo;s opera <i>Carmen</i> you will remember the famous habanera rhythm in that work.  Do you hear other syncopated (off beat) sounds in the tango section and in the sonata?
      </p>

      <p>
      <span class="badge">5</span> How is <b>melody</b> or <b>pitch</b> used in this sonata?
      </p>

      <p>
      Do you hear high sounds or pitches?  Do you hear low sounds or pitches? Can you pick out a particular melody to hum from this sonata or do the melodies have a more rhapsodic, fantasy-like and improvisatory quality to them?
      </p>

      <p>
      Do the melodies seem to belong clearly to one key or do you hear dissonances and chromatic notes?
      </p>

      <p>
      <span class="badge">6</span> Can you hear different kinds of <b>texture</b> in this work?  Do you hear places where the violin plays single notes and other places where two notes are played together?  Do you hear places where more than 2 notes are played together to create a thicker texture?
      </p>

      <p>
      <span class="badge">7</span> What kinds of <b>timbre</b> do you hear? <b>Timbre</b> is the different qualities of sound that can be heard, for example when the composer create special effects using techniques described above.  Can you hear different qualities of sound created by the different virtuosic techniques?
      </p>

