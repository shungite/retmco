      <p><b>ERNEST BLOCH: FROM JEWISH LIFE: PRAYER</b></p>

      <p>Ernest Bloch (1880-1959) composed <i>From Jewish Life</i> around 1924. Bloch originally composed this moving triptych for cello and piano; it has since been arranged using various instrumentation by different arrangers. The work is made up of three pieces or movements: Prayer, Supplication, and Jewish Song, all dedicated to cellist Hans Kindler. Bloch wrote that his intent was not to reconstruct authentic Jewish music or melodies, but to capture the complex, ardent Jewish spirit and soul.</p>

      <p><b>When you listen to <i>Prayer</i> by Ernest Bloch, you will hear the opening movement from the work “From Jewish Life” arranged for chamber orchestra. The first movement “Prayer” is reflective, emotionally intense, and expressive.</b></p>

