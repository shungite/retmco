      <p><b>The following questions can help you listen to this work:</b></p>

      <p><span class="badge">1</span> <b> Form</b> in music refers to the musical architecture or the way the music is structured. <i>From Jewish Life</i> is made up of three pieces or movements:</p>
      <ol>
        <li>Prayer (Andante moderato)</li>
        <li>Supplication (Allegro non troppo)</li>
        <li>Jewish Song (Moderato)</li>
      </ol>

      <p>At the concert on March 17, 2015, you will hear the first movement of <i>From Jewish Life</i> titled <i>Prayer</i>. <i>Prayer</i> can be considered a version of variation form in which the initial main theme is recurring and then contrasted with another theme. As you listen to <i>Prayer</i> can you pick out the recurring first theme and a second contrasting theme?</p>

      <p><span class="badge">2</span> This arrangement of <i>Prayer</i> is for solo cello and chamber orchestra. At the opening of this work what <b>instruments</b> do you hear? Can you hear the solo cello featured right from the beginning of the work? Can you hear the rising opening theme in the solo cello part echoed by the violins?</p>

      <p>When do you hear individual instruments or sections and when do you hear the orchestra playing together? Which instruments are given the predominant motifs that are heard in this work?</p>

      <p><span class="badge">3</span> Bloch uses <b>expressive elements</b> (musical elements that express certain feelings or dispositions) to convey emotional intensity, sadness, and a reflective quality. Do you hear how the slow <b>tempo</b> (the speed of the music) helps to create a somber, reflective and introspective mood?</p>

      <p>How are <b>dynamics</b> (the volume of the music) used to create emotional intensity and an introspective, prayerful quality? Is there a wide range of dynamics used in this piece or a narrow range of dynamics? Do you hear sudden, dramatic changes in dynamics or are there more subtle changes in dynamics? What level of dynamics is mostly used in this piece?</p>

      <p><span class="badge">4</span> How is <b>rhythm</b> used to create a sense of <i>Prayer</i>? Is this the kind of music that you can easily tap a steady rhythm or beat to—is there a strong sense of pulse and time? Can you hear strong accents in this music? Or are the rhythmic patterns free and exploratory? Are rhythmic and melodic patterns more <b>legato</b> (smooth, even, flowing, and connected)? How does this contribute to the reflective, introspective nature of this work?</p>

      <p>Can you hear different rhythmic treatment between the two contrasting themes? Do you hear a flowing rhythmic pattern in the opening theme and a repeated note rhythmic pattern in the second, contrasting theme? Do you hear any repetition of rhythmic fragments or motifs? How do the contrasting themes and motif repetition affect the emotional quality of this work?</p>

      <p><span class="badge">5</span> How is <b>melody or pitch</b> used in <i>Prayer</i>? Can you sing or hum the opening theme played in the cello? Can you hear that this opening rising, stepwise theme has different sound qualities than a major scale? Can you hear the influences of Jewish melodies and prayer modes in the melodies and motifs of this piece? Can you hear unusual, exotic sounds created by the use of modes and intervals of augmented seconds? The Jewish liturgical mode <i>Ahava Raba</i> is featured in the final, concluding section or <b>coda</b> of this piece.</p>

      <p>Do you hear short, recurring melodic phrases (<b>leitmotif</b>)? Can you hear the voice of a synagogue cantor in the sounds and repetition of motifs and melodic phrases?</p>

      <p>Can you hear how short melodic and rhythmic motifs are transformed when they are played by different instruments, when they are repeated, and when motifs in heard in sequences and variations?</p>

      <p>Can you hear two different main melodic themes in this piece? The second theme can be identified by its contrasting nature and sounds. Can you hear a repeated note pattern in the second main theme? When do you hear high sounds or pitches? When do you hear low sounds or pitches? Can you hear a phrase where the cello plays from a very high range down to a lower range? What kind of emotional quality do the higher pitches create? What kind of emotional quality do the lower pitches create in this work?</p>

      <p><span class="badge">6</span> What different kinds of <b>textures</b> do you hear in this work? Do you hear the fuller orchestral sound and defined harmonies of Haydn’s Cello Concerto in D major? Or do you hear more impressionist harmonies and textures? Impressionist music is characterized by vague tonalities and an indefinite quality to harmonies. Do you hear atmospheric sounds? Do you hear colour, movement, and suggestion of impressionist harmonies in this work?</p>

      <p><span class="badge">7</span> What kinds of instrumental <b>timbre</b> do you hear? <b>Timbre</b> is the different qualities of sound that can be heard, for example the kinds of sounds that the string instruments make when they play together. When does the timbre of the music change because certain instruments are added or taken away? Can you hear how different instrumental timbre is used to transform melodic phrases and motifs?
      </p>

