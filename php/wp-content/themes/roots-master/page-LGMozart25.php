          <p><span class="badge">1</span> What is your immediate response to this music?  Does this music sound like any other music you have heard before?  What does this music make you think of?  Have you heard any other music by Mozart?  His music has been used in many films and on TV. If you have heard other music by Mozart, is it similar or different to the Symphony No. 25?  Perhaps you could watch the movie Amadeus, about the life of Mozart, and listen for the Symphony No. 25 played at the beginning of the movie.</p>

          <p><span class="badge">2</span> What adjectives might describe the mood that you felt when listening to this symphony? Can you identify what musical elements may have created that mood for you?  When did the mood change and why?</p>

          <p><span class="badge">3</span> What musical elements did you enjoy or find interesting?  Did you enjoy the melodies that you heard?  Did the rhythms, dynamics, or tempos used by Mozart help make this work exciting?  Did you hear parts of the symphony that made you feel peaceful or calm and if so, what part of the symphony were they in?  Were there any parts that sounded particularly dramatic to you?  If so, which movement did you hear them in and why did they feel dramatic to you?</p>

          <p><span class="badge">4</span> Were there parts of the Symphony that you did not enjoy?  Why or why not?  Can you identify which music elements made you enjoy or not enjoy the music?</p>

          <p><span class="badge">5</span> Different people often have different responses to the same music.  Ask someone else who heard the same music about their response to Mozart&rsquo;s Symphony No. 25.</p>

          <p><span class="badge">6</span> What feelings did it seem that Mozart was trying to communicate to his audience about this work? What music elements seemed to be important to him?</p>

          <p><span class="badge">7</span> Is there other music by Mozart that you could listen to and compare to the sounds and experience of the Symphony No. 25? Mozart only wrote two symphonies in minor keys.  Symphony no. 25 is the first minor symphony and known as the &ldquo;little g minor.&rdquo;  The second minor symphony is No. 40 and sometimes known as the &ldquo;great g minor.&rdquo; Can you listen to Symphony No. 40 and compare it to No. 25 written many years earlier?</p>

