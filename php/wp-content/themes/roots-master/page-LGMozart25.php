          <p><span class="badge">1</span> Can you identify the <b>form (organizational structure)</b> of this symphony? Can you hear that it is written in four different sections, or movements? These movements use forms typical for a symphony written in this classical time period. They are:</p>

          <ol>
            <li>Allegro con brio (fast and lively, with vigour)</li>
            <li>Andante (a slower movement)</li>
            <li>Menuetto and Trio (A minuet dance form)</li>
            <li>Allegro (fast and lively)</li>
          </ol>

          <p>Each of the four sections or movements also has a particular form.  The first and fourth Allegro movements are written in sonata form.  Can you hear two different themes introduced by the orchestra at the beginning of these movements that Mozart develops in different ways throughout the movement, and then returns to at the end of the movement? This kind of compositional development is characteristic of classical sonata form.</p>

          <p>The slower second movement Andante is in the form of an operatic aria.</p>

          <p>The third movement is a three part dance form called a minuet and trio. Can you hear themes in the first part of this movement, then a contrasting section, and then the sounds repeated from the first section again?  This particular dance minuet was not intended for people to dance to. Why would it be difficult to dance to this particular minuet?</p>

          <p><span class="badge">2</span> What <b>instruments</b> can you hear in this symphony? How many performers are playing onstage? What string instruments do you hear (violin, viola, cello, double bass)?  Can you pick out other instruments?  Which ones? Mozart wrote this symphony for two oboes, two bassoons, four horns, and string instruments.</p>

          <p><span class="badge">3</span> Can you identify the mood Mozart is trying to create in each movement?  How does Mozart use <b>tempo</b> (the speed of the music), <b>dynamics</b> (the volume of the music), and <b>expression</b> (musical elements that express certain feelings or dispositions) to create different moods?  Can you hear how Mozart creates dramatic music in the Sturm und Drang (Storm and Stress) style with contrasting tempos and dynamics and sudden changes in tempo and dynamics? Can you hear dramatic dynamic changes in the fourth and final movement? Dan you hear the first movement being loudly?  What kinds of dynamics do you hear in the second, slow movement?  The third dance movement?</p>

          <p>Which movements use mostly fast music?  Which movement uses mostly slower music?  Can you hear when the tempo of the music changes and gets faster or slower?  How does that affect the mood of the work?</p>

          <p>When do you hear music played loudly? Quietly?  When does the volume of the music change?  When does the music get louder (crescendo) or get quieter (decrescendo)?</p>

          <p>Do you hear musical sounds that are short sounding (staccato) or very smooth sounding (legato)?  Where do you hear suddenly loud sounds (accents)?</p>

          <p><span class="badge">4</span> How is <b>rhythm</b> used in this symphony? Do you hear sections that have a steady rhythm or beat that you could tap to?</p>

          <p>The opening movement begins with a repeated syncopated (off-the-beat) 5 note pattern heard in the violins and violas.  Can you hear the oboes play four long notes over the syncopated patterns? Syncopated rhythmic patterns are a feature of the Sturm und Drang style. Can you hear syncopated rhythms return in the final, fourth movement?</p>

          <p>Can you hear any repeated rhythmic patterns in other movements? Did any particular rhythmic pattern stand out for you that you could listen for in this symphony?</p>


          <p><span class="badge">5</span> How is <b>melody</b> used in this symphony?  Do you hear high sounds or pitches?  Do you hear low sounds or pitches?  Can you hear when a melody suddenly jumps much higher?  Do you hear the large leaps in the melody and the jagged melodic themes associated with the Sturm und Drang style in the first and fourth movements?</p>

          <p>Do the melodies heard in this first movement sound familiar to you?  You may have heard this first movement played in the opening of the film Amadeus.</p>

          <p>Can you hear the &ldquo;Mannheim rocket&rdquo; at the beginning of this work? A rapidly rising series of broken chord notes was known as a &ldquo;Mannheim rocket.&rdquo;</p>

          <p>Can you pick out two contrasting themes in the first and fourth movements?  Can you pick out other melodies or themes in each of the four movements?  Can you hear melodies or themes that are repeated throughout each movement and sound quite different to each other? Can you hear melodies or themes that are louder and more dramatic than others?  Do you hear themes are that calmer and gentler? Do you hear where Mozart has lets different instruments take turns playing the same melody or parts of the melody?  The contrast of these different themes helps create a sense of drama in this symphony.</p>

          <p><span class="badge">6</span> Can you hear different kinds of <b>texture</b> in this work?  Where do you hear lots of instruments playing together so that the sound is thick?  Where do you hear just a few instruments playing?  Can you hear which instruments are playing? Do you hear instruments playing the same notes all together?  When two or more instruments play the same pitch, the effect is known as unison.  Mozart made more use of unison playing in this early symphony than he did in later symphonies.  Can you hear the orchestra instruments playing loudly together in unison at the very beginning of the symphony?  Can you hear more unison playing but performed quietly at the beginning of the fourth movement?  Where else can you hear unison playing?</p>

          <p><span class="badge">7</span> What kinds of instrumental <b>timbre</b> do you hear? Timbre is the different qualities of sound that can be heard, for example the kinds of sounds that the string instruments make when they play together.  When does the timbre of the music change because Mozart adds or takes away different instruments?</p>

          <p>How does the timbre change in the second slow movement Andante when the bassoons answer the violins? How does the timbre change again in the Trio section of the third movement Minuetto and Trio when just the wind instruments play together? Do you hear different instruments or instrument groups calling back and forth together at different points in the symphony?</p>


