						<p>
							﻿<strong>Concerto in D Minor, BWV 1059<br>Johann Sebastian Bach </strong>
						</p>
						<p>
							Several Bach concertos exist in versions featuring different solo instruments. It&rsquo;s likely that he created the revised versions of many of them to be performed at the Collegium Musicum concerts for which he was responsible, during the term in Leipzig that crowned his career. The piece you will hear at this concert probably originated as a concerto for oboe that he worked on briefly, during the periods he spent in Weimar or Anhalt-C&ouml;then, before abandoning it. 
						</p>
						<p>
							The outer movements were reconstructed from the instrumental sinfonias, known to be part of a lost concerto, that open each part of Cantata No. 35. It&rsquo;s also possible that the cantata&rsquo;s first aria was the concerto&rsquo;s original slow movement. Due to the difficulties posed in reconstructing that aria as an instrumental concerto movement, the second movement is normally performed in modern times in an adaptation from the opening movement of Cantata No. 156, which also serves as the opening movement of the Harpsichord Concerto in F Minor, BWV 1056. 
						</p>
						<p>
							The concerto contains a great deal of energy, but by setting it in a minor key, Bach lent it a rather melancholic character. The outer movements call upon the soloist for a great deal of agility and breath control. In between comes a slow movement radiating pure, noble beauty. Lucie Horsch will perform the concerto on soprano recorder. 
						</p>
						<p>
							<strong>Serenade for Strings, Op. 11<br>Dag Wir&eacute;n</strong>
						</p>
						<p>
							Internationally, this Swedish composer is known by a single work: this charming <em>Serenade</em>. It has been performed at home and abroad more often than any other Swedish composition. Part of that popularity flowed from the final movement, a catchy march, being used as the theme music for <em>Monitor</em>, a long-running BBC television arts program. 
						</p>
						<p>
							After studies in his homeland, Wir&eacute;n relocated to Paris in 1931. Contact with the music of Prokofiev, Honegger and especially Stravinsky led to his adopting Stravinsky&rsquo;s lean, tart neo-classical style, but infused with touches of romantic warmth. Later in his career, he served in a variety of musical capacities, including pianist, music critic and vice-chairman of the Swedish Composers&rsquo; Association. 
						</p>
						<p>
							In the wake of the Second World War, his style moved toward a more contemporary brand of speech, but he never fully embraced the avant-garde styles of his lifetime. He often said that he intended his music to &ldquo;entertain and please and to create a listener-friendly &lsquo;modern&rsquo; music.&rdquo; On another occasion he stated, &ldquo;I believe in God, Mozart and Carl Nielsen.&rdquo; 
						</p>
						<p>
							He had a strong theatrical bent, composing ballets and sets of incidental music for plays (including several works of Shakespeare) and music for films. He also created five symphonies, concertos for piano, violin, cello and flute, and numerous pieces of chamber music. In 1960, he composed a television ballet, <em>The Evil Queen</em>, that won a prize, and he created a song, <em>Waltz from Somewhere Else</em>, that was the official Swedish entry in the 1965 Eurovision Song Contest (sadly, it didn&rsquo;t win). 
						</p>
						<p>
							The <em>Serenade for Strings</em> marks Wir&eacute;n&rsquo;s clearest homage to Mozart and his serenades and divertimenti. He composed it in 1937, shortly after he returned to Sweden from Paris. He wrote it for conductor Tobias Wilhelmi and the Stockholm Chamber Orchestra. 
						</p>
						<p>
							The opening movement is brisk and cheeky, sporting an especially impudent second theme. Author Matthew Lynch writes of this movement that it &ldquo;evokes images of the pastoral setting of Wir&eacute;n&rsquo;s upbringing, where countryside and tradition stood side by side.&rdquo; An unidentified writer on the website <em>Fugue for Thought</em> writes that it contains &ldquo;&hellip;&thinsp;really satisfying writing for strings. It&rsquo;s engaging, buoyant and crisp&thinsp;&hellip;&thinsp;The colours and detail and depth fabricated from the string orchestra are vivid and clean.&rdquo; 
						</p>
						<p>
							The serenade&rsquo;s emotional heart lies in the expressive second movement. In it, Wir&eacute;n offered an occasionally darker emotional tone, and an intriguing contrast between bowed and pizzicato playing. &ldquo;It&rsquo;s not going to change your life,&rdquo; the <em>Fugue for Thought</em> blogger wrote, &ldquo;but its simple beauty is irrefutable.&rdquo; Next comes a darting, playful scherzo, once again with passing emotional shadows. An irresistible march, a witty parody of anything you care to name&mdash;a brass band or goose-stepping soldiers have been put forward&mdash;closes the score in toe-tapping fashion. Don&rsquo;t be surprised if you find yourself whistling the main theme as you leave the church! 
						</p>
						<p>
							<strong>Concerto in F Major, RV 433 &mdash; &lsquo;La tempeste di mare&rsquo;<br>Antonio Vivaldi</strong>
						</p>
						<p>
							In the final decades of the seventeenth century, the modern, transverse flute (so called because the performer holds it sideways to the face) first began to dominate its predecessor, the recorder (which is held straight ahead), then virtually replaced it. Italian composers felt some reluctance in writing concertos for flute, until a visit from the German virtuoso Johann Joachim Quantz in 1726 changed their minds. The next year, Vivaldi first made prominent use of the flute in his opera <em>Orlando furioso</em>, where it decorated an aria in spectacular fashion. 
						</p>
						<p>
							The six flute concertos that make up his Op. 10 were published in 1728 by Michel-Charles Le C&egrave;ne of Amsterdam. The title page indicated that they had been printed at the publisher&rsquo;s own expense, suggesting his belief that the market was ready for music showcasing the instrument. Hefty sales proved his instinct correct. 
						</p>
						<p>
							The concerto you will hear at this concert is the opening work in the Op. 10 collection. It bears the evocative subtitle <em>The Storm at Sea</em>. The same nickname is connected with other works of Vivaldi: multiple concertos for flute, oboe, violin and bassoon, both in F Major and bearing the Ryom catalogue numbers RV 98 and RV 570; and the Violin Concerto in E-flat Major, RV 253. The reasons behind this multiplicity, and the origin of the subtitle, remain unknown. Regarding the compact concerto you will hear on this program, the bold, bustling character of the outer movements certainly qualifies it for the subtitle. Lucie Horsch will perform the concerto on alto recorder. 
						</p>
						<p>
							<strong>Concerto in F Major<br>Giuseppe Sammartini</strong> 
						</p>
						<p>
							During Sammartini&rsquo;s lifetime, his fame was overshadowed somewhat by that of his younger brother, Giovanni Battista, who made important contributions to the development of the Classical symphony. Giuseppe wrote music too, both instrumental and vocal, but was best known as a performer. In fact, an English obituary said of him, that he was &ldquo;thought to be the finest player on the oboe in Europe.&rdquo; He had risen to the position of soloist in the opera orchestra of his native city, Milan, before moving to England in 1727. His gifts made it easy for him to find work. He played in the orchestra that Handel regularly assembled in London, at the King&rsquo;s Theatre, to perform his operas. Later, he came into the exclusive, private employ of the Prince of Wales. 
						</p>
						<p>
							During this period, most recorder music written on the continent featured the treble member of the family. English composers and performers, on the other hand, preferred the smaller descant and soprano instruments. This Concerto in F Major can therefore be safely assigned to Sammartini's period in England. Still, it follows the three- movement, fast&ndash;slow&ndash;fast structure standardized in Italy. The outer movements are sunny and vivacious, giving the soloist little time to draw breath. In between comes a gently melancholic and melodically appealing siciliano. Lucie Horsch will perform the concerto on soprano recorder. 
						</p>
						<p>
							<strong>Symphony No. 28 in C Major, K. 200/189k<br>Wolfgang Amadeus Mozart</strong> 
						</p>
						<p>
							This charming work probably dates from shortly after Mozart&rsquo;s most prolific symphony-writing period, 1770 to 1773, during which time he composed no fewer than 28 symphonies. 
						</p>
						<p>
							Research has shown that the catalogue numbers assigned to several of these works do not accurately reflect the order of composition. Mozart wrote the well-known Symphony No. 25 in G Minor in October 1773. Its highly dramatic nature reflected the exposure to the music of Joseph Haydn and other progressive composers that he had undergone in a recent trip to Vienna. 
						</p>
						<p>
							Next in order, in April 1774, came the sunny work known as No. 29. The precise date of No. 28 is still being debated, in part because some unknown person made a strong effort to erase it from the manuscript score. The most likely time is November 1774, which places it after Nos. 29 and 30 (composed in April and May 1774, respectively), and four years before the next symphony, No. 31, the &lsquo;Paris&rsquo; Symphony. 
						</p>
						<p>
							Several scholars have noted that No. 28 marked a substantial creative progression for Mozart. Ekkehart Kroher writes that its style &ldquo;so well expresses the cheerfulness of the key of C that it must have surprised his contemporaries. The music&rsquo;s creativeness, moreover, corresponds to an expansion of the form, which is reflected not only externally, for example in containing four movements, with a minuet, or in the addition of a coda, but also in the depth of expression. In particular the finale is no longer a mere conventional finish but is now elevated in rank to counterbalance the symphony&rsquo;s opening movement.&rdquo; 
						</p>
						<p>
							The noted American Classical period scholar H.C. Robbins Landon writes, &ldquo;This has always been a popular work, and it was one of the few Mozart works which were available on records before the Second World War. Two enchanting aspects are the solo horn &lsquo;echoes&rsquo; in the minuet and the trill-laden finale.&rdquo; 
						</p>
						<p>
							Mozart constructed the brisk opening movement on a spirited first theme and a gracious second. Author William Malloch writes that &ldquo;the music is not grand, but smart &lsquo;society&rsquo; music full of the frivolous laughter of the sort quietly captured by Elgar in the <em>Enigma Variations</em>&rsquo; tenth variation, <em>Dorabella</em>.&rdquo; In the second movement, Mozart calls for the strings to play with mutes, enhancing this sweet, flowing music&rsquo;s genteel quality. The following minuet continues the second movement&rsquo;s moderate pacing and dignified manners. The finale abandons all caution as it races merrily to a near-breathless conclusion. 
						</p>
