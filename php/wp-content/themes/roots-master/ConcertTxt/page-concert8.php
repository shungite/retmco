						<p>
							<strong>Lucie Horsch</strong> 
						</p>
						<p>
							&ldquo;The young musician&thinsp;&hellip;&thinsp;once again demonstrates her confidence and genuine fascination for her instrument, from which she creates sounds that one&mdash;way too hastily&mdash;probably wouldn&rsquo;t have expected to be possible&rdquo; (<em>Klassik Akzente</em>). 
						</p>
						<p>
							Eighteen-year-old Lucie Horsch is one of the most remarkable musical talents of her generation, and is already in great demand as a solo recorder player both in her native Netherlands and internationally. She was invited to perform with the Los Angeles Chamber Orchestra &amp; at the Norsj&oslash; Chamber Music Festival in Norway. Lucie has also performed at the Early Music Festival in Innsbruck, Austria, the Next Generation Festival in Bad Ragaz, Switzerland and the International Chamber Music Festival Utrecht, among others. 
						</p>
						<p>
							In 2014, she was chosen to represent The Netherlands in the Eurovision Young Musician contest, and two years later was awarded the renowned Concertgebouw Young Talent Award, in the presence of Sir John Eliot Gardiner. Lucie records exclusively for the Decca Classics label. Her debut disc features concertos and transcriptions of works by Vivaldi, a composer for whom Lucie feels a particular affinity. 
						</p>
						<p>
							Being a proud ambassador of the recorder, Lucie is passionate about breaking down preconceptions, to experiment and to push boundaries. Besides performing the broad Baroque repertoire for recorder, both with conductor and also as player/director, she enjoys performing contemporary repertoire. Lucie plays on recorders built by Fred Morgan, Doris Kulossa, Stephan Blezinger and Seiji Hirao, with the generous support of the Prins Bernhard Foundation. She also gratefully uses a specially designed tenor flute from Tokyo. 
						</p>
						<p>
							Lucie began to study the recorder at the age of five. In 2011 she was taken on as a student at the prestigious Sweelinck Academie at the Amsterdam Conservatorium, where she is now a regular student with Walter van Hauwe. Also a talented pianist, she first studied with Marj&eacute;s Benoist and is now in the class of Jan Wijn at the Amsterdam Conservatorium. She was a member of the National Children&rsquo;s Choir for seven years, performing with conductors such as Sir Simon Rattle, Mariss Jansons and Jaap van Zweden. 
						</p>
