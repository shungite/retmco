					Lucie Horsch: the &ldquo;latest big thing<br>in recorder playing&rdquo; 
				</div>
			</h4>
			<p>
				Just like modern audiences&rsquo; strict routine of restraining applause and staying hushed between musical movements, the popular canon of solo repertoire is, in large part, an inheritance of the Romantic era. 
			</p>
			<p>
				This means that not only reams of traditional repertoire, but also whole groups of traditional instruments, have fallen into neglect as the casualties of musical &ldquo;progress.&rdquo; Thankfully, the campaigns to rehabilitate the harpsichord, lute, cornet, and recorder and their associated repertoire haven&rsquo;t gone away. 
			</p>
			<p>
				In Lucie Horsch we have a leading ambassador for the recorder, which has claim to being the most beguiling classical instrument ever invented. The Decca-signed teenager is the &ldquo;latest big thing in recorder playing&rdquo; (<em>The Guardian</em>) who possesses &ldquo;fearsome virtuosity and superb technique&rdquo; (<em>BBC Music Magazine</em>). 
			</p>
			<p>
				At the MCO, we can&rsquo;t help but feel sorry for those who missed&nbsp;her performance with us in 2016, a sleeper hit of the season. Nearly everyone is obliged to play the recorder in elementary school. But where most of us squawked and hooted, Horsch fluttered and trilled with the graceful agility of a master quickly in the making. 
			</p>
			<p>
				Hear her perform the music of Bach and Vivaldi in true baroque style at our April 2018 concert. You may never see another recorder player, or teenager, as musically gifted as her. 
			</p>
			<p>
				<b>Lucie Horsch</b> 
			</p>
			<p>
				Born into a family of professional musicians, seventeen-year-old Lucie Horsch began to study the recorder at the age of five. Things seem to have fallen quickly into place since then: at nine a televised performance of hers caused something of a national sensation, in her early teens she represented The Netherlands at Eurovision, and in 2016 she was awarded the prestigious Concertgebouw Young Talent Award. 
			</p>
			<p>
				About her debut CD with Decca records, <em>BBC Music Magazine</em> writes, &ldquo;This is a disc to buy, and display in years to come as the start of a distinguished career.&rdquo; 
			</p>
