		<h4><div>Strings,<br>then strings</div></h4>
		<p>Two centuries offer completely different takes on how to use a string orchestra, and guest conductor Scott Yoo will explore that idea with us &mdash; right here in this century!</p>
		<p>Although not too terribly well-known, Parry’s <i>An English Suite</i> makes an admirable addition to a concertgoer’s knowledge of chamber music. It is the ultimate English work, as in &lsquo;cheers&rsquo; or &lsquo;ta&rsquo; and &lsquo;Bob&rsquo;s your uncle.&rsquo; Some of it was composed in the late 1800s, but it was completed in 1914.</p>
		<p>Roll back two centuries to <i>The Four Seasons</i>, probably the best-known piece of music in the classical repertoire. Vivaldi&rsquo;s set of four violin concertos will be play / conducted by Yoo.</p>
		<p>Make that three centuries: the present one will be well represented, too, with new music from Randolph Peters!</p>
		<p><b>Randolph Peters</b></p>
		<p>Winnipeg composer Randolph Peters is particularly well-known for his output of roughly 100 film scores made mostly for Canadian films. The first work of his performed by the MCO was <i>Three Quarks for Muster Mark!</i> in January of 1991. In addition to his <i>Paradoxes of the Heart</i>, we have played his <i>Tango</i> on several occasions.</p>
		<p>We can’t wait to hear what&rsquo;s in store for us in October!</p>

