		<h4><div>Discover two symphonies<br>with Anne Manson</div></h4>
		<p>MCO Music Director and Conductor Anne Manson will juxtapose two symphonies on this program, and will preface each with its story.</p>
		<p>One of Joseph Haydn&rsquo;s last symphonies, the 103rd was first performed in 1795. It is nicknamed &lsquo;The Drumroll&rsquo; after the long roll on the timpani with which it begins. A review of the premiere in the <i>Morning Chronicle</i> reported &ldquo;continual strokes of genius, both in air and harmony.&rdquo;</p>
		<p>Sketches of Beethoven&rsquo;s first symphony have been found from that very year. In the work of the young composer can be heard the influence of his teacher &mdash; Joseph Haydn &mdash; and other predecessors, including Mozart. It&rsquo;s definitely Beethoven, however, and with it, the young master burst onto the scene in 1800 Vienna.</p>
		<p>The first and (almost) last symphonies of two masters in one sitting! We’ll leave it to Anne
		to break it down.</p>
		<p><b>When composers clash</b></p>
		<p>&ldquo;... the issue surfaced most notably upon the publication of Beethoven&rsquo;s first compositions, the Op. 1 piano trios. Wishing to assist the young composer, Haydn suggested that Beethoven include the phrase &lsquo;pupil of Haydn&rsquo; underneath his name in order to garner advantage from Haydn&rsquo;s considerable fame. Beethoven bristled.&rdquo;</p>
		<p>According to the account left by Beethoven&rsquo;s pupil Ferdinand Ries, &ldquo;Beethoven was unwilling to because, as he said, although he had some instruction from Haydn he had never learned anything from him. However, at the old composer&rsquo;s 76th, he knelt down before Haydn and fervently kissed the hands and forehead of his old teacher.&rdquo;</p>

