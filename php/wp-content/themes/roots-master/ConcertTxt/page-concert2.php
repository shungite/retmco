						<p>
							<strong>Symphony No. 44, in E Minor, &lsquo;Trauer-Symphonie&rsquo; (Tragic Symphony)<br>Joseph Haydn</strong> 
						</p>
						<p>
							When Haydn began composing symphonies in the late 1750s, little distinction existed between opera overtures and symphonies written for performance at concerts. One of his primary accomplishments was to chart a separate, independent course for the symphony. In his more than 100 examples, he raised it to the first summit of its artistic value. 
						</p>
						<p>
							He composed his initial symphonies during the three-year period he spent with his first employer, the Bohemian nobleman Count Karl Joseph Franz von Morzin. In 1761, after Morzin disbanded his orchestra due to financial difficulties, Haydn took up the position of Vice-Kapellmeister to the even wealthier and more influential Esterh&aacute;zys, a dynastic family of Hungarian nobles. His numerous responsibilities included composing operas, symphonies, chamber and vocal music, and maintaining the court orchestra and library. 
						</p>
						<p>
							Between 1768 and 1772, he composed a number of urgent, dramatic works in minor keys, including Symphony No. 44. This is often referred to as his <em>Sturm und Drang</em> (Storm and Stress) period, mirroring an emotionally similar and slightly later movement in German-language literature. This symphony&rsquo;s subtitle or nickname is said to have originated with Haydn himself. He requested that the third movement be performed at his funeral. 
						</p>
						<p>
							&ldquo;Full of passionate fire in the outer movements and a gaunt contrapuntal severity in the strictly canonic minuet (offset by a radiant trio with a shining horn solo), this is the <em>Sturm und Drang</em> symphony at its most characteristic,&rdquo; wrote the noted Classical-period scholar, H.C. Robbins Landon. &ldquo;Haydn&rsquo;s language has darkened and a new mood of drama and rhetoric prevails.&rdquo; Another notable element is the placing of the minuet before the slow movement. Haydn employed this practice frequently in his string quartets, but on only six occasions in a symphony. 
						</p>
						<p>
							<strong>Viola Concerto in G Major<br>Georg Philipp Telemann</strong> 
						</p>
						<p>
							Even in an era known for its composers&rsquo; productivity, Telemann was exceptional. A rough estimate of his output numbers several thousand pieces. Estimating is all that can be done so far; the publishing firm of Barenreiter began cataloguing his music in 1950, and its editors have still not completed the project. 
						</p>
						<p>
							Such efforts would be meaningless if Telemann&rsquo;s music did not deserve them, but clearly it does. The skill, imagination, and vitality that his finest works display are qualities too precious to ignore. During his lifetime, his music earned him the reputation as the finest German composer of the day, superior even to J.S. Bach, and his popularity rivaled that of Handel. 
						</p>
						<p>
							Telemann stated that he was no great lover of concertos, but his many examples of the form belie that view. His Viola Concerto is among the earliest to be composed for this instrument, created at a time when the viola was beginning to emerge from its place within the string ensemble to take on an individual identity. The concerto has yet to be dated precisely but he probably composed it before 1720. Rather than following the three-movement chamber sonata form favoured by Vivaldi, his contemporary, Telemann cast the concerto in the four-movement layout of the Baroque church sonata, with movements in alternating slow and quick tempos. 
						</p>
						<p>
							<strong>Snow White 2014&thinsp;|&thinsp;2017<br>Michael Oesterle</strong> 
						</p>
						<p>
							<em>The composer has provided the following note: </em> 
						</p>
						<p>
							Many of my works are about my fascination with the lives of scientists. In these compositions I don&rsquo;t attempt to give a precise outline or demonstration of any specific scientific theorem. They are simply the result of having been inspired by the force of concentration and creativity of scientists, their method of work and the frequency with which they meet society&rsquo;s opposition. <em>Snow White</em> continues a series of works about the British mathematician Alan Turing&nbsp;(1912-1954). 
						</p>
						<p>
							Alan Turing took a second-hand violin and a sextant to Princeton University: the violin, to be like Einstein, the sextant to chart his course while aboard the ship that carried him there. He never learned to play the violin well, (his brother referred to his playing as &ldquo;excruciating&rdquo;) but he loved to play and played for those he loved. He played his favourite melody <em>Molly Malone</em> for his lover and later, for the officers who arrested him. He played as a declaration of faith&nbsp;in civilization and the need to strive towards greatness of both heart and mind. 
						</p>
						<p>
							I cannot fathom this curious, chatty, sporty, caring, playful, brilliant, man choos&shy;ing to end his life with a bite from a poisoned apple. Yet he frequently chanted the morbid couplet of Snow White&rsquo;s evil queen: 
						</p>
						<p>
							<em>Dip the apple in the brew<br> Let the sleeping Death seep through</em><br>&mdash;<em>Snow White and the Seven Dwarves</em>, Walt Disney Productions, 1937 
						</p>
						<p>
							The animated film <em>Snow White</em> shocked the graphically naive audiences of the 1930s. A queen, alchemy, numeracy, friendships forged under duress, an apple, injustice, and transformation&mdash;this fairytale struck at the hearts of those heading into their own great battle between good and evil. For those who chose knowledge above all else, as Turing did, the apple held a special ambiguity. 
						</p>
						<p>
							<em>Snow White</em> was commissioned in 2014 by the Tafelmusik Baroque Orchestra. The 2017 version was written for Holland Baroque. Both versions have been premiered by violinist Aisslinn Nosky. 
						</p>
						<p>
							<strong>Sinfonia concertante for Violin and Viola in E-flat Major, K. 364<br>Wolfgang Amadeus Mozart</strong> 
						</p>
						<p>
							Mozart felt unappreciated in his native city, Salzburg, so in 1777 he set out in search of a position worthy of his talents. The main stops in his two-year odyssey were Paris and Mannheim. He didn&rsquo;t find what he was looking for in either city. He returned to Salzburg an unhappy man, but one enormously stimulated by the music he had heard. Mannheim, with its acclaimed court orchestra and circle of innovative composers, proved to be a major source of inspiration. 
						</p>
						<p>
							A type of piece that was popular in Mannheim and Paris alike was the symphonie concertante. It combined featured roles for several solo instruments, with the weight and richness of thought of a symphony. Both during and after Mozart&rsquo;s trip, he composed several works in this vein. They include this magnificent piece for violin and viola, which probably dates from the summer or early autumn of 1779. 
						</p>
						<p>
							Knowing his usual practice, it is likely that Mozart composed it with specific solo performers in mind. Given his expertise on both featured instruments, he may have been thinking of himself! No record remains of either the date of the premiere or the identities of the musicians who took part in it. It was only published ten years after his death, and was hardly played at all during the following century. 
						</p>
						<p>
							The spacious first movement bears a confident, almost heroic nature, and is the most ingeniously conceived of the three. The dignified pathos of the slow movement shifts the mood dramatically. The recent death of Mozart&rsquo;s mother, in Paris, may have influenced its tone. The viola really comes into its own here, Mozart using its darker tone to enhance the music&rsquo;s melancholy spirit. These emotional clouds are blown quickly away by the finale, as impish a rustic romp as Mozart ever created. 
						</p>
