						<p>
							<strong>Aisslinn Nosky</strong> 
						</p>
						<p>
							Hailed as &ldquo;a fearsomely powerful musician&rdquo; by the <em>Toronto Star</em>, Canadian-born violinist Aisslinn Nosky is one of the most versatile and dynamic violinists today. She is in demand internationally as a soloist and director and was appointed Concertmaster of the Handel and Haydn Society in 2011. She has performed in solo and chamber music recitals across North America, Europe and Asia. Recent collaborations include the Orchestra of the Age of Enlightenment, the Utah Symphony, Holland Baroque, the Calgary Philharmonic, and Tafelmusik Baroque Orchestra. In 2016 Aisslinn was named Principal Guest Conductor of the Niagara Symphony. 
						</p>
						<p>
							As Co-Artistic Director of I FURIOSI Baroque Ensemble, Aisslinn has helped bring an enthusiastic new audience to Baroque music. Since 2001, I FURIOSI has presented its own flamboyant and inventive concert series in Toronto, and they have toured North America and Europe with engagements at Tage Alter Musik (Regensburg, Germany), the Galway Early Music Festival, the Lam&egrave;que International Baroque Festival, the Mosel Musikfestival, the Indianapolis Early Music Festival, and the Montr&eacute;al Baroque Festival. 
						</p>
						<p>
							As a founding member of the Eybler quartet, Nosky explores repertoire from early quartet literature on period instruments. The Eybler Quartet&rsquo;s recording of Haydn&rsquo;s Opus 33 string quartets was released in 2012 on the Analekta label. The <em>Globe and Mail</em> mused: &ldquo;Many a great string quartet annihilates Haydn with incorrect tempos, intense legato, and a general misunderstanding of classical syntax. Here we have them as the composer might have heard them himself. In fact, maybe even better.&rdquo; 
						</p>
