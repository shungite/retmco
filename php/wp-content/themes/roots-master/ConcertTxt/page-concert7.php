						<p>
							﻿<strong>Tracy Dahl</strong> 
						</p>
						<p>
							Canada&rsquo;s premiere coloratura soprano, Tracy Dahl, has appeared throughout her career with such opera houses as the Metropolitan Opera, San Francisco Opera, Houston Grand Opera, Santa Fe Opera, Canadian Opera Company, Teatro alla Scala (Milan) and the Th&eacute;&acirc;tre du Ch&acirc;telet (Paris). Her 2017/18 engagements will include Carl Orff&rsquo;s <em>Carmina Burana</em> with the St. Louis Symphony and Vancouver Symphony, Leonard Bernstein&rsquo;s <em>Candide</em> with the Toronto Symphony Orchestra and Mahler&rsquo;s 4th Symphony with the Vancouver Symphony. 
						</p>
						<p>
							Her recent operatic engagements include concert performances of <em>Candide</em> with Bramwell Tovey and the Vancouver Symphony Orchestra; Despina in <em>Cos&igrave; fan Tutte</em> with Canadian Opera Company; Gilda in <em>Rigoletto</em> with Edmonton Opera and Manitoba Opera; the title roles in <em>Lucia di Lamermoor</em> and <em>Maria Stuarda</em> with Pacific Opera Victoria; the world premieres of Unsuk Chin&rsquo;s <em>Alice in Wonderland</em> and Peter Ash&rsquo;s <em>The Golden Ticket</em> with Opera Theatre of St. Louis; Cunegonde in Candide and Mabel in <em>The Pirates of Penzance</em> with Calgary Opera; and Madame Mao in <em>Nixon in China</em> with Opera Colorado, Portland Opera, and Vancouver Opera. Among her many notable debuts are Adele in <em>Die Fledermaus</em> at the Metropolitan Opera, where she later returned as Zerbinetta in <em>Ariadne auf Naxos</em>; Florestine in the world premiere of <em>The Ghosts of Versailles</em>, and Valencienne in <em>The Merry Widow</em>; also as Olympia in the San Francisco Opera production of <em>Les Contes d&rsquo;Hoffmann</em> opposite Pl&aacute;cido Domingo, where she returned as Oscar in <em>Un Ballo in Maschera</em> and the title role in <em>Lucia di Lammermoor</em>. 
						</p>
						<p>
							Her discography includes <em>A Disney Spectacular</em> with the Cincinnati Pops (Telarc), <em>Glitter and Be Gay</em> with the Calgary Philharmonic (CBC), <em>A Gilbert and Sullivan Gala</em> with the Winnipeg Symphony Orchestra (CBC), and <em>Love Walked In</em>, a Gershwin collection with the Bramwell Tovey Trio (Red Phone Box Company). 
						</p>
						<p>
							﻿<strong>Andriana Chuchman</strong> 
						</p>
						<p>
							This season, soprano Andriana Chuchman returns to the Lyric Opera of Chicago in the title role of a new production of <em>Orph&eacute;e et Eurydice</em> and to the Metropolitan Opera as Valencienne in <em>The Merry Widow</em>. She also makes her debuts at the Lincoln Center White Lights Festival in staged performances of the Pergolesi <em>Stabat Mater</em> and the Atlanta Opera as Marie in <em>La fille du r&eacute;giment</em>. Her concert engagements include appearances with the Regina Symphony Orchestra. 
						</p>
						<p>
							A graduate of the Ryan Opera Center at the Lyric Opera of Chicago, Ms Chuchman has also appeared at the Metropolitan Opera, Houston Grand Opera, Washington National Opera, San Diego Opera, Chicago Opera Theater, Spoleto Festival USA, the Glimmerglass Festival, and the Bard Music Festival. In concert she has appeared with the Detroit, Toronto and Winnipeg symphony orchestras, and at the Ravinia Festival. 
						</p>
						<p>
							In her native Canada, Ms Chuchman recently made her debut at the Canadian Opera Company as Olympia in <em>Les Contes d&rsquo;Hoffmann</em>. She has also appeared at the Edmonton Opera as Yum-Yum in <em>The Mikado</em> and Marie in <em>La fille du r&eacute;giment</em>, and at the Manitoba Opera as Pamina in <em>Die Zauberfl&ouml;te</em>. Concert performances have included engagements with the Toronto, Edmonton, Prince George and Winnipeg symphony orchestras. 
						</p>
						<p>
							Born in Winnipeg, Ms Chuchman received her Bachelor&rsquo;s Degree in Voice Performance from the School of Music at the University of Manitoba. She is the recipient of the Opera Theater of St. Louis&rsquo; 2017 Mabel Dorn Reeder Award, and was also a prizewinner at the Finals of the 2009 Neue Stimmen Competition in Germany and received a Sullivan Foundation Encouragement Award in 2007. 
						</p>
						<p>
							﻿<strong>Jos&eacute; Evangelista</strong> 
						</p>
						<p>
							Jos&eacute; Evangelista, born in Valencia (Spain) in 1943, began his musical studies with Vicente Asencio while simultaneously studying physics. Later work in computers led him to Canada. Settling in Montr&eacute;al in 1970, he studied composition with Andr&eacute; Pr&eacute;vost and Bruce Mather. Between 1979 and 2009, he was a professor at the University of Montr&eacute;al. Additionally he was a founding member of several music companies. He has won several prizes and garnered numerous commissions, including Itin&eacute;raire (Paris), Kronos Quartet, Groupe vocal de France, the Soci&eacute;t&eacute; de musique contemporaine du Qu&eacute;bec, Radio-Canada and Chants Libres. His music, which has been performed in Canada, the United States, Europe, and Asia, draws on his Spanish origins and on the traditions of Indonesian gamelan, the Western avant-garde, and modal music. 
						</p>
