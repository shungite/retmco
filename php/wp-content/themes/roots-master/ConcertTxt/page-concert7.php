						<p>
							<b>Earl Stafford</b> 
						</p>
						<p>
							Earl began his music education at the age of eight, making his debut two years later as a piano soloist with orchestra. Since then, he has played around the world, receiving critical accolades for his performances. The <i>Globe and Mail</i> wrote, &ldquo;His pianistic artistry was sheer poetry.&rdquo; He was awarded the gold medal as best accompanist at the 1980 International Ballet Competition in Varna, Bulgaria. 
						</p>
						<p>
							In 1984, Earl was appointed Music Director and Principal Conductor for Canada&rsquo;s Royal Winnipeg Ballet, a position he held until 2009. He has also served as music director for the Saskatoon Symphony, and has held positions on the faculties of the University of Manitoba and The Banff Centre. Earl was invited to conduct for Queen Elizabeth&rsquo;s Royal Jubilee Gala in 2002 and was music director for the Governor General&rsquo;s Awards with the National Arts Centre Orchestra. He has made numerous recordings for CBC and has appeared as a piano soloist on Bravo and A&amp;E. 
						</p>
						<p>
							Earl&rsquo;s work with the Royal Winnipeg Ballet earned him a sterling reputation, which has resulted in numerous repeat invitations as guest conductor. He has worked with many of Canada&rsquo;s finest orchestras including the Toronto Symphony, the National Arts Centre Orchestra, the Symphonies of Vancouver, Victoria, Kitchener-Waterloo, Winnipeg and Saskatoon and the Orchestre M&eacute;tropolitain du Grand Montr&eacute;al. 
						</p>
						<p>
							Internationally, Earl has appeared with the Tokyo Philharmonic, the Hungarian State Opera, the Scottish Chamber Orchestra, the Queen&rsquo;s Hall Orchestra of London and the Theatre Harmony Orchestra of Moscow. Earl has also collaborated with many of the world&rsquo;s finest artists, including James Ehnes, Janina Fialkowska, Desmond Hoebig, Angela Hewitt, Angela Cheng, Josh Groban, Evelyn Hart, Oscar Peterson, Joel Quarrington, Alain Trudel, Stephen Sitarski, Kyoko Takezawa, Mel Torm&eacute;, Ron Paley, Edith Wiens, Mark Zeltzer, Ian Tyson, Neil Sedaka, Fernando Varela, Jann Arden, Anne Murray and more. 
						</p>
						<p>
							Earl is comfortable conducting and playing a variety of styles of music&mdash;from the classics to the avant-garde, and from chamber music to pops. He is a versatile conductor who isn&rsquo;t afraid to take chances. 
						</p>
						<p>
							<b>Measha Brueggergosman</b> 
						</p>
						<p>
							Noted by the <i>San Francisco Chronicle</i> as &ldquo;a singer of rare gifts and artistic intensity&rdquo; and by the <i>Miami Herald</i> for possessing &ldquo;a superb voice capable of just about everything,&rdquo; Canadian soprano Measha Brueggergosman has emerged as one of the most magnificent performers and vibrant personalities of the day. She is critically acclaimed by the international press as much for her innate musicianship and voluptuous voice as for a sovereign stage presence. 
						</p>
						<p>
							3.2 billion television viewers worldwide witnessed the Opening Ceremonies of the Vancouver 2010 Olympic and Paralympic Winter Games, and all heard the lauded soprano&rsquo;s epic performance of the &lsquo;Olympic Hymn.&rsquo; 
						</p>
						<p>
							Notable performances of the recent past include: Mahler&rsquo;s <i>Des knaben Wunderhorn</i> conducted by Jaap van Zweden with the Chicago Symphony; Cage&rsquo;s <i>Aria with Renga</i> under Michael Tilson Thomas at the debut concert of the YouTube Symphony at Carnegie Hall; Messiaen&rsquo;s <i>Poemes pour Mi</i> with Daniel Harding and the London Symphony and Mendelssohn&rsquo;s <i>Elijah</i> with Yannick N&eacute;z&eacute;t-Seguin and the Orchestre M&eacute;tropolitain du Montr&eacute;al. Measha also performed at Gustavo Dudamel&rsquo;s inaugural concert as Music Director of the Los Angeles Philharmonic. 
						</p>
						<p>
							Deeply committed to the art of song, Measha Brueggergosman has given solo recitals at London&rsquo;s Wigmore Hall, Roy Thomson Hall, Carnegie Hall, Spivey Hall, the Kennedy Center, and the Palais des Beaux-Arts in Brussels, and many other venues across North America and Europe. Always happy to try something new, she participated in the Verbier Festival&rsquo;s one-off presentation of <i>Rufus Wainwright Goes Classical</i>, sharing performances of works from the classical repertoire with Rufus Wainwright and Angelika Kirchschlager, arranged especially for the occasion. 
						</p>
						<p>
							Ms Brueggergosman has been honoured to participate in a number of special events including the gala re-openings of Roy Thomson Hall and the University of Michigan&rsquo;s Hill Auditorium, Canada Day celebrations from Parliament Hill in Ottawa, and the opening ceremonies of the XVI International AIDS Conference in Toronto&mdash;sharing the stage with Bill Gates and former President Bill Clinton. She has given a Royal Command Performance for Queen Elizabeth II and has sung for, amongst others, the Prince of Wales, Queen Margrethe II of Denmark, Queen Sonja of Norway, President Tarja Halonen of Finland, Nelson Mandela and Kofi Annan. In 2012 she served as a judge for the inaugural season of <i>Canada&rsquo;s Got Talent</i>. Measha has hosted other cultural variety shows and was the subject of a full-length CBC feature documentary, <i>Spirit in her Voice</i>. 
						</p>
						<p>
							Beyond the great concert halls of the world, Ms Brueggergosman lends her voice, passion, and energy to social and environmental causes as a Canadian goodwill ambassador for three international organizations: the African Medical and Research Foundation; Learning Through the Arts; and the World Wildlife Fund. Her commitments to these organizations have taken her on a broad spectrum of missions&mdash;from primary schools in New Brunswick to internally displaced persons camps of northern Uganda. 
						</p>
						<p>
							In April 2015 Measha and her husband Markus welcomed to their family Sterling Markus, a baby brother for three year old Shepherd Peter. 
						</p>
						<p>
							<b>Luke Nickel</b> 
						</p>
						<p>
							Luke Nickel (b. 1988) is an award-winning Canadian interdisciplinary artist and researcher currently undertaking a PhD at Bath Spa University. He has worked with ensembles EXAUDI, the Bozzini Quartet, and the Winnipeg Symphony Orchestra, and has collaborated with galleries and organizations such as the Panoply Performance Laboratory (Brooklyn), G39 (Cardiff), and the Arnolfini (Bristol). 
						</p>
						<p>
							A recipient of support from the Canada Council and other arts organizations, Luke currently has two main strands of work. The first is centred around memory and the process of transmitting musical scores. In this practice, he aims to remove the concrete document of the score and relocate it to the unstable domain of the human memory. He does this by communicating all the parameters found in traditional scores in ephemeral means such as unrecorded conversations or temporary audio files. By rendering the score unstable, he allows forgettings, mis-rememberings, and transformations to permeate the fabric of the musical material. 
						</p>
						<p>
							The second strand of Luke&rsquo;s work is focused on the notion of text as sound&mdash;or sound in text. In this stream of work Luke is developing spoken vocal quartets that examine traditionally musical qualities (such as proportion, silence, and density) while at the same time treating the text itself musically. Oftentimes the subject of the text is, in itself, musical. So far these works have taken the format of performances, installations, objects, and writings and have been presented at the Arnolfini Gallery, Bang the Bore concert series, and Bath Spa University. Examples can be heard in the pieces String Quartet #1 and <i>White Fang Field Recording</i>. 
						</p>
						<p>
							Luke is also an active curator, and currently co-directs the Cluster Festival in Winnipeg. Cluster Festival is now in its seventh year and has featured hundreds of interdisciplinary artists from across the world in innovative forms of presentation. 
						</p>
