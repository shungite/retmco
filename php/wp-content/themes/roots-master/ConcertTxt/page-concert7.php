					﻿Tracy Dahl &amp; Andriana Chuchman:<br>Duets and Arias 
				</div>
			</h4>
			<p>
				Sopranos Tracy Dahl and Andriana Chuchman&nbsp;have a special relationship with Winnipeg. They&rsquo;re both high-flying, international opera singers with roots in Peg City. They have another unique connection&thinsp;&mdash;&thinsp;Andriana is Tracy&rsquo;s former pupil. 
			</p>
			<p>
				Tracy is, of course, one of Canada&rsquo;s premier coloratura sopranos; a singer who carries off the agile runs, trills, and leaps of the coloratura repertoire &ldquo;with sunshine, rainbows and laser light&rdquo; (<em>Opera Canada</em> ). There&rsquo;s no need to impress the reader by reeling off all the illustrious stages on which she&rsquo;s performed, but let&rsquo;s mention a few anyway: Th&eacute;&acirc;tre du Ch&acirc;telet, the Metropolitan, La Scala, the Hamburg Opera. 
			</p>
			<p>
				Even if Dahl hadn&rsquo;t been given a place on the Women&rsquo;s Executive Network&rsquo;s 2014 &ldquo;Canada&rsquo;s Most Powerful Women&rdquo; list, her list of accomplished former students (at the University of Manitoba and elsewhere) would surely testify to her indelible impact on the Canadian singing community. 
			</p>
			<p>
				One of those former students is Andriana herself, who just ten years ago was a star voice student at the U of M. Today she stars alongside the likes of Placido Domingo on the world&rsquo;s top opera stages. Praised by the<em> New York Times</em> and<em> Chicago Tribune</em> for her vocal brilliance, Chuchman cut her teeth learning Ukrainian folk songs with her sister in the Garden City home where they grew up. 
			</p>
			<p>
				It&rsquo;s not often that a guest artist sells out their debut concert with the MCO, but that&rsquo;s what Andriana did in December 2016. Seeing who she&rsquo;s sharing the stage with at our March 2018 concert, which features classic duets and arias, we&rsquo;d recommend getting your tickets to this one ASAP. 
			</p>
			<p>
				<b>Like teacher, like pupil&thinsp;&hellip;</b> 
			</p>
			<p>
				Sopranos Tracy Dahl and Andriana Chuchman have parallel experiences at The Metropolitan Opera in New York. Both replaced leading sopranos on short notice, Dahl as Adele in <em>Die Fledermaus</em> in 1991 and Chuchman as Adina in <em>L&rsquo;Elisir d&rsquo;Amore</em> just this past January. For both, it represented an impromptu Met debut. But their connection goes much further than that. 
			</p>
			<p>
				Dahl is Chuchman&rsquo;s former teacher&hellip; &ldquo;She&rsquo;s part of our family, the boys treat her like a big sister,&rdquo; [Dahl] said. In fact, when Dahl wished to continue her singing career when her children were very young, Chuchman travelled with them as a nanny&rdquo;&mdash;CBC News, Manitoba. 
