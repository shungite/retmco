						<p>
							<strong>Airs d&rsquo;Espagne (Spanish Melodies)<br>Jos&eacute; Evangelista</strong>
						</p>
						<p>
							<strong>The composer has provided the following note:</strong>
						</p>
						<p>
							Airs d&rsquo;Espagne consists of 15 folk melodies from Spain. They include work songs, lullabies, entertainment songs and religious songs. They come from a variety of regions and most of them are probably fairly old. The melodies are presented as such, or at most repeated, without formal development or modulations. My purpose was to emphasize the melodic character of this material. This piece was commissioned by the CBC (Winnipeg). 
						</p>
						<p>
							<strong>&lsquo;Domine Deus,&rsquo; from Mass in C Minor, K. 427<br>Wolfgang Amadeus Mozart</strong> 
						</p>
						<p>
							Mozart composed 16 complete settings of the Mass, nearly all of which date from his years as a young man in service to the Prince-Archbishop of Salzburg. This much later and more elaborate setting is another matter altogether. Probably inspired by significant events in his personal life&mdash;and unlike most of his works, not created in response to the dictates of a salaried position or commission&mdash;it is as expansive and deeply felt as the majority of his previous Masses were brief and dutiful. For unknown reasons, he left it incomplete. This excerpt is a heartfelt prayer to God and to Jesus Christ. 
						</p>
						<p>
							<strong>&lsquo;Laudamus te,&rsquo; from Gloria in D Major, RV 589<br>Antonio Vivaldi</strong> 
						</p>
						<p>
							Vivaldi&rsquo;s duties as music director of the Ospedale della Piet&agrave;, a school for foundling girls in Venice, did not originally include composing sacred choral music, but it was added to his responsibilities in 1713. He created two settings of the <em>Gloria</em> text during his earliest days of writing sacred choral works. This one is the more extroverted and brilliant of the two. The text originated with hymns used in the earliest days of the Christian church, comprising the songs of the angels on the night of the Nativity, the praise of God by His titles, and the invoking of Christ. It is divided into twelve brief sections, and Vivaldi matched their varied moods with equally varied and consistently inspired music. The third section, <em>Laudamus te</em> (We praise thee, Lord), is music of vigorous energy and simple, unaffected piety. 
						</p>
						<p>
							<strong>Symphony No. 17 in G Major, K. 129<br>Wolfgang Amadeus Mozart</strong> 
						</p>
						<p>
							&lsquo;Sinfonia&thinsp;/&thinsp;del Sgr Cavaliere Amadeo Wolfgango&thinsp;/&thinsp;Mozart nel mese di Maggio 1772&thinsp;/&thinsp;Salisburgo.&rsquo; The most common theory regarding this inscription on this piece&rsquo;s autograph manuscript is that Mozart had begun it earlier, put it aside, and completed it &lsquo;in the month of May, 1772.&rsquo; It was the second of three symphonies that he finished that month. 
						</p>
						<p>
							The first movement is notable for its effortless elegance and its playful use of crescendo, the gradual rise in volume that Gioachino Rossini (1792-1868) would use to such charming effect in his operas. The second movement is a serene outpouring of sweet emotions. Neal Zaslaw comments that here &ldquo;Mozart spins a magical web of common-coin melodic fragments.&rdquo; The finale is a rapid, jig-like romp. Mozart wrote of a similar passage in his comic opera <em>The Abduction from the Seraglio</em> that it &ldquo;must go very fast&mdash;and the ending must make a truly great racket. The more noise the better&mdash;so that the audience doesn't grow cold before the time comes to applaud.&rdquo; 
						</p>
						<p>
							<strong>&lsquo;Se viver non degg&rsquo;io,&rsquo; from &lsquo;Mitridate, King of Pontus,&rsquo; K. 87<br>Wolfgang Amadeus Mozart</strong> 
						</p>
						<p>
							In December 1769, the thirteen-year-old Mozart and his father Leopold set out on their first tour of Italy. The teenage composer met with immense acclaim wherever they stopped. In Milan, he received a commission to compose the first opera of the following year&rsquo;s Carnival season. He received the libretto of the commissioned, dramatic opera, <em>Mitridate, King of Pontus</em>, while staying in Bologna in October, and set to work at once. Despite the opera&rsquo;s six-hour duration, the premiere in Milan on 26 December 1770 proved highly successful. An additional 20 performances followed in short order, but then it disappeared from the stage until it was revived in Salzburg in 1971. 
						</p>
						<p>
							The libretto was based on a play by the seventeenth-century French author, Jean Racine. From 124 to 88 B.C., Mithridates, an actual historical figure, served as king of Pontus, a region of what is now Turkey. The play and libretto deal in fictional terms with the turbulent, eventually tragic relations between him, his new young wife, and his two sons. This lyrical duet involves Queen Aspasia, the fianc&eacute;e of Mithridates, and Sifare, his son (the latter a &lsquo;trousers&rsquo; role, a male character sung by a woman). 
						</p>
						<p>
							<strong>&lsquo;Sull&rsquo;aria,&rsquo; from &lsquo;The Marriage of Figaro,&rsquo; K. 492<br>Wolfgang Amadeus Mozart</strong> 
						</p>
						<p>
							<em>The Marriage of Figaro</em> is a play by the Frenchman Pierre Augustin Caron de Beaumarchais, a sequel to <em>The Barber of Seville</em>. Various love affairs and romantic deceptions are resolved by the final curtain, but not before Count Almaviva&rsquo;s servants, including Figaro the barber, have proven themselves as intelligent and sympathetic&mdash;if not more so&mdash;as he. (Quite a revolutionary concept for the late eighteenth century!) Mozart&rsquo;s operatic version premiered with great success in Vienna in May, 1786 and has maintained its popularity ever since. The characters who perform this sweet-natured duet are Countess Almaviva and her maid, Susanna. The Countess dictates to Susanna an invitation to a tryst, addressed to the Countess&rsquo;s husband, in a plot to expose his infidelity. 
						</p>
						<p>
							<strong>&lsquo;Ah! guarda sorella,&rsquo; from &lsquo;Cos&igrave; fan tutte,&rsquo; K. 588<br>Wolfgang Amadeus Mozart</strong> 
						</p>
						<p>
							The plot of this 1789 comic opera was said to be based upon actual events that had occurred among the Viennese aristocracy a short time earlier. A cynical older man placed a bet with two young fellows: that their fianc&eacute;es would prove unfaithful within 24 hours&mdash;and with each other&rsquo;s mates! The wager went ahead, and the aging manipulator won. From this situation, Mozart and librettist Lorenzo da Ponte fashioned a masterful comedy (albeit one with serious undercurrents): <em>Cos&igrave; fan tutte</em> (All Women Are Like That). In this gentle duet from Act One, the sisters Dorabella and Fiordiligi praise their lovers. 
						</p>
						<p>
							<strong>Symphony No. 65, in A Major<br>Joseph Haydn</strong> 
						</p>
						<p>
							Haydn composed this intriguing symphony during the period 1772 to 1774. It may have originated, in whole or in part, as incidental music for one of the plays that impresario Carl Wahr&rsquo;s theatrical company performed at the request of Haydn&rsquo;s royal employer, Prince Nicolaus Esterh&aacute;zy. The troupe was in residence on the Esterh&aacute;zy&rsquo;s lavish country estates every summer from 1772 to 1777. Among the plays they staged were several by Shakespeare, including <em>Hamlet</em>. No record exists of Haydn composing an incidental score for that dark masterwork, but it&rsquo;s fascinating to speculate how it might have sounded. 
						</p>
						<p>
							The opening movement of Symphony No. 65 is brisk and melodious. Some scholars hear in it echoes of the Eastern European folk music that fascinated Haydn throughout his career. The slow second movement is an appealingly eccentric creation, dotted with quasi-fanfare wind instrument figures and abrupt shifts in dynamics from very soft to very loud. Musing upon its possibly theatrical origins, the noted Classical-period scholar H.C. Robbins Landon has written that it has &ldquo;the whiff of greasepaint about it.&rdquo; Haydn constructed the outer panels of the compact third movement minuet on offbeat rhythms. The central trio, in which he peeled back the orchestra to strings alone, veers into quieter but darker waters. The finale is a rollicking hunting-style piece, one of several that Haydn composed over the years. 
						</p>
						<p>
							<strong>El desdichado (The Unhappy One)<br>Camille Saint-Sa&euml;ns</strong><br>&mdash;arr. Vivian Fung 
						</p>
						<p>
							Saint-Sa&euml;ns was another Romantic French composer who succumbed to the allure of Spanish music. He composed this sultry duet in 1871, on a text by Jules Barbier and in the style of a slow-tempo dance, the bolero. 
						</p>
						<p>
							<strong>&lsquo;Chanson espagnole&rsquo; (Spanish Song)<br>Claude Debussy</strong><br>&mdash;arr. Vivian Fung 
						</p>
						<p>
							In creative terms, Debussy, at 21, was still a late-Romantic French composer of the Massenet/Gounod school when he composed this charming and colourful duet in 1883. It&rsquo;s an example of the powerful attraction that Spanish music held for numerous French composers of the period (Bizet, Massenet, Chabrier, Ravel, Lalo et al.). It was inspired by his first serious (though one-sided) romantic infatuation, with Marie Vasnier, the wife of a much older Parisian architect. The singing lessons he gave her, and her winsome personality, led to his loving her from afar. She regularly sang his early songs in private with the composer at the piano, and together they premiered the <em>Chanson espagnole</em>. He dedicated the song to her. The text is by Louis Charles Alfred de Musset. 
						</p>
