						<p>
							<b>aaadagio<br>Luke Nickel</b> 
						</p>
						<p>
							<i>The composer has provided the following note:</i> 
						</p>
						<p>
							For the last three years, I have not written a single note of music. Instead, I&rsquo;ve made pieces collaboratively with musicians using any means except musical notation. I&rsquo;ve made pieces that consist solely of orally-transmitted descriptions of an imaginary factory. I&rsquo;ve made pieces that chew up existing music and spit it out into tangled new configurations. I&rsquo;ve made pieces for art galleries that contain only speaking, not a note of music to be heard. 
						</p>
						<p>
							This piece for the Manitoba Chamber Orchestra marks my return to traditional musical notation. But with it I bring the great joy and endless possibility that I found using these other compositional methods. 
						</p>
						<p>
							In writing for the MCO, I have chosen to use Barber&rsquo;s Adagio as a source material. I&rsquo;ve always been intrigued by the first musical phrase of the piece: the succulent chords, the sense of floating tension&thinsp;&hellip; 
						</p>
						<p>
							I like to imagine Barber&rsquo;s <i>Adagio</i> as a great ship that sails calmly through our collective subconscious. In my piece <i>aaadagio</i>, we listen to what lies beneath the behemoth ship. What turgid melodies swirl through the deep? What creaking sounds does the ship make while weathering a storm? How does light sound when it glints on the water? 
						</p>
						<p>
							Full disclosure: I&rsquo;m writing this description before I&rsquo;ve written the piece, let alone heard it! So, please consider this a statement of intent, and forgive me if the piece you&rsquo;re about to hear doesn&rsquo;t quite match what you&rsquo;ve read. 
						</p>
						<p>
							In short: A former composition teacher would often jokingly ask me what the effect would be if I used Barber&rsquo;s <i>Adagio</i> to end of one of my compositions. This piece is my answer. (<i>aaadagio</i> is dedicated to Chris Paul Harman, to whom I owe my love of musical provocation.) 
						</p>
						<p>
							<b>Metamorphosen<br>Richard Strauss</b> 
						</p>
						<p>
							On 2 October 1943, the opera house in Munich, Strauss&rsquo;s home city, was destroyed by Allied bombs. The composer was moved to write as follows to his biographer, Willi Schuh: &ldquo;The burning of the Munich Hoftheater, the place consecrated to the first performances of (Wagner&rsquo;s) <i>Tristan</i> and <i>Meistersinger</i>, in which 73 years ago I heard (Weber&rsquo;s) <i>Der Freisch&uuml;tz</i> for the first time, where my good father sat for 49 years as first horn in the orchestra&thinsp;&hellip;&thinsp;this was the greatest catastrophe which has ever been brought into my life, for which there can be no consolation and in my old age, no hope.&rdquo; 
						</p>
						<p>
							Immediately afterwards, he sketched a few bars of music that he labelled <i>Mourning for Munich</i>. He put them aside after they reminded him of a waltz he had composed in 1939 for a documentary film about Munich, a film that was never released. He revised the waltz by adding a sombre, minor-key section inspired by the destruction of the opera house, and christened the resulting concert work <i>Munich: A Memorial Waltz</i>. 
						</p>
						<p>
							In September 1944, conductor Paul Sacher commissioned a new work from him. The following February, the Semper Opera House in Dresden (where eight Strauss operas had premiered, as well as three of Wagner&rsquo;s) suffered a similar fate to Munich&rsquo;s opera house. Then the Vienna State Opera was heavily damaged on 12 March 1945. The very next day, Strauss turned back to the 1943 &lsquo;mourning&rsquo; sketch and used it as the point of departure for <i>Metamorphosen</i> (subtitled &lsquo;a study for 23 solo strings&rsquo;), the work that he composed in response to Sacher&rsquo;s commission. He completed it on 12 April, mere weeks before the end of the war. 
						</p>
						<p>
							It is a deeply emotional, passionately eloquent lament, reflecting his distress over not only the destruction of the opera houses, but what they represented to him: the grand German culture that had nurtured him. For a time, he considered releasing this very personal work for performance only after his death. In the end, he relented. Sacher conducted the premiere on 25 January 1946, leading the Collegium Musicum of Zurich. 
						</p>
						<p>
							Strauss scored <i>Metamorphosen</i> for 10 violins, five violas, five cellos, and three double basses. It falls into three broad, continuous sections. The centre panel is quick in tempo and impassioned in mood. Strauss book-ended it with slow sections, the last even slower and more desolate than the first. 
						</p>
						<p>
							&ldquo;Themes proliferate and interweave in seamless counterpoint,&rdquo; author Michael Kennedy has written. &ldquo;These themes, particularly the first, sound familiar to the listener, not because they are especially reminiscent of earlier Strauss themes, but because they seem to carry echoes of Wagner, Brahms, and others. Only at the very end do we realize what the principal theme really is, when Strauss quotes the opening of the Funeral March from Beethoven&rsquo;s &lsquo;Eroica&rsquo; symphony (No. 3), and writes underneath it in the score &lsquo;In Memoriam!&rsquo; He later said that not until he reached this point did he realize that this was the theme that had been haunting him from the start. In no other work of his is the writing for strings more eloquent, more moving, and more technically accomplished. The thematic allusions are so subtle yet so poignant that we seem to be hearing a funeral hymn for the whole of German music.&rdquo; 
						</p>
						<p>
							<b>Four Last SongsRichard Strauss</b><br>&mdash;arranged by John Greer 
						</p>
						<p>
							Like his eminent predecessors, Schubert, Schumann and Brahms (as well as his contemporaries Hugo Wolf and Gustav Mahler), Strauss made major contributions to the repertoire of German-language <i>lieder</i> (art songs). He composed some 200 in all. He wrote many of the early ones for a specific singer: Pauline de Ahna, the soprano who became his wife in 1894. &ldquo;She sang my songs,&rdquo; he wrote, &ldquo;with an expression and a poetry such as I have never heard since.&rdquo; 
						</p>
						<p>
							Toward the end of 1946, four years after he had composed his most recent songs, he came across <i>Im Abendrot</i> (In the Evening&rsquo;s Glow), a poem by Joseph von Eichendorff (1788-1857). Its mood suited his world-weary, post-war frame of mind perfectly. The characters, an elderly couple gazing into the sunset, reflected his and Pauline&rsquo;s situation like a glove. He completed his setting on 6 May 1948. 
						</p>
						<p>
							An admirer had recently sent him a volume of poems by Hermann Hesse (1877-1962). From it, he chose four pieces, possibly intending to join them together with <i>Im Abendrot</i> to form a song-cycle. He completed only three of them: <i>Fr&uuml;hling</i> (Spring) on 18 July, 1948; <i>Beim Schlafengehen</i> (Falling Asleep), on 4 August; and <i>September</i>, on 20 September. 
						</p>
						<p>
							Strauss died without hearing them in concert. He left no indication that he intended them to be performed together, and therefore no sequence of presentation. Ernst Roth, an editor at his publishing company, Boosey and Hawkes, decided that they formed a cycle. The performing order upon which he settled&mdash;<i>Beim Schlafengehen</i>, <i>September</i>, <i>Fr&uuml;hling</i>, and <i>Im Abendrot</i>&mdash;was followed at the premiere. That took place in London on 22 May 1950, eight months after Strauss&rsquo;s death. Norwegian soprano Kirsten Flagstad was the soloist and Wilhelm Furtw&auml;ngler conducted the Philharmonia Orchestra. Soon afterwards Roth, with the benefit of hearing the songs performed, revised the order into the one by which they have been known ever since. 
						</p>
						<p>
							These mellow, achingly beautiful works represent&mdash;consciously so&mdash;Strauss&rsquo;s musical last will and testament. In them he put aside the realistic horrors of mid-twentieth century life and returned to the ripely Romantic style of his early music, deepened by vast intervening experience. 
						</p>
						<p>
							<i>Fr&uuml;hling</i> (Spring), the first song, pays rapturous, nostalgic tribute to that glorious season of the year, one that seems full of hope after the chill of winter. The cycle of seasons continues in <i>September</i>, the second song. As summer turns inexorably to autumn, text, atmosphere and music darken and decay, and the poet begins to accept the inevitable end of all things. 
						</p>
						<p>
							<i>Beim Schlafengehen</i> (Falling Asleep), the third song, continues the poet&rsquo;s journey towards the afterlife. <i>Im Abendrot</i> (In the Evening&rsquo;s Glow), the fourth song, completes the voyage. After the soprano has sung the final words, &ldquo;ist dies etwa der Tod?&rdquo; (can this, perhaps, be death?), Strauss quoted the &lsquo;transfiguration&rsquo; theme from <i>Death and Transfiguration</i>, a tone poem he had composed 60 years previously. As he lay on his deathbed, he said to his daughter-in-law, Alice, &ldquo;Dying is just as I composed it in <i>Death and Transfiguration</i>.&rdquo; Pauline Strauss died less than a year later, nine days after the first performance of the <i>Four Last Songs</i>. 
						</p>
