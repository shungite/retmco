						<p>
							<b>Earl Stafford</b> 
						</p>
						<p>
							Earl began his music education at the age of eight, making his debut two years later as a piano soloist with orchestra. Since then, he has played around the world, receiving critical accolades for his performances. The Globe and Mail wrote, &ldquo;His pianistic artistry was sheer poetry.&rdquo; He was awarded the gold medal as best accompanist at the 1980 International Ballet Competition in Varna, Bulgaria. 
						</p>
						<p>
							In 1984, Earl was appointed Music Director and Principal Conductor for Canada&rsquo;s Royal Winnipeg Ballet, a position he held until 2009. He has also served as music director for the Saskatoon Symphony, and has held positions on the faculties of the University of Manitoba and The Banff Centre. Earl was invited to conduct for Queen Elizabeth&rsquo;s Royal Jubilee Gala in 2002 and was music director for the Governor General&rsquo;s Awards with the National Arts Centre Orchestra. He has made numerous recordings for CBC and has appeared as a piano soloist on Bravo and A&amp;E. 
						</p>
						<p>
							Earl&rsquo;s work with the Royal Winnipeg Ballet earned him a sterling reputation, which has resulted in numerous repeat invitations as guest conductor. He has worked with many of Canada&rsquo;s finest orchestras including the Toronto Symphony, the National Arts Centre Orchestra, the Symphonies of Vancouver, Victoria, Kitchener-Waterloo, Winnipeg and Saskatoon and the Orchestre M&eacute;tropolitain du Grand Montr&eacute;al. 
						</p>
						<p>
							Internationally, Earl has appeared with the Tokyo Philharmonic, the Hungarian State Opera, the Scottish Chamber Orchestra, the Queen&rsquo;s Hall Orchestra of London and the Theatre Harmony Orchestra of Moscow. Earl has also collaborated with many of the world&rsquo;s finest artists, including James Ehnes, Janina Fialkowska, Desmond Hoebig, Angela Hewitt, Angela Cheng, Josh Groban, Evelyn Hart, Oscar Peterson, Joel Quarrington, Alain Trudel, Stephen Sitarski, Kyoko Takezawa, Mel Torm&eacute;, Ron Paley, Edith Wiens, Mark Zeltzer, Ian Tyson, Neil Sedaka, Fernando Varela, Jann Arden, Anne Murray and more. 
						</p>
						<p>
							Earl is comfortable conducting and playing a variety of styles of music&mdash;from the classics to the avant-garde, and from chamber music to pops. He is a versatile conductor who isn&rsquo;t afraid to take chances. 
						</p>
						<p>
							<b>Measha Brueggergosman</b> 
						</p>
						<p>
							Noted by the San Francisco Chronicle as &ldquo;a singer of rare gifts and artistic intensity&rdquo; and by the Miami Herald for possessing &ldquo;a superb voice capable of just about everything,&rdquo; Canadian soprano Measha Brueggergosman has emerged as one of the most magnificent performers and vibrant personalities of the day. She is critically acclaimed by the international press as much for her innate musicianship and voluptuous voice as for a sovereign stage presence. 
						</p>
						<p>
							3.2 billion television viewers worldwide witnessed the Opening Ceremonies of the Vancouver 2010 Olympic and Paralympic Winter Games, and all heard the lauded soprano&rsquo;s epic performance of the &lsquo;Olympic Hymn.&rsquo; 
						</p>
						<p>
							Notable performances of the recent past include: Mahler&rsquo;s Des knaben Wunderhorn conducted by Jaap van Zweden with the Chicago Symphony; Cage&rsquo;s Aria with Renga under Michael Tilson Thomas at the debut concert of the YouTube Symphony at Carnegie Hall; Messiaen&rsquo;s Poemes pour Mi with Daniel Harding and the London Symphony and Mendelssohn&rsquo;s Elijah with Yannick N&eacute;z&eacute;t-Seguin and the Orchestre M&eacute;tropolitain du Montr&eacute;al. Measha also performed at Gustavo Dudamel&rsquo;s inaugural concert as Music Director of the Los Angeles Philharmonic. 
						</p>
						<p>
							Deeply committed to the art of song, Measha Brueggergosman has given solo recitals at London&rsquo;s Wigmore Hall, Roy Thomson Hall, Carnegie Hall, Spivey Hall, the Kennedy Center, and the Palais des Beaux-Arts in Brussels, and many other venues across North America and Europe. Always happy to try something new, she participated in the Verbier Festival&rsquo;s one-off presentation of Rufus Wainwright Goes Classical, sharing performances of works from the classical repertoire with Rufus Wainwright and Angelika Kirchschlager, arranged especially for the occasion. 
						</p>
						<p>
							Ms Brueggergosman has been honoured to participate in a number of special events including the gala re-openings of Roy Thomson Hall and the University of Michigan&rsquo;s Hill Auditorium, Canada Day celebrations from Parliament Hill in Ottawa, and the opening ceremonies of the XVI International AIDS Conference in Toronto&mdash;sharing the stage with Bill Gates and former President Bill Clinton. She has given a Royal Command Performance for Queen Elizabeth II and has sung for, amongst others, the Prince of Wales, Queen Margrethe II of Denmark, Queen Sonja of Norway, President Tarja Halonen of Finland, Nelson Mandela and Kofi Annan. In 2012 she served as a judge for the inaugural season of Canada&rsquo;s Got Talent. Measha has hosted other cultural variety shows and was the subject of a full-length CBC feature documentary, Spirit in her Voice. 
						</p>
						<p>
							Beyond the great concert halls of the world, Ms Brueggergosman lends her voice, passion, and energy to social and environmental causes as a Canadian goodwill ambassador for three international organizations: the African Medical and Research Foundation; Learning Through the Arts; and the World Wildlife Fund. Her commitments to these organizations have taken her on a broad spectrum of missions&mdash;from primary schools in New Brunswick to internally displaced persons camps of northern Uganda. 
						</p>
						<p>
							In April 2015 Measha and her husband Markus welcomed to their family Sterling Markus, a baby brother for three year old Shepherd Peter. 
						</p>
