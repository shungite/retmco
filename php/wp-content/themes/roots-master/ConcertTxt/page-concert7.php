		<h4><div>Measha Brueggergosman<br>&amp; the genius of Strauss</div></h4>
		<p>Measha Brueggergosman embodies the term &lsquo;diva&rsquo; in the best sense. Who else could boast of both singing opera on the world&rsquo;s most prestigious stages and acting as a judge on Project Runway?</p>
		<p>Winnipeggers and the MCO have been with Brueggergosman since the beginning. As an up-and-coming soloist, the soprano performed an emotional concert with the MCO two weeks after the 2001 World Trade Center attacks. While establishing herself as an international celeb over the course of the next 15 years, she would perform with the MCO no fewer than five times, as well as record a CD with our orchestra for CBC Records.</p>
		<p>This season, the Juno-winner sings the exquisite Four Last Songs of Richard Strauss in an intimate arrangement for piano quintet. With Earl Stafford conducting, the MCO also performs Strauss&rsquo; Metamorphosen and a new work by local composer Luke Nickel.</p>
		<p><b>More Measha</b></p>
		<p>N&eacute;e Measha Gosman, Measha and her husband combined their last names when they married to form Brueggergosman. Never one to follow the beaten path, the celebrated opera singer has recorded Ron Sexsmith and Frank Sinatra tunes, appeared on Much Music&rsquo;s Video on Trial, and performed the <i>Olympic Hymn</i> at the Opening Ceremonies of the 2010 Olympic Games. The vocally acrobatic performance inspired the NBC host to remark with a laugh, &ldquo;That&rsquo;s a hymn for you, right there.&rdquo;</p>

