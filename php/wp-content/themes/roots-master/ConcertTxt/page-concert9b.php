						<p>
							<b>Rh&eacute;al Cenerini</b> 
						</p>
						<p>
							Rh&eacute;al Cenerini is an author who looks at themes of universal significance through the multiple lenses of theatre, poetry and prose. From the search for identity to an examination of human relations in all their aspects, including notions of power and success, his works find inspiration in the writings of Brecht, Claudel, Goethe, Auden and Anouilh. At the same time, his characters and the situations they face are often steeped in the same reality that the author himself knows the best, that of his home province of Manitoba, formed by the coming together of First Nations people, immigrants from throughout the world and the ever-present influence of the American giant at its doorstep. What results from this merger of literary currents and perspectives is as surprising as it is unique. 
						</p>
						<p>
							Rh&eacute;al grew up on a farm just south of the town of Cardinal in south central Manitoba. After finishing high school in Notre Dame de Lourdes, he went on to obtain a literature degree at the St. Boniface College and a diploma in agriculture at the University of Manitoba. He started writing in his early teens, originally focusing on poetry before moving on to theatre with his first play, Aucun Motif (No Motive) which was presented in 1982. Since then, he has continued to write for the stage while at the same time working in a number of agriculture-related areas including teaching farm business management and market gardening. He has written a dozen plays, both in French and in English. Seven have been published with les &Eacute;ditions du bl&eacute; in St. Boniface and five have been staged at Th&eacute;&acirc;tre Cercle Moli&egrave;re. Along with his wife Carol and their four children, he lives on a small farm on the outskirts of La Salle. 
						</p>
						<p>
							<b>Genevi&egrave;ve Pelletier</b> 
						</p>
						<p>
							Genevi&egrave;ve Pelletier is a Canadian actor and director from Winnipeg. Since 2012, she has been the artistic director of Th&eacute;&acirc;tre Cercle Moli&egrave;re, Canada&rsquo;s oldest theatre company, which celebrated its 90th anniversary in 2015. She is interested in cultural crossings and the multiple possibilities opening up in a world that is becoming increasingly small, making for fertile and complex creative spaces. 
						</p>
						<p>
							<b>Th&eacute;&acirc;tre Cercle Moli&egrave;re</b> 
						</p>
						<p>
							Th&eacute;&acirc;tre Cercle Moli&egrave;re is the oldest theatre company with uninterrupted programming in Canada. Established in 1925, the company&rsquo;s founders had the objective of bringing the French language to life within and beyond St. Boniface&rsquo;s vibrant community. Today, the objective remains the same as the company strives to bridge language barriers with their subtitled performances and to present inclusive and diverse programming to appeal to Winnipeg&rsquo;s ever-changing community. &ldquo;The company&rsquo;s future is brimming with new partnerships, co-productions with major players, Canadian tours and new works that reflect the francophone community today,&rdquo; says Genevi&egrave;ve Pelletier, Artistic Director of the organization. &ldquo;We are thrilled about the new developments to come for this 90-year-old company, en route to our centennial.&rdquo; 
						</p>
						<p>
							<b>Michael Oesterle</b> 
						</p>
						<p>
							The Princeton educated Michael Oesterle composes in a style reminiscent of the American minimalists (Terry Riley, John Adams), and offers a balance between simplicity and complexity that has a broad appeal to audiences. This is the second time Dame Evelyn plays Oesterle&rsquo;s <i>Kaluza Klein</i> with the MCO; it was commissioned for her and premiered by the MCO in 2012. 
						</p>
