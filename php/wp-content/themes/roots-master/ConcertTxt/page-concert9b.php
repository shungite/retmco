						<p>
							<strong>L&rsquo;Histoire du Soldat (The Soldier&rsquo;s Tale)<br>Igor Stravinsky</strong> 
						</p>
						<p>
							Stravinsky shot to fame through his brilliant collaborations with the legendary Russian impresario, Sergei Diaghilev, and his dance company, Les Ballets russes: <em>The Firebird</em> (1910), <em>Petrushka</em> (1911) and <em>The Rite of Spring</em> (1913). The First World War temporarily put a halt to the partnership. The Bolshevik Revolution of 1917, through which the imperial Russia where Stravinsky had grown up was dismantled, also made a profound impact on him, in terms both spiritual and practical. 
						</p>
						<p>
							Settling in Switzerland, he solidified and expanded his creative relationship with author, Charles-Ferdinand Ramuz, which had begun in 1915. The tempestuous events of recent history had virtually wiped out Stravinsky&rsquo;s income from his music and his family estates. To make ends meet, he and Ramuz cast about for a joint project that would be inexpensive to produce and which could be toured profitably in Switzerland. A new Stravinsky style, Neo-classicism, was born of those practicalities. <em>The Soldier&rsquo;s Tale</em>, the fascinating hybrid work that was so different from almost everything he had composed previously, showed where he was headed stylistically (and where he would remain for the most part until the 1950s). 
						</p>
						<p>
							Stravinsky found a subject for them to work with in an anthology of Russian folk tales that he came across in the vast library of his father, Fyodor, the principal operatic bass of the Mariinsky Theatre in St. Petersburg. The story about a hapless soldier who strikes a Faust-like, tragic deal with the Devil, was set during the nineteenth-century reign of Tsar Nicholas I. In Ramuz and Stravinsky&rsquo;s version, they minimized references to a specific time and place in order to make their version of the allegorical fable more universal. 
						</p>
						<p>
							They decided to present it as a kind of staged narration with music and a bit of choreography. Something &ldquo;to be read, played and danced,&rdquo; was how they described it. The performing company would include a handful of actors and dancers, accompanied by an ensemble of clarinet, bassoon, cornet, trombone, violin, double bass, and percussion. 
						</p>
						<p>
							Producing <em>The Soldier&rsquo;s Tale</em> proved more complicated than Stravinsky &amp; Co. had expected. Securing sufficiently skilled performers, and adequate rehearsal time and funding, were the largest of many obstacles. Relations among the creators proved fractious at times and everything about the project was in a constant state of flux. It was rescued financially by Werner Reinhart, a generous Swiss patron, who underwrote all expenses. 
						</p>
						<p>
							The premiere took place in Lausanne on 28 September 1918, with Ernest Ansermet conducting. Stravinsky recalled that the portable stage was the size of two armchairs. The show won great success. A tour had been set up, but the Spanish influenza pandemic that had already devastated the war-weakened citizens of other countries swept into Switzerland, infected the contracted performers, and forced the tour&rsquo;s cancellation. 
						</p>
						<p>
							The following year, Stravinsky re-established his relationship with Diaghilev. There was no possibility of the Ballets russes staging <em>The Soldier&rsquo;s Tale</em>, as the temperamental impresario had little interest in a project in which he had had no input. It was only after several years and several unsuccessful productions that this unique, multi-disciplinary presentation found its way to popularity. Its greatest currency has been through the instrumental concert suite that Stravinsky drew from it in 1920, but it makes its full impact only when presented as it was conceived. 
						</p>
						<p>
							Stravinsky&rsquo;s tart, witty and rhythmically intricate score displayed several influences: Russian folk song, Romani fiddlers (as required by the plot, the violin often takes the musical spotlight), klezmer groups, the popular music of Spain (<em>The Royal March</em> is an example of the <em>pasodoble</em>), and his recent exposure to jazz (the ensemble sounds like a Dixieland band at times). &ldquo;My knowledge of jazz was derived exclusively from copies of sheet music,&rdquo; he admitted, &ldquo;and as I had never actually heard any of the music performed, I borrowed its rhythmic style not as played, but as written. I could imagine jazz sound, however, or so I liked to think. Jazz meant, in any case, a wholly new sound in my music, and <em>L&rsquo;Histoire du soldat</em> marks my final break with the Russian orchestral school in which I had been fostered.&rdquo; Among the music&rsquo;s other ingredients are parodies of military marches and popular music forms such as the tango, waltz and ragtime, and even two Luthera chorales, including <em>Ein&rsquo; feste Burg ist under Gott</em> (A Mighty Fortress Is Our God). 
						</p>
						<p>
							<strong>Nanabozho et le tambour<br>Nanabush and the Drum<br>Michael Oesterle&thinsp;/&thinsp;Rh&eacute;al Cenerini</strong> 
						</p>
						<p>
							<em>Rh&eacute;al Cenerini has provided the following note:</em> 
						</p>
						<p>
							When I was approached by the Manitoba Chamber Orchestra and the Th&eacute;&acirc;tre Cercle Moli&egrave;re about participating in this project, I felt strongly that it was important to tap into the history and traditions that make Manitoba such a unique and vibrant place. I hope that <em>Nanabozho et le tambour&thinsp;/&thinsp;Nana&shy;bush and the drum</em> enables their audiences as well as the arts community at large to savour the rich heritage of our region's Aboriginal origins. 
						</p>
						<p>
							<em>Michael Oesterle has provided the following note:</em> 
						</p>
						<p>
							I feel so very lucky to have been asked to write the music for Rh&eacute;al Cenerini&rsquo;s play <em>Nanabozho et le tambour</em>. The MCO/Th&eacute;&acirc;tre Cercle Moli&egrave;re production of this brilliant play continues my long and fruitful relationship with the Manitoba Chamber Orchestra. The production&rsquo;s pretext to create a new piece based on the theatrical and musical format of Stravinsky&rsquo;s <em>L&rsquo;Histoire du soldat</em>&mdash;is quite irresistible. Stravinsky&rsquo;s masterpiece is a perfect combination of basic, yet essential, elements. 
						</p>
						<p>
							It is fascinating that he was able to use the scarcity of resources in Europe during WWI to create an electrifying soundscape bursting with energy and character. For me, the seven instruments in this piece provide a unique combination of individualistic voices. They provoke an immediate sense of dialogue, ideal for telling the story of <em>Nanabozho et le tambour</em>. Working within the theatrical and musical parameters of <em>L&rsquo;Histoire du soldat</em> and the deeply rooted mythology of Cenerini&rsquo;s play has inspired me to work toward an original and contemporary work that is nonetheless steeped in historical and cross-cultural awareness. 
						</p>
