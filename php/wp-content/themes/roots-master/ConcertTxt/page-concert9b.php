				<div>
					Reflecting on the unique legacies of the French and First Nations<br>people in Canada through a spectacular theatrical presentation 
				</div>
			</h4>
			<p>
				<b>Note: ticket links on this page are for the June 8th performances. <a href="http://www.themco.ca/concert/nanabozho-7-june-2016/">June 7th concerts here.</a></b> 
			</p>
			<p>
				The trickster is jealous. He&rsquo;s fallen for the daughter of a powerful chief. Unfortunately for him, another man has laid claim to her heart: a coureur de bois, a little on the na&iuml;ve side to boot! What&rsquo;s a poor trickster to do but send his rival packing with a series of ploys that leave the outsider battered and bruised? The young woman, though, is far stronger, sager and more resourceful that anyone could have imagined&hellip; 
			</p>
			<p>
				The production is unique among MCO collaborations, an immersive new work of art that brings together a group of celebrated artists: Th&eacute;&acirc;tre Cercle Moli&egrave;re, writer Rh&eacute;al Cenerini and composer Michael Oesterle. In the tradition of Stravinsky&rsquo;s <em>A Soldier&rsquo;s Tale</em>, Nanabozho et le tambour&thinsp;/&thinsp;Nanabush and the Drum is an enchanting piece of theatre that merges music, myth, and poetry. 
			</p>
			<p>
				While celebrating Canada&rsquo;s 150th birthday, <em>Nanabush and the drum / Nanabozho et le tambour</em> illuminates the diversity of our heritage. The work is a bilingual work, equal parts French and English. Rich with physical humour, evocative music, and vivid theatrical elements, it will be accessible to a broad audience. Matinee performances for school groups will take place both days. 
			</p>
			<p>
				<b>Th&eacute;&acirc;tre Cercle Moli&egrave;re</b> 
			</p>
			<p>
				Th&eacute;&acirc;tre Cercle Moli&egrave;re is the oldest theatre company with uninterrupted programming in Canada. Established in 1925, the company&rsquo;s founders had the objective of bringing the French language to life within and beyond St-Boniface&rsquo;s vibrant community. Today, the objective remains the same as the company strives to bridge language barriers with their subtitled performances and present inclusive and diverse programming to appeal to Winnipeg&rsquo;s ever-changing community. 
			</p>
			<p>
				&ldquo;The company&rsquo;s future is brimming with new partnerships, co-productions with major players, Canadian tours and new works that reflect the francophone community today. We are thrilled about the new developments to come for this 90-year-old company, en route to our centennial&rdquo; &mdash;&thinsp;Genevi&egrave;ve Pelletier, Artistic Director. 
