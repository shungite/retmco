						<p>
							<strong>Ariel Barnes</strong> 
						</p>
						<p>
							Described as creating a &ldquo;mesmerizing musical experience&rdquo; by combining his &ldquo;deep personal connection&rdquo; (<em>Toronto Live Music Report</em>), &ldquo;luscious tone and technical prowess&rdquo; (<em>The Vancouver Sun</em>), Ariel Barnes has also been hailed as &ldquo;truly an inspiring artist&thinsp;&hellip;&thinsp;the outstanding Canadian cellist of his generation&rdquo; (Maestro Bramwell Tovey). Equally comfortable with baroque through to modern musical languages, his international concert engagements include concerto appearances, chamber music collaborations, solo recitals and world premiers of contemporary art music. 
						</p>
						<p>
							As both a celebrated performer and recording artist, Ariel is often broadcast by CBC Radio 2 and BBC Radio 3, and was selected as one of six cellists across Canada to be a featured artist in CBC Radio&rsquo;s Canadian Bach Cello Suite Project. His recordings have garnered two Western Canadian Music Awards and a JUNO nomination, and are met with critical acclaim by the press. Ariel&rsquo;s concerti performances include appearances with the Vancouver Symphony, Victoria Symphony, Okanagan Symphony, Vancouver Island Symphony, Festival de Febrero Chamber Orchestra and the Turning Point Ensemble, among others. Coming seasons see engagements with the Nuernberger Symphoniker, Kingston Symphony, Okanagan Symphony, Knightwind Ensemble, Vancouver Island Symphony and a touring and recording project with the Manitoba Chamber Orchestra. As a champion of contemporary music, he has had three concerti written especially for him, has commissioned numerous other chamber works, and has premiered well over 50 new pieces in the literature. Ariel has received generous project support from the British Columbia Arts Council, Manitoba Arts Council, Canada Council for the Arts and, most notably, as a winner of the 2012 Canada Council Musical Instrument Bank Competition. The breadth of his musical interests has led to collaborations with such artists as Patrick Watson, Michael Bubl&eacute;, The Tragically Hip, Baroque specialist Elizabeth Wallfisch and maverick harpsichordist Mahan Esfahani. 
						</p>
						<p>
							<strong>Jeffrey Ryan</strong> 
						</p>
						<p>
							Jeffrey Ryan was almost an accountant. Three months into his first semester at Wilfrid Laurier University, he begged to transfer into the Music Faculty to become a composer. Which, after growing up training his ear with Petula Clark, The Partridge Family and Captain and Tennille, playing saxophone and flute in high school bands, singing in two choirs, and writing his own songs for voice class, surprised absolutely no one. 
						</p>
						<p>
							Now, as a freelance composer based in Vancouver, Ryan finds inspiration in the world around him&mdash;nature, science, literature, visual art, even the stock market&mdash;and creates music that runs the gamut from orchestral and chamber works to opera, art song, and choral music. 
						</p>
						<p>
							Praised for his &ldquo;strong personal voice&rdquo; (<em>Globe and Mail</em>) and recipient of SOCAN&rsquo;s Jan V. Matejcek New Classical Music Award, Ryan&rsquo;s music has engaged audiences in concerts and broadcasts around the world. Commissions include works for the Cleveland Orchestra, Vancouver Symphony, Toronto Symphony, Montr&eacute;al Symphony, Victoria Symphony, Manitoba Chamber Orchestra, Aventa Ensemble, Arditti Quartet, Penderecki Quartet, Tafelmusik Chamber Choir, and the Canadian Art Song Project. 
						</p>
						<p>
							Major recent collaborations include <em>Seasons of the Sea</em> with First Nations storyteller Rosemary Georgeson for Vetta Chamber Music, and <em>Afghanistan: Requiem for a Generation</em> with poet Suzanne Steele, commissioned by the Calgary Philhsarmonic Orchestra. 
						</p>
						<p>
							Well represented on disc, recordings of Ryan&rsquo;s music have garnered multiple JUNO and Western Canadian Music Award nominations. His discography includes the portrait CDs <em>Fugitive Colours</em> (Vancouver Symphony/Gryphon Trio/Naxos Canadian Classics) and <em>Quantum Mechanics</em> (Centrediscs), along with many individual works. 
						</p>
						<p>
							Ryan was the Vancouver Symphony&rsquo;s Composer-in-Residence (2002-2007) and Composer Laureate (2008/09). He was an Affiliate Composer with the Toronto Symphony (2000-2002), and is currently Composer Advisor for Music Toronto. He holds degrees from Wilfrid Laurier University, the University of Toronto, and the Cleveland Institute of Music. 
						</p>
						<p>
							<strong>Michael Oesterle</strong> 
						</p>
						<p>
							Michael Oesterle is a Canadian composer living in Deux Montagnes, Qu&eacute;bec. Over the past 15 years he has written several works for the Manitoba Chamber Orchestra, including a cello concerto, a violin concerto, and a concerto for vibraphone and strings. He has also made a chamber orchestra version of Grieg&rsquo;s piano concerto and arranged the two Jan&aacute;&#x10D;ek string quartets for the MCO. Recently he composed the music for the MCO/Th&eacute;&acirc;tre Cercle Moli&egrave;re&rsquo;s production of Rh&eacute;al Cenerini&rsquo;s play <em>Nanabozho et le tambour/Nanabush and the Drum</em>. 
