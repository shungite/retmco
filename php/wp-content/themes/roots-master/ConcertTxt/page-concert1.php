						<p>
							<strong>Earthshine<br>Jeffrey Ryan</strong> 
						</p>
						<p>
							<em>The composer has provided the following note:</em> 
						</p>
						<p>
							Earthshine is sunlight that is reflected off the Earth onto the dark part of the Moon as seen in our night sky. Particularly when the Moon is in a crescent phase, it is possible to see both the crescent brightly illuminated by the Sun, and the rest of the Moon faintly illuminated by the Earth. In this way, earthshine is an impact that our Earth has on something beyond the Earth, while metaphorically, it is a reflection of ourselves that brings light into darkness. <em>Earthshine</em> is a single-movement work for string orchestra exploring light and shadow and the space in between. 
						</p>
						<p>
							<strong> Cello Concerto, Hob. VIIb:1 in C Major<br>Joseph Haydn </strong> 
						</p>
						<p>
							Haydn spent the final 48 years of his life in the employ of the Esterh&aacute;zys, a noble Hungarian family who maintained lavish estates in and around Vienna. His numerous responsibilities included composing operas, symphonies, chamber and vocal music, as well as maintaining the court orchestra and library. Having a superb orchestra to work with was a crucial tool in his quest to expand the contents and meaning of the symphony as a form of music. What more could a composer ask than to have his new pieces played immediately by a &lsquo;crack&rsquo; ensemble? 
						</p>
						<p>
							In gratitude to his musicians, Haydn composed several pieces designed specifically to showcase the superb individual performing skills possessed by many of them. One such work is the Cello Concerto in C Major. Haydn wrote it, probably during the mid-1760s, for Joseph Weigl, leader of the cello section in the Esterh&aacute;zy orchestra. The two were on quite friendly terms, as displayed both by Haydn&rsquo;s gift of this excellent composition, and by the fact that he served as godfather to one of Weigl&rsquo;s sons. 
						</p>
						<p>
							Based on Haydn&rsquo;s inclusion of it in a register that he kept of his own works, the concerto was long known to exist, but the music itself had vanished. It only came to light again in 1961, when a set of authentic parts turned up in the Czech National Library in Prague. They had been sitting for nearly 200 years, undisturbed and uncatalogued, in the personal collection of the Kolovrat-Krakovsky family. The concerto was copied without delay, and received its modern premiere on 19 May, 1962 at the Prague Spring Festival. Miloa S&aacute;dlo was the soloist and Sir Charles Mackerras conducted the Czechoslovak Radio Symphony Orchestra. It has quickly proven itself an international favourite. 
						</p>
						<p>
							The opening movement is confident and sunny. It demands much of the soloist, both technically and expressively. A lyrical slow movement follows, one that exploits the cello&rsquo;s warm, vocal qualities to the full. The concerto concludes with a rhythmic tour-de-force, one of Haydn&rsquo;s most brilliant exercises in virtuoso writing. 
						</p>
						<p>
							<strong> Cello Concerto: &lsquo;The Iron Man&rsquo; 2005 | 2017<br>Michael Oesterle </strong> 
						</p>
						<p>
							<em> The composer has provided the following note: </em> 
						</p>
						<p>
							At the last stage of the archaeological sequence known as the three-age system, iron took the place of bronze and determined man&rsquo;s development as a &lsquo;technological society.&rsquo; The ability to work iron was considered among the greatest triumphs on the timeline of human evolution. It has shaped our values: courage, strength, honour, endurance, but chiefly the arrogant defiance of the limitations of the natural world. Man became the Iron Man, resistant to the&nbsp;vagaries of nature, resistant to change, defiant of history. Our race to control nature has become a self-destructive metaphor as dated and inadequate to our current social and environmental needs as the period of time that it references. 
						</p>
						<p>
							The titles of the five movements reflect the progression of industrialism. <em>To dream of burning coals</em> mimics an early optimism, fast and melodic with appropriately British overtones. Methodical, determined, and contemplative, <em>Abraham Darby</em> refers to three men&mdash;grandfather, father, and son&mdash;who were all pivotal in the development of ironworking processes that made our modern infrastructure possible. <em>The Bloomery Method</em> refers to a pre-industrial ironworking&nbsp;technique replaced by the Darbys. Its smug and capricious overtones suggest that the technique may have been replaced but the nature of man remains the same. The <em>Crucible Technique</em> that replaced the Bloomery Method is a more mature and fatalistic musette, a working man&rsquo;s music with overtones of a medieval organ grinder. <em>Ned Ludd</em>, the name of the mythical leader of the anti-industrial Luddite movement, marks a new awareness of the true meaning of &lsquo;progress&rsquo; with a melancholy song in 13/8 time. It reflects on our inability to shrug off the values of the Iron Age, values that have become as instinctual, as ritualistic, and as hard to disregard as the impulses of our reptilian brain. 
						</p>
						<p>
							This piece was commissioned in 2005 by The Canadian Broadcasting Corporation for cellist Yegor Dyachkov and the Manitoba Chamber Orchestra. The 2017 version has a revised orchestration, written for cellist Ariel Barnes and the MCO. 
						</p>
						<p>
							<strong> Symphony No. 29 in A Major, K. 201/186a<br>Wolfgang Amadeus Mozart </strong> 
						</p>
						<p>
							In 1769, Mozart was awarded the post of Honorary Concertmaster in the Salzburg Court Orchestra. His duties included leading from the first desk of violins (this being before the rise of the conductor, as we know the role), playing solos, and writing new music for the orchestra to perform. Three years would pass before he began receiving a salary for these considerable responsibilities. 
						</p>
						<p>
							Between 1770 and 1773, he composed no fewer than 28 symphonies. This huge output sprang from his ever-practical responses to the demands of the time. One early cause was the death in December 1771 of the Prince-Archbishop of Salzburg, Mozart&rsquo;s employer, with whom he enjoyed a fairly cordial relationship. Mozart&rsquo;s association with his employer would deteriorate catastrophically under the Prince-Archbishop&rsquo;s successor, the strict and unappreciative Hieronymous Colleredo. 
						</p>
						<p>
							Colleredo&rsquo;s outlook meant that theatrical entertainments were banned during the official period of mourning for his predecessor. The public&rsquo;s thirst for music had to be satisfied through instrumental concerts, and Mozart was only too happy to supply material. Once the needs that had inspired this outpouring of symphonies ran their course, his output slackened, never again to reach such concentrated heights. 
						</p>
						<p>
							His symphonies of this period are uniformly pleasant, festive works, perfectly fulfilling their sole ambition: to entertain. In contrast, his next symphony is quite a startling work, one foreshadowed in his catalogue only by the minor key overture to his oratorio <em>La Betulia liberata</em> (Betulia Liberated, 1771). Symphony No. 25 in G Minor, K. 183, which Mozart completed on 5 October 1773, is his first full-scale piece in a minor key, and his first exercise in symphonic drama. Its stark, pulsating opening movement provided an appropriate soundtrack for the harrowing title sequence in the film Amadeus. 
						</p>
						<p>
							Switching moods once again, the next symphony, K. 201 in A Major (known as No. 29 and completed on 6 April 1774), is one of his sunniest, most optimistic orchestral pieces. The light, transparent orchestration (just strings plus pairs of oboes and horns) adds further appeal. Mozart scholar Neal Zaslaw writes of it, &ldquo;Much of what I have stated about K. 183 could be repeated about this work, including the use of sonata form in three of the four movements; the strongly contrasted character of the andante (in this case perhaps noble serenity rather than longing); the symphonic rather than the dance quality of the minuet; and the basing of the opening of the finale on a transformation of the opening of the first movement. 
						</p>
						<p>
							&ldquo;Despite its fully worked-out sonata form, including a development section which Alfred Einstein calls &lsquo;the richest and most dramatic Mozart had written up to this time,&rsquo; the finale has the character of a hunt, with its mandatory repeated notes and other hunting horn calls. The thoroughgoing excellence of this symphony has long been recognized; it and K. 183 are the earliest of Mozart&rsquo;s symphonies in the repertoires of major orchestras.&rdquo; 
						</p>
