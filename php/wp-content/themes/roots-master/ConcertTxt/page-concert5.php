						<p>
							<b>Water Circle<br>Jim Hiscott</b>
						</p>
						<p>
							<i>The composer has provided the following note:</i>
						</p>
						<p>
							<i>Water Circle</i> is inspired by the M&eacute;tis fiddle music of Manitoba and the Prairies, and by an incident I witnessed this past summer at Lake of the Woods. Five adult loons suddenly appeared close to where I was standing&mdash;probably two parents and three grown offspring&thinsp;&mdash;and formed a tight circle moving clockwise, practically head-to-tail. After doing this for a few minutes, they broke apart, and then all dived underwater, maybe to look for fish. Then two came up and took off in one of the wild dances these birds often do, racing across the water, one after the other, while the other three watched. Why the circle-dance? A fishing ritual? What had happened underwater? I&rsquo;ve always been amazed by loons, and loved their many different calls; but this was a real mystery, and will remain so.
						</p>
						<p>
							M&eacute;tis fiddle music has been an inspiration for me throughout my 30-plus years in Manitoba, in my button accordion playing as well as in my composed music. The particular feel and the &lsquo;hoppy&rsquo; jigging dances are infectious and unlike any other music. This piece includes two new reels&mdash;one two-part and the other three-part&mdash;as well as interlocking loon calls. I&apos;m very honoured to be able to write for the Frontier Fiddlers, who play this music so well, and for the Manitoba Chamber Orchestra, who do such wonderful work with young musicians in communities all around Manitoba.
						</p>
						<p>
							<i>Water Circle</i> was commissioned by the Manitoba Chamber Orchestra with the assistance of the Manitoba Arts Council.
						</p>
						<p>
							<b>Violin Concerto No. 2 in E Major, BWV&thinsp;1042<br>Johann Sebastian Bach</b>
						</p>
						<p>
							No direct evidence is available to place Bach&rsquo;s violin concertos at a particular time in his career. In terms of style and activities, the six years he spent in the German town of Anhalt-C&ouml;then seem the most likely period. He was a skillful violinist, although he was far better known as an organist. Carl Philipp Emanuel, the most famous of his sons, stated: &ldquo;In his youth, and until the approach of old age, he played the violin cleanly and penetratingly.&rdquo; Bach wrote exceptionally well for the violin because as Carl continues, &ldquo;He understood to perfection the possibilities of all stringed instruments.&rdquo; Many Bach concertos exist in arrangements featuring a variety of solo instruments. He transcribed this glowing Violin Concerto in E Major for harpsichord. The outer sections bustle with energy. The most remarkable portion is the slow second movement, an especially beautiful creation with the character of a vocal aria.
						</p>
						<p>
							<b>Divertimento for Strings<br>B&eacute;la Bart&oacute;k</b>
						</p>
						<p>
							The commission for this work&mdash;Bart&oacute;k&rsquo;s sole composition for string orchestra&mdash;came from Swiss conductor Paul Sacher. This enterprising and discerning musician commissioned an impressive number of works which have earned the stature of twentieth-century classics, thus playing a significant role in making the chamber orchestra a favoured medium of the period. The list of works he requested included music by Igor Stravinsky (<i>Concerto for Strings</i>); Richard Strauss (<i>Metamorphosen</i>, which the MCO will be performing on 26 April); Arthur Honegger (Symphonies 2 and 4); Paul Hindemith (<i>Harmony of the World Symphony</i>); Frank Martin (<i>Petite symphonie concertante</i>); and another, previous work by Bart&oacute;k: <i>Music for Strings, Percussion and Celesta</i>. 
						</p>
						<p>
							In his second commission to Bart&oacute;k, Sacher specifically asked for something shorter and lighter, and Bart&oacute;k obliged. He composed this Divertimento in August 1939, in the restful retreat of Sacher&rsquo;s Alpine chalet. It was not only the nature of Sacher&rsquo;s request and the ideal working conditions under which Bart&oacute;k composed it that account for its character, but also the overall relaxation and lightening of tone in his music. This trend would continue in further works such as the <i>Concerto for Orchestra</i> and Piano Concerto No. 3. Sacher conducted the Basle Chamber Orchestra in the <i>Divertimento&rsquo;s</i> premiere on 11 June 1940.
						</p>
						<p>
							Bart&oacute;k may have had in mind such entertainment-oriented works as Mozart&rsquo;s Classical-era divertimenti when he began the piece. However, in terms of style and texture, it turned out to have much more in common with the concerto grosso, a Baroque-era form founded upon the interaction of two bodies of strings, one larger than the other. Folk-inflected dance rhythms drive the Divertimento&rsquo;s bright, appealing outer movements. The slow central movement is much different. Something of the political turmoil of the times may have made its way into it. It displays a strong vein of pathos, underpinned by dark, restless undercurrents.
						</p>
