						<p>
							<b>The Frontier Fiddlers</b>
						</p>
						<p>
							Made up of students in the Frontier School Division, the Frontier Fiddlers represents <i>la cr&egrave;me de la cr&egrave;me</i> of young fiddle players in the province.
						</p>
						<p>
							They come from all corners of the province: from Churchill, Brochet, and Waterhen to Falcon Lake, and they play Red River jigs with breakneck speed&thinsp;&hellip;&thinsp;and all of them are teenagers or younger.
						</p>
						<p>
							With appearances in many different communities, and numerous talented young players eager to join their ranks, their members shift a fair amount according to season and location of performance. But their roots are firmly in Manitoba. They&rsquo;ve performed with The Pas Trappers&rsquo; Festival, Festival du Voyageur, and at conferences held by the Canadian School Boards Association, Manitoba Association of School Trustees, Manitoba Association of School Superintendents, the Manitoba Teachers&rsquo; Society, and the Manitoba First Nations Education Resource Centre. They also performed for Her Majesty The Queen during her last visit to Winnipeg, and played a pre-concert recital at an MCO concert in 2010.
						</p>
						<p>
							Members enjoy their energetic, finger-blistering performances as much as audiences do&mdash;a result of the tremendous dedication they show to their craft and the deeply Manitoban tradition of the Red River Jig.
						</p>
						<p>
							<b>Jim Hiscott</b>
						</p>
						<p>
							Jim Hiscott was born in 1948 in St. Catharines, Ontario. In 1971, after earning a Master&rsquo;s Degree in Theoretical Particle Physics, he switched to music composition, studying with Samuel Dolin at the Royal Conservatory of Music and David Lidov and Richard Teitelbaum at York University. His compositions have been performed across North America, and in Europe and Asia by many artists including the Hilliard Ensemble, the St. Lawrence String Quartet, the Winnipeg Symphony Orchestra, the Montreal Symphony Orchestra, the Vancouver New Music Society ensemble, Rivka Golani, Arraymusic, and Philadelphia&rsquo;s Rel&acirc;che.
						</p>
						<p>
							Jim Hiscott has performed his own works for button accordion in the Winnipeg Symphony Orchestra&rsquo;s New Music Festival, the Vancouver New Music Society series, Toronto&rsquo;s Big Squeeze Festival, and on the main stage of the Vancouver Folk Music Festival. He has appeared as button accordion soloist with several major Canadian symphony orchestras. 
						</p>
						<p>
							Recent premieres of music by Jim Hiscott include <i>Spiral</i>, performed by violists Daniel and Michael Scholz; <i>I Spoke to No One</i>, given by the GroundSwell Ensemble; <i>The Song of the Stars</i>, for soprano Maria-Luz Alvarez with the Manitoba Chamber Orchestra; <i>Manimasii Aura</i> (button accordion and chamber ensemble), performed by Simeonie Keenainak and the CBC Radio Orchestra; <i>Beating Heart</i> (solo violin and button accordion with chamber orch.), performed by Atis Bankas, Jim Hiscott, and the Orchestra of St. Mark&rsquo;s; <i>In Memoriam Walter Klymkiw</i> (SATB choir, soloists, and violin solo), given by the Oleksandr Koshetz Choir with vocal soloists and violinist Gwen Hoebig; and <i>North Wind</i> (dizi and orchestra), played by Xiao-Nan Wang and the Winnipeg Symphony Orchestra.
						</p>
						<p>
							String Quartet #2 from his CBC Records compact disc Blue Ocean&thinsp;/&thinsp;Music of Jim Hiscott was nominated for Outstanding Classical Composition at the 2004 Western Canadian Music Awards.
						</p>
