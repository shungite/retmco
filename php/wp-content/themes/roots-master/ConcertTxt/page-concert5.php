						<p>
							<strong>Simone Dinnerstein</strong> 
						</p>
						<p>
							Simone Dinnerstein is one of the most acclaimed pianists of her generation&mdash;called &ldquo;an artist of true expressive force&rdquo; by the Washington Post and &ldquo;a throwback to such high priestesses of music as Wanda Landowska and Myra Hess&rdquo; by Slate. The New York-based pianist gained an international following with the remarkable success of her recording of Bach&rsquo;s Goldberg Variations, for which she independently raised the funds to record. Released in 2007 on Telarc, it ranked No. 1 on the U.S. Billboard Classical Chart in its first week of sales and was named to many &lsquo;Best of 2007&rsquo; lists, including those of the New York Times, Los Angeles Times and the New Yorker. 
						</p>
						<p>
							Dinnerstein&rsquo;s performance schedule has taken her around the world since her acclaimed New York recital debut at Carnegie Hall&rsquo;s Weill Recital Hall in 2005, to venues including the Kennedy Center for the Performing Arts, Vienna Konzerthaus, Berlin Philharmonie, Sydney Opera House, Seoul Arts Center and London&rsquo;s Wigmore Hall; festivals that include the Lincoln Center Mostly Mozart Festival, the Aspen, Verbier and Ravinia festivals; and performances with the Vienna Symphony Orchestra, Dresden Philharmonic, Staatskapelle Berlin, RAI National Symphony Orchestra, Royal Scottish National Orchestra, Czech Philharmonic, Danish National Symphony Orchestra, New York Philharmonic, Minnesota Orchestra, Atlanta Symphony, Baltimore Symphony, Montr&eacute;al Symphony Orchestra, Melbourne Symphony Orchestra, Orquestra a Sinfonica Brasileira, and the Tokyo Symphony. Simone Dinnerstein studied with Solomon Mikowsky, Maria Curcio and Peter Serkin and was an Astral artist. 
						</p>
						<p>
							This season, Dinnerstein will release her new album, Mozart in Havana, recorded with the Havana Lyceum Orchestra. The Orchestra will join her on tour in June, making their U.S. debut. Later this season, Dinnerstein will begin touring the premiere of a new concerto for piano and strings written for her by Philip Glass. Also, in the fall of 2017, Dinnerstein will premiere and begin touring her collaboration with choreographer Pam Tanowitz, New Work for Goldberg Variations. Arriving on the 10th anniversary of Dinnerstein&rsquo;s acclaimed recording, the work is a setting for piano and a sextet of female dancers. 
						</p>
						<p>
							<strong>Marjan Mozetich</strong> 
						</p>
						<p>
							Marjan Mozetich is one of the most performed and broadcast Canadian composers of today. When CBC Radio broadcast a performance of his violin concerto, Affairs of the Heart, the Corporation&rsquo;s switchboards lit up from coast to coast. There were numerous reports of what those who work in radio sometimes call &ldquo;the driveway experience:&rdquo; where listeners are so captivated by what they&rsquo;re hearing that they remain in their cars, listening to the end, even though they&rsquo;ve long since arrived home. His music is spiritual, lush, and romantic, with touches of minimalism. Born in Italy of Slovene parents, he studied piano at the University of Toronto, and in Italy and England. In 1976 he founded and was until 1978 co-artistic director of ARRAY (now called ARRAYMUSIC). Since then he has maintained independence as a freelance composer, while also teaching composition at Queen&rsquo;s University. Leading ensembles from across Canada and the world have performed his music, and many recordings of it have been produced and used variously in film and by dance companies. 
						</p>
						<p>
							<strong>Philip Glass</strong> 
						</p>
						<p>
							Born in Baltimore, Maryland, Philip Glass is a graduate of the University of Chicago and the Juilliard School. In the early 1960s, Glass spent two years of intensive study in Paris with Nadia Boulanger and, while there, earned money by transcribing Ravi Shankar&rsquo;s Indian music into Western notation. By 1974, Glass had a number of innovative projects, creating a large collection of new music for The Philip Glass Ensemble, and for the Mabou Mines Theater Company. This period culminated in Music in Twelve Parts, and the landmark opera, Einstein on the Beach, for which he collaborated with Robert Wilson. Since Einstein, Glass has expanded his repertoire to include music for opera, dance, theater, chamber ensemble, orchestra, and film. His scores have received Academy Award nominations (Kundun, The Hours, Notes on a Scandal) and a Golden Globe (The Truman Show). In the past few years several new works were unveiled, including an opera on the death of Walt Disney, The Perfect American (co-commissioned by Teatro Real, Madrid and the English National Opera), a song cycle entitled If&eacute;, written for Angelique Kidjo, a new touring production of Einstein and the publication of Glass&rsquo;s memoir, Words Without Music, by Liveright Books. In May 2015, the Los Angeles Philharmonic, conducted by Gustavo Dudamel, performed the world premiere of a double piano concerto Glass wrote for Katia and Marielle Lab&egrave;que. 
						</p>
						<p>
							In November, the Washington National Opera premiered a revised version of Glass&rsquo;s opera, Appomattox, created in collaboration with librettist Christopher Hampton. Glass celebrated his 80th birthday on January 31st, 2017 with the world premiere of his 11th Symphony. The 80th birthday season will continue throughout 2017, with performances and celebrations including US Premieres of his operas The Perfect American and The Trial, and the world premiere of String Quartet No. 8 and Piano Concerto No. 3. Glass will begin his tenure as the Carnegie Hall 2017/18 Richard and Barbara Debs Composer&rsquo;s Chair. 
						</div>
					</p>
					<div class="tab-pane fade" id="notes">
						<p>
							<strong>Keyboard Concerto No. 7 in G Minor, BWV 1058<br>Johann Sebastian Bach</strong> 
						</p>
						<p>
							Bach composed his keyboard concertos for the Collegium Musicum concerts in Leipzig. In his time, the solo parts were played on the harpsichord, but pianists have long adopted them as well. They are all based on previously existing works, not all of them his own (the <em>Concerto for Four Keyboards</em>, for example, is a transcription of a concerto for four violins by Antonio Vivaldi). 
						</p>
						<p>
							They are the earliest surviving keyboard concertos, Bach being the first composer to bring the harpsichord forward into the solo spotlight from its long-standing role of accompanist or continuo player. Soloists at the Collegium Musicum performances were often his sons or his finest pupils. 
						</p>
						<p>
							Concerto No. 7 is also widely known in its original form as the Concerto for Violin in A Minor. Having a harpsichord as the solo instrument communicates its personality with equal eloquence. Bach relaxes from the forcefulness and bustle of the opening movement with an andante that sheds its initial air of stateliness and reveals a noble, flowing theme. The finale melds fugue and dance into an exhilarating conclusion. 
						</p>
						<p>
							<strong>Sonata for Strings<br>Sir William Walton</strong> 
						</p>
						<p>
							With time and experience, the jazz-inflected cheekiness and crackling energy that first brought celebrity to Walton coalesced with a deep vein of warmth. This mature style found an enduring welcome in the concert halls of the world. 
						</p>
						<p>
							In 1971, conductor Sir Neville Marriner approached Walton to commission a new work for string orchestra as a vehicle for his ‘crack’ ensemble, the Academy of St Martin-in-the-Fields. The composer declined. Marriner countered with the possibility of a transcription for string orchestra of Walton’s String Quartet in A minor (1947). The composer accepted the proposal eagerly. 
						</p>
						<p>
							Work proceeded during the autumn of the year at Walton’s home in Italy. His friend and fellow composer, Sir Malcolm Arnold, completed the piece by transcribing the finale, under Walton’s supervision. Marriner and the Academy gave the piece, under the new title <em>Sonata for Strings</em>, its premiere in Perth, Australia, in March 1972. 
						</p>
						<p>
							The first movement underwent the most radical changes, primarily in condensation. It opens with the original quartet texture, playing quietly, with mutes in place. When the full ensemble enters, nervous energy makes itself felt, resonating throughout this section. A vigorous fugato bursts forth during the development, but the movement dissolves into peacefulness at the close. Walton made far fewer alterations to the remaining three sections. The first is a darting, headlong scherzo. The sonata’s expressive heart lies in the lyrical slow movement. A dazzling, rhythmically buoyant rondo completes the score. 
						</p>
						<p>
							The MCO recorded this piece on <em>Sea Sketches</em>, an album that CBC Records released in 2003, with former Music Director Roy Goodman conducting. 
						</p>
						<p>
							<strong>Fantasia … sul un linguaggio perduto (Fantasia … on a lost language)<br>Marjan Mozetich</strong> 
						</p>
						<p>
							This work, in its original form for flute, violin, viola and cello, was commissioned by the Ontario Arts Council for the Galliard Ensemble, who premiered it in Edmonton in 1981. 
						</p>
						<p>
							In 1988, CBC Radio commissioned the composer to prepare the transcription for string orchestra that you will hear at this concert. It was recorded by the Amadeus Ensemble on a CBC CD (MVCD1038), and given its concert premiere by the Thirteen Strings Chamber Orchestra of Ottawa in 1993. It is an emotionally compelling and beautifully melodic study in rhythm, a skillful combination of traditional romantic elements (perhaps the “lost language” of the title) and contemporary ones. 
						</p>
