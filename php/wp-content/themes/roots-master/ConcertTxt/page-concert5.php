		<p>How lucky we are to welcome back the Frontier Fiddlers!  </p>
		<p>Made up of students in the Frontier School Division, the group represents <i>la cr&egrave;me de la cr&egrave;me</i> of young fiddle players in the province. They&rsquo;ll treat us to a spectacular medley of Red River jigs and traditional folk music. The Frontier Fiddlers and the orchestra will also together perform a new work by favourite Winnipeg composer Jim Hiscott.</p>
		<p>After intermission, the MCO will perform Bach&rsquo;s second violin concerto, with all the pomp it deserves. Then on to Bartok&rsquo;s <i>Divertimento</i>, a famous 20th-century work that harkens back to the light-hearted 18th-century musical form that is its namesake.</p>
		<p><b>The Frontier Fiddlers</b></p>
		<p>They come from as far away as Grand Rapids, Matheson Island, Berens River, and Norway House. They play Red River jigs with breakneck speed. And all of them are teenagers or younger.</p>
		<p>The Frontier Fiddlers are a select group of talented Frontier School Division students. They performed for Her Majesty The Queen during her last visit to Winnipeg, and played a pre-concert recital at an MCO concert in 2010. They maintain a special connection with the MCO through our northern outreach group, &lsquo;Fiddlers on the Loose,&rsquo; who have led several workshops for the Frontier Fiddlers.</p>

