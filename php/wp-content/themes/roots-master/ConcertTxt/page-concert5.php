			<h4>
				<div>
					Simone Dinnerstein<br>&mdash;&thinsp;Philip Glass premiere 
				</div>
			</h4>
			<p>
				One almost feels regal music should accompany the announcement itself: on January 23rd, the MCO will be performing the Canadian premiere of a new work by Philip Glass, the doyen of post-60s classical music. 
			</p>
			<p>
				Glass is a musical institution. If he&rsquo;s not the father of minimalism&thinsp;&mdash;&thinsp;a title that probably belongs to Terry Riley&thinsp;&mdash;&thinsp;he&rsquo;s certainly its most visible ambassador. The fact that works like <em>Koyaanisqatsi</em>, <em>Mad Rush</em>, and <em>Einstein on the Beach</em> have scored countless Hollywood films and advertisements does not diminish their significance; like Mozart or Gershwin, Glass composes irresistible ear-worms that have left an indelible mark on popular culture. 
			</p>
			<p>
				On January 23rd, the MCO premieres Glass&rsquo; third piano concerto. Pianist Simone Dinnerstein, famous for interpretations of Bach, performs the commissioned work&rsquo;s solo in a concert that also, naturally, includes music by dear old Johann Sebastian. 
			</p>

			<p>
				Also programmed that evening is twentieth century music by Marjan Mozetich and William Walton. If neither are household names, they deserve to be. Mozetich, one of the most broadcast classical composers in Canada, has written a stunning body of work that has been divided into a post-romantic and minimalist period; his magical <em>Fantasia&thinsp;&hellip;&thinsp;sul un linguaggio perduto</em> straddles both. 
			</p>
			<p>
				A performance of Walton&rsquo;s exquisite <em>Sonata for String Orchestra</em> concludes what will surely be one of the most notable Manitoba concerts of early &lsquo;18. 
			</p>
			<p>
				<b>Simone Dinnerstein</b> 
			</p>
			<p>
				American pianist Simone Dinnerstein possesses a &ldquo;majestic originality of vision&rdquo; (<em>The Independent</em>) and is &ldquo;one of the great Beethoven pianists of our time&rdquo; (<em>Philadelphia Inquirer</em>). 
			</p>
			<p>
				She had this to say about the Glass commission: &ldquo;The idea&thinsp;&hellip;&thinsp; first germinated in Philip Glass&rsquo;s garden, where we met for breakfast one beautiful morning in the fall of 2014. There are almost no concertos written for piano and strings since Bach&rsquo;s time. Both Glass and I have a strong interest in the music of Bach and how it impacts us today. The pairing of the Bach concerto with his own composition will create myriad strands of connectivity, enabling the listener to create bridges between the old and the new.&rdquo; 
			</p>
