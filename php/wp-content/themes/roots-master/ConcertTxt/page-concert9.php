			</p>
			<p>
				As an up-and-coming soloist, the soprano performed an emotional concert with the MCO two weeks after the 2001 World Trade Center attacks. While establishing herself as an international star over the next 15 years, she would perform with the MCO no fewer than six times, as well as record her first CD with our orchestra for CBC Records. In the 2017/18 season, we&rsquo;re proud to say that she joins the MCO as Guest Artist-in-Residence. 
			</p>
			<p>
				An astoundingly eclectic interpreter with a special gift for post-Romantic repertoire, Measha will perform Mahler&rsquo;s almost decadently rich <em>Lieder eines Fahrenden Gessellen</em> (Songs of a Travelling Journeyman) at this concert. She explains her motivation for selecting this piece: 
			</p>
			<p>
				&ldquo;When I almost died in 2009, I told myself three things: that I would live closer to my parents in the Maritimes, that I would have kids, and that I would sing more Mahler&hellip;&thinsp;It&rsquo;s something of a miracle as a classical singer to find repertoire that literally feels like it was written for you yesterday.&rdquo; 
			</p>
			<p>
				In addition to this decidedly Germanic offering, the MCO performs great music by Russian, Spanish, and Canadian composers. 
