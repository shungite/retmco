						<p>
							<strong>Turtles All The Way Down<br>Heidi Ouellette</strong> 
						</p>
						<p>
							<em>The composer has provided the following note:</em> 
						</p>
						<p>
							<em>Turtles All the Way Down</em> is a new work for string orchestra with flute, clarinet, piano and percussion commissioned by the Manitoba Chamber Orchestra. The piece is inspired by the work of children&rsquo;s author and illustrator Jon Klassen. Originally from Winnipeg, Klassen&rsquo;s work has a uniquely sparse yet expansive style&mdash;reflected in both his imagery and narratives. I am particularly drawn to this dichotomist combination of qualities in both visual and aural realms. 
						</p>
						<p>
							This new composition is specifically inspired by the third book of Klassen&rsquo;s Hat Trilogy, <em>We Found a Hat</em>. The story follows two turtles who discover a hat together and are quickly faced with a dilemma: there is one hat to be had, and two of them who want it. After delightful tension builds due to the problematic equation of one hat and two turtles, the story takes a surprising, surreal turn. The story ends in a dream sequence with both turtles floating in a starry sky, each wearing a hat. It is a striking image and conclusion that continues to resonate for some time after finishing the book.
						</p>
						<p>
							While the piece is not necessarily an aural &lsquo;scoring&rsquo; of the book, it is intended to have a narrative quality. The friendship between the two turtles acts as a starting point to explore musical relationships. Meanwhile, the striking desert setting of <em>We Found A Hat</em> (a prominent and essential element of the book) inspires the harmonic and textural context of the piece. The story takes place over the course of a day, with the sky moving from day to night throughout the illustrations. The piece also reflects this cyclical nature. With direct inspiration from the magical ending in the book, the piece concludes with a somewhat unexpected sparkling, optimistic finale. 
						</p>
						<p>
							Much of Klassen&rsquo;s original material is straightforward and understated: the muted colour scheme, desert setting, humble protagonists, and minimal text. On the surface, this is a simple, charming story about two turtles. However, it is also an exploration of honesty, generosity, and perception. <em>Turtles All the Way Down</em> reflects this multi-layered approach, capturing a sound world that combines restraint and minimalism with personality, vulnerability and occasional bursts of vibrant resonance. 
						</p>
						<p>
							<strong>Serenade for Strings<br>Vasily Kalinnikov</strong> 
						</p>
						<p>
							The tragic brevity of Kalinnikov&rsquo;s life silenced a genuine talent before it was able to blossom fully. His legacy is slim but it is a treasure to listeners who enjoy the music of Rimsky-Korsakov, Tchaikovsky and Borodin. 
						</p>
						<p>
							The son of an ecclesiastical family, he began his education at the seminary in Oryol after his family moved there. His talent enabled him to become director of the institute&rsquo;s choir at age 14. In 1884, he relocated to Moscow to study at the Conservatory, but because he couldn&rsquo;t afford the fees he had to drop out after only a few months. A short time later, he won entrance to the music school of the Philharmonic Society on scholarship. After graduation in 1892, he eked out a meagre living by playing the bassoon, timpani and violin in various ensembles, and by working as a music copyist. 
						</p>
						<p>
							Tchaikovsky came to admire him and recommended him for the position of music director of the Mal&iuml;y Theater, and later for the Italian Theater, as well. Signs of the tuberculosis that Kalinnikov&rsquo;s life of deprivation had brought about soon made themselves felt, however, and doctors ordered him to seek a warmer, healthier climate. He spent the rest of his life in Ukraine, devoting himself to composition and relying upon friends to support him. 
						</p>
						<p>
							He created primarily orchestral works, including two splendid symphonies, a suite, two intermezzi, a handful of overtures and symphonic poems, and incidental music for Aleksey Tolstoy&rsquo;s play <em>Tsar Boris</em>. The celebrated composer and pianist Sergei Rachmaninoff paid him a visit. Appalled by the wretched state in which Kalinnikov was living, Rachmaninoff gave him financial assistance and helped to have some of his works published. These fees went to Kalinnikov&rsquo;s widow, after her husband&rsquo;s death at age 34. 
						</p>
						<p>
							The first of the two Russian serenades for strings on this program is a student work, dating from 1891 when Kalinnikov was 25. It was performed at his graduation concert the following year. Consisting of a single movement, it has the character of a slow, melancholy waltz. 
						</p>
						<p>
							<strong>Lieder eines fahrenden Gesellen (Songs of a Wayfarer)<br>Gustav Mahler<br>&mdash;arr. Arnold Schoenberg </strong> 
						</p>
						<p>
							The character of the forlorn, heart-broken wanderer became a familiar and highly representative figure in romantic art. This early work of Mahler presents a marvelous update of the tradition into late nineteenth century terms. It may include an element of autobiography, Mahler regularly declaring himself something of an outcast. 
						</p>
						<p>
							He composed <em>Songs of a Wayfarer</em> from 1883 to 1885, initially for voice and piano. The version with orchestral accompaniment followed during the early 1890s. It was premiered by the Berlin Philharmonic on March 16, 1896, with baritone Anton Sistermanns as the soloist and the composer conducting. 
						</p>
						<p>
							Mahler wrote the text himself, under the clear influence of <em>Des Knaben Wunderhorn</em> (The Youth&rsquo;s Magic Horn), a collection of German folk poetry that exercised a profound, long-lasting influence on his music. He is known to have written two further poems for the cycle, but he did not include them in the final score and may never even have set them to music. 
						</p>
						<p>
							Shortly thereafter, he began Symphony No. 1 in D Major, a work which shares not only much of the same atmosphere with the song-cycle, but two musical themes. The inter-connection between song and symphony would become a recurring feature in his music. 
						</p>
						<p>
							Mahler and Arnold Schoenberg (1874-1951) developed a cordial, if occasionally strained, personal and professional relationship. Mahler appreciated Schoenberg&rsquo;s early music, with its late-romantic style reflecting his own. For Schoenberg&rsquo;s part, despite their occasional differences, he held Mahler in great esteem, regularly performing the older man&rsquo;s music at the concerts he organized. 
						</p>
						<p>
							In Vienna in 1918, seven years after Mahler&rsquo;s death, Schoenberg and his followers established an organization called The Society for Private Musical Performances. Their concerts of chamber music were open only to subscribers, no publicity was undertaken, and reviews were not permitted. The audience was not even allowed to express its pleasure or displeasure with what they heard&mdash;no applause, hissing or booing, thank you! 
						</p>
						<p>
							The Society&rsquo;s goal was simply to give this often complex music the hearing it was unlikely to receive otherwise, and under the most sympathetic conditions possible. The best available musicians were engaged, and they were allowed as much rehearsal time as was deemed necessary. Subscribers were not informed of the repertoire before the concerts, and most pieces were scheduled more than once, in order to give listeners the opportunity to become more familiar with them. 
						</p>
						<p>
							In the society&rsquo;s three-year existence, 113 concerts took place and more than 150 works by 42 composers were heard, including pieces by such major figures as Stravinsky, Debussy, Ravel, Bart&oacute;k, Strauss, Reger, Schoenberg and Zemlinsky. The organizers wished to present orchestral works, but they were faced with practical and financial limitations that made this impossible. They prepared special transcriptions of the orchestral works that appealed to them, some 50 in all. The early arrangements were confined to one or two pianos, but more elaborate forces gradually came into use. 
						</p>
						<p>
							Eventually the expense of such an ambitious enterprise made it no longer financially feasible, and the Society&rsquo;s final performance took place on 5 December 1921. 
						</p>
						<p>
							Schoenberg&rsquo;s chamber version of Mahler&rsquo;s <em>Songs of a Wayfarer</em> was premiered at a Society concert on 6 February 1920. The scoring is for voice, flute and piccolo, clarinet, piano, harmonium, percussion and strings. 
						</p>
						<p>
							The first song presents the narrator&rsquo;s unhappy reaction to the approaching marriage of a former lover. Between its gloomy, yearning opening and closing verses the tone shifts briefly to a more positive sentiment, reflecting the poet&rsquo;s abiding love of nature. That cheerful atmosphere continues in the second song, as the narrator sets out across a field on a sunny spring morning. Mahler used this melody in the first movement of Symphony No. 1. By song&rsquo;s end, happiness again gives way to doubt. 
						</p>
						<p>
							Stark drama pervades the next number, as the poet reacts in agonized tones to the &ldquo;red-hot knife&rdquo; that failure in love has lodged in his heart. The final song begins with a funeral march, a type of piece Mahler would go on to use regularly. In the peaceful concluding section, the wanderer is finally granted a measure of contentment, through the tranquility and lack of pain he expects to find in the afterlife. Mahler transplanted this section to mid-way through the third movement of Symphony No. 1. 
						</p>
						<p>
							<strong>Tres Sonetos (Three Sonnets), Op. 54<br>Joaqu&iacute;n Turinar<br>&mdash;arr. Vivian Fung</strong> 
						</p>
						<p>
							This Spanish composer&rsquo;s parents wanted him to pursue a career in medicine, but the musical gift that he had displayed from childhood could not be denied. His studies began with piano lessons and progressed to composition. Early successes emboldened him to visit Madrid in hopes of having an opera performed there. This proved impossible for an unknown composer from the provinces. 
						</p>
						<p>
							Under the guidance of a generous and more experienced composer, Manuel de Falla, he tried his hand at composing zarzuelas, a form of operetta. Finding no success in that field, either, he relocated to Paris and undertook further study. His instructors at the Schola Cantorum included Vincent d&rsquo;Indy. He composed several works in a modernist style, but they generated little enthusiasm. 
						</p>
						<p>
							In 1907, Turina and de Falla took part in a Parisian performance of a Turina quintet. The celebrated Spanish composer, Isaac Alb&eacute;niz, was in the audience. Afterwards he took his two young colleagues to a caf&eacute; and recommended that Turina cast off his conservative models and look to Spanish folk music for inspiration. Turina decided to take this advice, but only to the point of making folk flavouring one ingredient in his style. The shift earned him the breakthrough he had been longing for, through such pieces as the vivid orchestral tone poem, <em>La procesi&oacute;n del Rocio</em> (1913). 
						</p>
						<p>
							Turina composed songs throughout his career. The <em>Tres Sonetos</em> date from 1930. The texts are by Francisco Rodr&iacute;guez Mar&iacute;n (1855-1943), a Spanish poet and scholar who was devoted to Spanish literature, and who published several editions of Cervantes&rsquo; classic <em>Don Quixote</em>. The set begins and ends with ardent love poems (<em>Yearning</em>, and <em>To those eyes</em>). In between comes the playfully cynical <em>Go away!</em>. 
						</p>
						<p>
							<strong>Serenade for Strings in C Major, Op. 48<br>Pyotr Il&rsquo;yich Tchaikovsky</strong> 
						</p>
						<p>
							The cheery Mozart and the often gloomy Tchaikovsky might seem an odd pair, but they had more in common than one might think. Both were superb entertainers and composed magnificent dance music, and shared a flair for musical drama, with or without voices. 
						</p>
						<p>
							Besides, Tchaikovsky, like virtually all great composers, adored Mozart. &ldquo;I not only like Mozart, I idolize him,&rdquo; he wrote. &ldquo;He captivates, delights and warms me. To hear his music is to feel that one has done a good deed. It is my profound conviction that Mozart is the culminating point of musical beauty. In Mozart I love everything, for we love everything in those we love truly. He was as pure as an angel, and his music is rich in divine beauty.&rdquo; 
						</p>
						<p>
							Tchaikovsky paid his respects to Mozart several times. He did so as directly as possible through his Suite No. 4, <em>Mozartiana</em> (1887), which presents orchestrations of his idol&rsquo;s music for piano and for voices. In terms of general classical style, he made homage through the delicious <em>Variations on a Rococo Theme</em> for cello and orchestra (1876), and this glowing <em>Serenade for Strings</em> (1880). For all its classical leanings, it is a romantic work through and through. 
						</p>
						<p>
							He composed it immediately after the bombastic <em>1812 Overture</em>, a commissioned work that he had written hastily and with little enthusiasm. &ldquo;The overture will be very loud and noisy, but I wrote it without any warm feelings of love and so it will probably be of no artistic worth,&rdquo; he confided to his patroness, Nadezhda von Meck. &ldquo;But the <em>Serenade</em>, on the contrary, I wrote from inner compulsion. This is a piece from the heart and so, I venture to say, it does not lack artistic worth.&rdquo; Completed on 4 November 1880, it had only a month to wait for its first performance, given by students at the Moscow Conservatory on December 3rd as a surprise gift to the composer. The public premiere followed in October 1881, and won huge success. 
						</p>
						<p>
							Tchaikovsky told a friend that he intended the opening movement as a tribute to Mozart, whose opera <em>The Magic Flute</em> he had recently been studying for relaxation. A stately introduction in slow tempo leads to a vigorous and charming allegro. A gracious waltz, one of Tchaikovsky&rsquo;s best, follows, and then a sweet, heartfelt elegy. The finale makes use of two Russian folk songs. The first is treated as a slow, wistful introduction. The second kicks off the ensuing allegro with mischievous charm, a quality abetted by a gracious second theme of Tchaikovsky&rsquo;s own invention. Near the close, the return of the first movement&rsquo;s opening theme brings the serenade full circle. 
						</p>
