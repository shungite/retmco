						<p>
							<strong>Measha Brueggergosman</strong> 
						</p>	﻿ 
						<p>
							Motivated and hungry for new experiences, Measha Brueggergosman&rsquo;s highlights on the opera stage include Giulietta and Antonia in <em>Les contes d&rsquo;Hoffmann</em>, Elettra in <em>Idomeneo</em>, Madame Lidoine in <em>Dialogues des Carm&eacute;lites</em>, Jenny in Weill&rsquo;s <em>Mahagonny</em>, Emilia Marty in <em>V&#x11B;c Makropulos</em>, Hannah in Miroslav Srnka&rsquo;s <em>Make No Noise</em>, and Sister Rose in Jake Heggie&rsquo;s <em>Dead Man Walking</em>. 
						</p>
						<p>
							On the concert platform she has appeared with numerous organizations, including the New World Symphony Orchestra and the symphony orchestras of Boston, Cleveland, San Francisco and London, and the New York Philharmonic&mdash;with conductors such as Barenboim, Sir Andrew Davis, Dudamel, Harding, David Robertson, Tilson Thomas and Welser-M&ouml;st. 
						</p>
						<p>
							Measha began her career predominantly committed to the art of the song recital. She has presented innovative programs at Carnegie Hall, Washington&rsquo;s Kennedy Center, London&rsquo;s Wigmore Hall, both the Konzerthaus and Musikverein in Vienna, Madrid&rsquo;s Teatro Real, as well as at the Schwarzenberg, Edinburgh, Verbier and Bergen Festivals with celebrated collaborative pianists Justus Zeyen, Roger Vignoles, Julius Drake, and Simon Lepper. 
						</p>
						<p>
							Her first recording for Deutsche Grammophone, <em>Surprise!</em>, includes works by Schoenberg, Satie and Bolcom and is one of the most highly regarded debut albums of recent years. Her subsequent disc <em>Night and Dreams</em>, which features songs by Mozart, Brahms, Strauss, Schubert, Debussy, Duparc and Faur&eacute;, won several awards and her recording of the <em>Wesendonk Lieder</em> with Franz Welser-M&ouml;st and the Cleveland Orchestra earned her a Grammy nomination. <em>So Much To Tell</em>, her 2005 recording with the MCO, was nominated for a JUNO&#x2008;Award. 
						</p>
						<p>
							Measha Brueggergosman champions the education and involvement of new audiences and holds several honorary doctorates and ambassadorial titles with international charities. 
						</p>
						<p>
							<strong>Heidi Ouellette</strong> 
						</p>
						<p>
							Heidi Ouellette is a Winnipeg-based composer, curator, cultivator and administrator of the arts. She thrives in the space in between these roles and often blurs the lines between these titles. She is eager to collaborate with other performers and artists, and seeks projects that allow her to do so. Her interests lie in innovative approaches to music composition, incorporating recycling or re-use of music and materials (including re-conceptualization and re-contextualization),&nbsp;improvisation, and multi-disciplinary endeavours.&#x2028;&#x2028; 
						</p>
						<p>
							In recent years, Heidi has devoted much of her creative energy to co-founding and directing the Cluster: New Music&thinsp;+&thinsp;Integrated Arts Festival in Winnipeg. Curating this annual festival of contemporary sound and art has expanded her artistic focus from strictly musical to multi- and inter-disciplinary realms, has evolved her approach to her own composition, and reflects a conscious shift towards a more community-driven approach to her work. With an increasing&nbsp;awareness of the collective ecology of the arts, Heidi strives to contribute to the growth and vitality of the artistic communities around her. 
						</p>
						<p>
							In tandem with the development of Cluster, Ms Ouellette has built a career as a leader in the arts&mdash;most recently acting as Executive Director for GroundSwell. Heidi approaches her work with the local new music concert series (and collaborations with a myriad of other organizations) as an extension of her efforts to create, curate and cultivate the arts in Winnipeg, Canada, and beyond. 
						</p>
