		<h4><div>Suzie LeBlanc<br>&amp; the poetry of Elizabeth Bishop</div></h4>
		<p>Soprano Suzie LeBlanc will return to our stage for her third MCO appearance, but this time she&rsquo;s planned the concert around a personal project.</p>
		<p>The Acadian singer first connected with the work of American poet Elizabeth Bishop five years ago after finding a leaflet in the basement of a church on the Bay of Fundy. She felt strangely drawn to the little photo of the poet and set out to learn all she could about her.</p>
		<p>The power in Bishop&rsquo;s words had a profound effect on LeBlanc and several composers became aware of her interest &mdash; they, too, were fans!</p>
		<p>Christos Hatzis has set music to the poems <i>I Am in Need of Music</i>, <i>Insomnia</i>, <i>The Unbeliever</i>, and <i>Anaphora</i>; and we will hear <i>Silken Water</i> from <i>The Bishop Suite</i> by Alasdair MacLean.</p>
		<p><b>Elizabeth Bishop</b></p>
		<p>Elizabeth Bishop (1911-1979) was the Poet Laureate of the United States in 1949 and 1950. She won the Pulitzer Prize for Poetry in 1956 and the National Book Award in 1970 and, in 1976, was the recipient of the Neustadt International Prize for Literature.</p>
		<p>Bishop was born in Massachusetts, but lived for a time in Great Village, Nova Scotia. It was in the basement of a church on the Bay of Fundy that Acadian Suzie LeBlanc began to explore the poetry of Elizabeth Bishop.</p>

