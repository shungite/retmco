			<p>
				Musically, Arvo P&auml;rt strikes an unusual pose: one foot in the minimalist tradition of the twentieth century, and the other all the way back in the music of the Middle Ages. It is perhaps because he is a musical giant that P&auml;rt is capable of accomplishing this act of extreme flexibility. 
			</p>
			<p>
				While the Estonian&rsquo;s mystical music once inflamed Soviet cultural authorities, today it is practically a pop cultural phenomenon. Works like <em>Fratres</em>, <em>Spiegel im Spiegel</em>, and <em>Tabula Rasa</em> have scored countless films (<em>There Will be Blood</em>, <em>The Place Beyond the Pines</em>, <em>Gravity</em>), and he remains the most widely performed living composer. 
			</p>
			<p>
				Still, local opportunities to enjoy a live performance of P&auml;rt&rsquo;s <em>Te Deum</em> are somewhat rare, given the work&rsquo;s expansive orchestration. In it, choir, strings, piano and pre-recorded tape drift in out of the foreground to conjure an atmosphere that is serene and staggeringly beautiful. This performance of <em>Te Deum</em> is made all the more special because it&rsquo;s the 45th anniversary concert for both the MCO and The Winnipeg Singers. 
			</p>
			<p>
				But before <em>Te Deum</em>, we&rsquo;ll hear extracts from Eleanor Daley&rsquo;s <em>Requiem</em>, another moving contemporary choral work with a deep, contemplative quality. <em>Requiem&rsquo;s</em> a cappella arrangement will allow The Winnipeg Singers to take full advantage of our venue&rsquo;s superb acoustics, which respond so well to the resonance of voices. Our 45th anniversary concert concludes with a performance of Ola Gjeilo&rsquo;s <em>Sunrise Mass</em>, an uplifting work that seems, like the MCO and The Winnipeg Singers, to gaze optimistically upon new horizons. 
			</p>
			<p>
				<b>The Winnipeg Singers</b> 
			</p>
