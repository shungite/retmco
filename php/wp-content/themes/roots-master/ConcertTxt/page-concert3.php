						<p>
							<strong>The Winnipeg Singers</strong> 
						</p>
						<p>
							The Winnipeg Singers has long been regarded as one of Canada&rsquo;s finest choral ensembles. The choir consists of 24 trained voices, performing music that spans the Renaissance to the contemporary. It presents an annual concert series, engaging some of North America&rsquo;s finest musicians as guests, and collaborates with such groups as the Manitoba Chamber Orchestra and Winnipeg&rsquo;s Contemporary Dancers. 
						</p>
						<p>
							The Winnipeg Singers began in 1940 as &lsquo;The Choristers,&rsquo; a CBC Radio choir, directed by W.H. Anderson. In the 1950s Filmer Hubble conducted the group in a weekly CBC broadcast called <em>Sunday Chorale</em>. In the 1970s Dr. William Baerg formed the &lsquo;CBC Winnipeg Singers,&rsquo; performing concert broadcasts. When the CBC was no longer able to support the choir, The Winnipeg Singers began to produce its own annual concert series, which it has done ever since. Past Artistic Directors include Bill Baerg, John Martens, Wayne Riddell, Mel Braun, Vic Pankratz and Rudy Schellenberg. The current Artistic Director, Yuri Klaz, began his tenure with the 2003/04 season. 
						</p>
						<p>
							In 2005 the choir represented Canada at the World Choral Symposium on Choral Music in Kyoto, and in 2016 toured Germany, Austria and Italy, and was recognized at the Florence International Festival as &ldquo;most outstanding choir.&rdquo; 
						</p>
						<p>
							<strong>Yuri Klaz</strong> 
						</p>
						<p>
							Born in Petrozavodsk, Russia, Yuri Klaz graduated from his hometown&rsquo;s Music College and Conservatory before finishing his Master&rsquo;s degree in conducting at the St. Petersburg State Conservatory. In 1982, he was appointed an Associate Professor of choral and orchestral conducting at the Petrozavodsk Conservatory and, in 1987, became the Artistic Director and Conductor of the Chamber Choir of Petrozavodsk&rsquo;s Karelian Art Centre. Under his guidance, the choir earned numerous awards performing in concert tours and music festivals in Russia, Germany, Finland, Estonia, Ireland and Norway. 
						</p>
						<p>
							In 1995, by decree of President Boris Yeltzin, Mr. Klaz was awarded the prestigious title, &lsquo;Honoured Artist of Russia,&rsquo; investing him with a silver medal for exceptional success in the development of art in Russia. In 2000, the Winnipeg Philharmonic Choir recruited Yuri Klaz as its Artistic Director and Conductor. Quickly becoming a leader in Winnipeg&rsquo;s choral community, in 2003 he was appointed Artistic Director and Conductor of The Winnipeg Singers, and now also directs the First Mennonite Church Choir. In 2005, Mr. Klaz made a critically acclaimed debut as a conductor with the WSO in a choral series that included Faur&eacute;&rsquo;s <em>Requiem</em>, Verdi&rsquo;s <em>Stabat Mater</em> and J.S. Bach&rsquo;s Cantata No. 4. 
						</p>
						<p>
							In 2006 Mr. Klaz made his first appearance with the Manitoba Chamber Orchestra conducting Mozart&rsquo;s Great Mass in C minor with the Winnipeg Philharmonic Choir and The Winnipeg Singers. He has also led The Winnipeg Singers in several performances at the WSO&rsquo;s New Music Festival. 
						</p>
						<p>
							Mr. Klaz has directed choirs around the world, including United States, Japan, Taiwan, Germany, Russia, Austria, France, Italy and The Netherlands, and has attended World Choral Symposiums in Vancouver, Minneapolis, Sydney, Kyoto and Copenhagen. Over 40 years of his conducting career Mr. Klaz has conducted numerous major choral works, including J.S. Bach&rsquo;s Mass in B Minor and Weihnachts-Oratorium (Christmas Oratorio); Handel&rsquo;s Messiah; Brahms&rsquo; <em>Ein Deutches Requiem</em>; Bruckner&rsquo;s <em>Requiem</em>; Mozart&rsquo;s <em>Requiem</em>; Mendelssohn&rsquo;s <em>Elijah</em>; Rutter&rsquo;s <em>Magnificat</em>; P&auml;rt&rsquo;s <em>Berliner Messe</em> and, most recently, Rachmaninoff&rsquo;s <em>Vespers</em>. 
							<p>
							</p>
							In June 2010, Yuri Klaz was honoured with the Winnipeg Arts Council&rsquo;s &lsquo;Making a Mark&rsquo; award in recognition of his contribution to choral music performance. In July 2016 Mr. Klaz led The Winnipeg Singers performing at the 5th International Choral Competition in Florence, bringing back home the Best Choir Award&mdash;&lsquo;Golden David.&rsquo; 
						</p>
						<p>
							<strong>Arvo P&auml;rt</strong> 
						</p>
						<p>
							A devout Orthodox Christian, Estonian composer Arvo P&auml;rt developed a style based on the slow modulation of sounds such as those produced by bells and pure voice tones, a technique reminiscent of the medieval Notre-Dame school and the sacred music of Eastern Orthodoxy. His major works include the violin concerto <em>Tabula Rasa</em> (1977), <em>Cantus in Memory of Benjamin Britten</em> (1977), <em>Magnificat-Antiphones</em> (1988), <em>The Beatitudes</em> (1991), and <em>Lamentate</em> (first performed 2003). His medieval liturgical sound won him a significant audience in the West during the late 1990s. Today he is one of the world&rsquo;s most widely performed composers. 
						</p>
						<p>
							<strong>Eleanor Daley</strong> 
						</p>
						<p>
							Eleanor Daley has been Director of Music at Fairlawn Avenue United Church in Toronto since 1982, and is currently the conductor of three choirs there. She has been the accompanist for the Bach Children&rsquo;s Chorus since 1995, and was the accompanist of the Amadeus Choir from 1991 to 2005. 
						</p>
						<p>
							A prolific composer, Eleanor has a remarkable gift for melody and sensitive interweaving of text and music. She has over 100 choral compositions in publication, and is commissioned extensively throughout North America and Europe. Her compositions have been performed and recorded in North America, Great Britain, Europe, South America and the Far East. Eleanor&rsquo;s compositions have twice received the National Choral Award for Outstanding Choral Composition of the Year by the Association of Canadian Choral Conductors at their national conference. In 2004 Eleanor was invited to be the first Composer-in-Residence at the international choral festival, Festival 500, in St. John&rsquo;s Newfoundland. 
						</p>
						<p>
							<strong>Ola Gjeilo</strong> 
						</p>
						<p>
							Ola Gjeilo (pronounced Yay-lo) is one of the most frequently performed composers in the choral world. An accomplished pianist, Gjeilo&rsquo;s improvisations over his own published choral pieces have become a trademark of his collaborations across the world. Born in Norway in 1978, Gjeilo moved to the United States in 2001 to begin his composition studies at the Juilliard School in New York City, where he currently resides and works as a full-time composer. Ola&rsquo;s albums include his 2016 Decca Classics label debut, <em>Ola Gjeilo</em>, featuring Tenebrae and Voces8, as well as his two piano records (<em>Stone Rose</em>, <em>Piano Improvisations</em>) and Phoenix Chorale&rsquo;s <em>Northern Lights</em>. Gjeilo&rsquo;s choral works are published by Walton Music, wind band works by Boosey &amp; Hawkes, and piano pieces by Walton Music and Edition Peters. 
						</p>
					</div>
					<div class="tab-pane fade" id="notes">
						<p>
							<strong>Selections from Requiem<br>Eleanor Daley</strong> 
						</p>
						<p>
							A Requiem is a Mass for the dead. Numerous composers have created them, with those of Mozart and Verdi standing as the most widely known examples (in addition to those by such other major composers as Victoria, Cherubini, Berlioz, Bruckner, Brahms, Faur&eacute;, Dvo&#x159;&aacute;k and Britten). Eleanor Daley composed this heartfelt example, for unaccompanied voices, in 1993. In 1994, it was named the outstanding new choral composition of the year by the Association of Canadian Choral Conductors. It has been recorded several times. 
						</p>
						<p>
							It was commissioned by Jake Neely, a member of the distinguished Canadian choir, the Elmer Iseler Singers. The request wasn&rsquo;t specific. Neely was caring for a terminally ill loved one at that time, and Daley thought it appropriate to reply to the commission with a Requiem. The Elmer Iseler Singers, with Iseler conducting, gave the premi&egrave;re at the Festival of the Sound in Parry Sound, Ontario, on 17 July 1993. 
						</p>
						<p>
							The texts are an eclectic mixture of the traditional Latin <em>Missa pro Defunctis</em> (Mass for the Dead), a Russian benediction, the <em>Book of Common Prayer</em>, Psalm 130, and poems by the American Mary Elizabeth Frye and the Canadian Carolyn Smart. Tonight, we will hear five of the work&rsquo;s eight movements. 
						</p>
						<p>
							<strong>Te Deum<br>Arvo P&auml;rt</strong> 
						</p>
						<p>
							The <em>Te Deum</em> text&mdash;a hymn of praise to God&mdash;dates back as far as the sixth century. No definitive source has yet been determined. The most likely authors are a pair of saints, St. Ambrose and St. Augustine, as well as the Yugoslavian cleric Niceta of Remesiana. It has been sung in a variety of settings: as part of the Matins ceremony, as a processional chant, at the conclusion of liturgical dramas, and as either a song of thanksgiving or a hymn of victory on the battlefield. Musical settings by particular composers date back to Palestrina and include versions by Taverner, Lassus, Lully, Purcell, Handel, Haydn, Berlioz, Bruckner, Verdi, Dvo&#x159;&aacute;k, Kod&aacute;ly and Walton. 
						</p>
						<p>
							P&auml;rt composed the <em>Te Deum</em> between 1984 and 1985. The premiere took place in Cologne, Germany, on 19 January 1985, performed by the Cologne Radio Symphony Orchestra and the Cologne Radio Chorus, Dennis Russell Davies, conducting. P&auml;rt revised the composition in 1992. 
						</p>
						<p>
							This is exquisitely beautiful music, time&shy;less in its ardent spirituality. Mirroring the text, volume levels range from whispered pianissimos to exultant fortissimos. 
						</p>
						<p>
							The composer rarely writes or says much about his music, preferring to let it speak for itself. He did provide the following note on the <em>Te Deum</em>: 
						</p>
						<p>
							&ldquo;The liturgical text of the <em>Te Deum</em> consists of immutable truths. I am reminded of the sense of immeasurable serenity imparted by a mountain panorama. Swiss artist Martin Ruf once told me that he can distinguish over twenty shades of blue in the mountains when the air is clear. His words immediately turned into sound; I began to &lsquo;hear&rsquo; those &lsquo;blue&rsquo; mountains. 
						</p>
						<p>
							&ldquo;I wished only to convey a mood, a mood that could be infinite in time, by delicately removing one piece&mdash;one particle of time&mdash;out of the flow of infinity. I had to draw this music gently out of silence and emptiness. 
						</p>
						<p>
							&ldquo;The work <em>Te Deum</em> was a quest for something evanescent, something long lost or not yet found, the quest for something believed to be non-existent, but so real that it exists not only within us but beyond our being as well.&rdquo; 
						</p>
						<p>
							Author Paul Hillier has written the following commentary: 
						</p>
						<p>
							&ldquo;The work is scored for three choirs, a string orchestra, prepared piano (which resonates rather like a cross between a harpsichord and an amplified clavichord), and the wind harp. Choir 1 (female voices) and choir 2 (male voices) sing the more overtly chant-like material, which is rhythmically free and melismatic; while choir 3 (mixed voices) sings a more syllabic, rhythmically defined setting of the text in four-part harmony. 
						</p>
						<p>
							&ldquo;The work falls into three main sections, each separated by a short pause. These are further divided into six, six and five sections respectively; thus there are 17 subsections in all, of irregular length, across which are stretched the text&rsquo;s 29 verses plus a concluding <em>Amen</em> and <em>Sanctus</em>. Within the three main sections, although each subsection is treated in varying degrees as a self-contained entity, the music itself is continuous&thinsp;&hellip;&rdquo; 
						</p>
						<p>
							<strong>Sunrise Mass<br>Ola Gjeilo</strong> 
						</p>
						<p>
							&ldquo;There isn&rsquo;t anything wrong with dissonance, as conflict and discord are natural parts of life and necessary for all positive development and maturation,&rdquo; Gjeilo has written. &ldquo;And in most areas of society, conflict is something we very much want to resolve. But in a great deal of avant-garde art, the goal seems to stay in the conflict itself, which to me becomes just a way of inflicting the listener with his own neurosis. Dissonance and high chromaticism are important to explore; the Modernists were brave to delve into parts of the human psyche that are dark and edgy, but I do think they got somewhat stuck in that. A lot of art pushed audiences away for some time. I think people naturally and instinctively want to experience transcendence, resolution and the feeling of redemption, joy and peace that the resolving of discord can yield.&rdquo; 
						</p>
						<p>
							The gorgeous and heartening <em>Sunrise Mass</em> was commissioned in 2007 by conductor Tore Erik Mohn for performance by two combined Norwegian choruses: Majorstua Kammerkor and Kammerkoret Nova. It was premiered on 2 November, 2008 in Oslo. Since then, it has been presented in concerts around the world, to great acclaim. 
						</p>
						<p>
							The following note appears on the composer&rsquo;s website: 
						</p>
						<p>
							&ldquo;The reason I used English titles, seemingly unrelated to the (mostly) Latin texts, for the movements in this setting of the Mass has mainly to do with the initial idea behind <em>Sunrise Mass</em>. I wanted the musical development of the work to evolve from the most transparent and spacey, to something completely earthy and grounded; from nebulous and pristine to more emotional and dramatic; and eventually warm and solid&mdash;as a metaphor for human development from child to adult, or as a spiritual journey. Most of my favourite composers are film composers working in America today, and this piece is partly influenced by movies and film scores from the past few years that I love dearly.&rdquo; 
						</p>
						<p>
							According to the composer, the first movement, <em>Kyrie</em> (The Spheres), evokes an atmosphere that sounds like &ldquo;floating in space, in deep silence, between stars and planets.&rdquo; <em>Gloria</em> (Sunrise), the second movement, at first maintains the restrained, ethereal mood. But once the string orchestra awakens to life with a forward-pressing rhythm, the music rapidly blossoms into joyousness. A return of the movement&rsquo;s opening mood of restraint closes this section. 
						</p>
						<p>
							The third movement, <em>Credo</em> (The City), opens and closes with the Mass&rsquo;s most concentrated periods of animation and volume. They bookend a thoughtful central episode. The final movement, <em>Sanctus</em> and <em>Agnus Dei</em> (Identity and The Ground) begins slowly and sweetly, then grows briefly in expressive intensity before reaching its final, contented destination. &ldquo;&hellip;&thinsp;I wanted to convey a sense of having &lsquo;arrived&rsquo; at the end of the Mass,&rdquo; the composer wrote, &ldquo;to have reached a kind of peace and grounded strength, after the long journey of the Mass, having gone through so many different emotional landscapes.&rdquo; 
						</p>
						<p>
							Author Kira Zeeman Rugen writes, &ldquo;In the <em>Sanctus</em>, Gjeilo brings the exact material from the <em>Kyrie</em> forward. The only differences between the respective sections within <em>Kyrie</em> and the <em>Sanctus</em> are the text and the use of a delicate, warm-coloured violin solo above the choral line. According to Gjeilo, the solo violin symbolizes the individual and the emergence of a conscious &lsquo;self;&rsquo; thus this movement is called Identity&thinsp;&hellip;&thinsp;The pathos that Identity creates is yearning, searching and acutely pensive. The Mass begins in the stars during the <em>Kyrie</em>, and then in the <em>Sanctus</em>, it circles back to the same material to symbolize the individual. It is as if it looks toward the stars, then mirrors what it sees, and only now is self-aware.&rdquo; 
						</p>
