						<p>
							<strong> Kiwetin-acahkos&mdash;North Star<br>Andrew Balfour </strong> <br>Fanfare for the Peoples of the North<br>A Canada 150 Signature Project 
						</p>
						<p>
							<em> The composer has provided the following note: </em> 
						</p>
						<p>
							<em> Kiwetin-acahkos </em> (pronounced &lsquo;Key way tin a sah quos&rsquo;) is an orchestral work that celebrates our northern country&mdash;its people, diversity, and the landscape that so defines our environment. Its title comes from a Cree word that means &lsquo;North Star,&rsquo; and evokes many other meanings. 
						</p>
						<p>
							<em> Kiwetin-acahkos </em> was commissioned by the Toronto Symphony Orchestra at the recommendation of the MCO, and funded by the Government of Canada in recognition of Canada&rsquo;s 150th anniversary. This performance is the piece&rsquo;s world premiere. The Toronto Symphony Orchestra will perform it again in June 2017. 
						</p>
						<p>
							<strong> Intermezzo from Goyescas<br>Enrique Granados </strong> 
						</p>
						<p>
							The music of Granados is strongly coloured by the folk traditions of his native Spain. His piano works and songs brim over with alluring Spanish-style rhythms and melodies. Like his contemporary, Dvo&#x159;&aacute;k, he rarely quoted authentic folk tunes, preferring to compose original themes in folk style. 
						</p>
						<p>
							In 1911, he composed his most famous piano piece, the suite called <em> Goyescas </em> . It was inspired by the vivid paintings in which the celebrated Spanish artist, Francisco Goya (1746-1818), had depicted the society and landscapes of his homeland. 
						</p>
						<p>
							American pianist Ernest Schelling, the first person to perform <em> Goyescas </em> in America, suggested that Granados use it as the basis for an opera. The unusual creative sequence was as follows: Granados and author Fernando Periquet created the dramatic form, Granados composed the music (founded upon, and expanded from, the piano suite), and Periquet fitted a final libretto to it. 
						</p>
						<p>
							The one-act opera runs approximately one hour. The plot is a typical operatic story of love, jealousy and revenge involving a group of young Spaniards. 
						</p>
						<p>
							The Paris Op&eacute;ra intended to stage the premiere production, but the outbreak of the First World War made that impossible. Ernest Schelling arranged for the debut to take place at the Metropolitan Opera in New York. Granados travelled from Spain to supervise the rehearsals. The highly successful premiere took place on 28 January 1916, on a double bill with Leoncavallo&rsquo;s Pagliacci. The first Spanish-language opera to be performed by that company, it received five performances that season&mdash;but not a single one since. 
						</p>
						<p>
							When the orchestral Intermezzo between the first two of the opera&rsquo;s three scenes proved too brief to accommodate the change of scenery, Granados composed, overnight, the second, more expansive version of this lushly romantic music that you will hear at this concert, in a transcription for solo cello and string orchestra. 
						</p>
						<p>
							The opera played a role in Granados&rsquo;s untimely death. American President Woodrow Wilson&rsquo;s invitation to perform a piano recital at the White House caused Granados to postpone his return to Spain. The ship that was bearing him and his wife home sank after being struck by a German torpedo in the English Channel. 
						</p>
						<p>
							<strong> Symphony No. 27 in G Major, K. 199 (K&thinsp;162a &amp; 161b)<br>Wolfgang Amadeus Mozart </strong> 
						</p>
						<p>
							This charming work dates from the period during which Mozart composed more symphonies than at any other time in his career. Between 1770 and 1773, he composed no fewer than 28 of them. None were published during his lifetime, despite their quality. This may have been the result of advice that his father Leopold gave him in 1778: &ldquo;I have not given any of your symphonies to be copied,&rdquo; he wrote, &ldquo;because I knew in advance that when you were older and had more insight, you would be glad that no one had got hold of them, though at the time you composed them you were quite pleased with them. One gradually becomes more and more fastidious.&rdquo; An unnecessarily harsh judgment of the sort of piece you will hear tonight! 
						</p>
						<p>
							In July 1773, Wolfgang and Leopold travelled to Vienna from their home in Salzburg. Their goal was to secure a position for Wolfgang, the seventeen-year-old genius, at the court of Maria Theresa, the ruler of the Austro-Hungarian Empire. Despite an audience with the monarch, no such position materialized. The Mozarts returned to Salzburg in September. Mozart would work there in unhappy and under-appreciated circumstances for another eight years before abandoning Salzburg for the life of a freelance musician in Vienna. 
						</p>
						<p>
							The 1773 visit to Vienna did have its positive side. Mozart had the opportunity to examine the latest compositions of the most celebrated composer of the day, Joseph Haydn, who later became a close and mutually inspiring friend. Mozart was also active as a composer, creating six string quartets, and incidental music for a play: Thamos, King of Egypt. 
						</p>
						<p>
							He composed several symphonies during this period, both before and after the trip to Vienna. The numbers that were attached to them during the late nineteenth century do not accurately reflect the order of composition. He wrote the well-known No. 25 in G Minor, for example, after the journey. Its highly dramatic nature reflected the exposure to the music of Haydn and other progressive composers that he had encountered in Vienna. 
						</p>
						<p>
							On the other hand, he created No. 27 in Salzburg shortly before the trip to Vienna. In it, he continued to follow the pattern of the three-movement Italian-style operatic overture which had served as his primary symphonic model up to that time. 
						</p>
						<p>
							The first movement is based on a cheerful first theme and a gracious second. The second movement (in which some writers hear pre-echoes of Schubert) includes several individual features. The upper strings use mutes, and the lower ones play mostly pizzicato, while Mozart gave the flutes a more prominent role than in the first movement. Mozart scholar Neal Zaslaw calls the main melody &ldquo;the kind of air sung beneath the balconies of young Mediterranean women in many an eighteenth-century opera.&rdquo; The high-spirited final might almost be a waltz if the tempo weren&rsquo;t so fast. Mozart bends the knee to earlier music by including passages in fugal style. 
						</p>
						<p>
							<strong> Earthshine<br>Jeffrey Ryan </strong> 
						</p>
						<p>
							<em> The composer has provided the following note: </em> 
						</p>
						<p>
							Earthshine is sunlight that is reflected off the Earth onto the dark part of the Moon as seen in our night sky. Particularly when the Moon is in a crescent phase, it is possible to see both the crescent brightly illuminated by the Sun, and the rest of the Moon faintly illuminated by the Earth. In this way, earthshine is an impact that our Earth has on something beyond the Earth, while metaphorically, it is a reflection of ourselves that brings light into darkness. <em> Earthshine </em> is a single-movement work for string orchestra exploring light and shadow and the space in between. 
						</p>
						<p>
							<strong> Concierto in modo galante<br>Joaquin Rodrigo </strong> 
						</p>
						<p>
							Known above all for the enchanting <em> Concierto de Aranjuez </em> for guitar and orchestra (perhaps the most-performed concerto of the twentieth century), Rodrigo also composed a large catalogue of other music. It includes additional concertos (for violin, piano, flute, cello, harp, two and four guitars), miscellaneous orchestral pieces, numerous solo guitar works, a ballet, a zarzuela (operetta), and many songs&mdash;nearly 200 works in all. 
						</p>
						<p>
							Blind from the age of three, he began his musical education in Valencia, then moved to Paris for composition lessons with Paul Dukas. He returned to work in Madrid after the Spanish Civil War. While his reputation as a composer grew steadily, he was also active as an academic, a music critic and radio producer. Later, he toured the world, teaching, lecturing, and attending festivals devoted to his music. He received many honours during his nearly 98 years, including major awards in France, the United States, Spain and Belgium. 
						</p>
						<p>
							This is how the <em> New Grove Dictionary of Music and Musicians </em> summed up his career: &ldquo;Rodrigo&rsquo;s music was fundamentally conservative, &lsquo;neocasticista&rsquo; or &lsquo;faithful to tradition&rsquo; to use the composer&rsquo;s own words. His first works revealed the influence of composers such as Granados, Ravel and Stravinsky, but his individual musical voice was soon heard in the songs, piano works and orchestral pieces composed during the 1920s and &lsquo;30s. As he matured, his wide knowledge of and sympathy for the music and culture of earlier times bore fruit. His forms were traditional, but appropriate for his purposes, and his musical language, drawn from Classical and nationalist sources, underpinned a melodic gift of remarkable eloquence. He made many of the finest settings of classical Spanish poetry, his guitar pieces are in the central repertory, and his concertos are the most significant such works composed in Spain.&rdquo; 
						</p>
						<p>
							The work you will hear this evening is his first of two pieces for cello and orchestra. The second work, <em> Concierto como un Divertimento </em> (Concerto in the Style of a Divertimento) followed in 1982. The <em> Concierto in modo galante </em> was commissioned in 1949 by the eminent Spanish cellist, Gaspar Cassad&oacute;. &ldquo;I would play it everywhere,&rdquo; he promised Rodrigo. &ldquo;I would record it, and I would see that it was published in Paris.&rdquo; 
						</p>
						<p>
							Strongly impressed with the thematic material Rodrigo came up with, Cassad&oacute; offered to write down the entire score himself, something required for every one of the blind composer&rsquo;s works. The two artists spent much of the summer of 1949 together, often working far into the night, sometimes by candlelight due to post-war power restrictions. Cassad&oacute; praised the completed concerto as &ldquo;an excellent work and a significant addition to the cello/orchestra repertoire&thinsp;&hellip;&thinsp;its instrumentation is so economical as to make this one of the few scores in which the solo cello is never drowned out by the weight of the orchestra.&rdquo; 
						</p>
						<p>
							The premiere took place in Madrid&rsquo;s Palacio de la M&uacute;sica on 4 November 1949, with Cassad&oacute; as the soloist and Ata&uacute;lfo Argenta conducting the Orquesta Nacional de Espa&ntilde;a. That performance and another that took place in Rome a year later drew strong audience approval. Feeling that the piece was too long, Cassad&oacute; made cuts without consulting Rodrigo. They were restored before the concerto was published. 
						</p>
						<p>
							In terms of style, the concerto pays homage to the eighteenth-century music of composers such as Luigi Boccherini (himself a professional cellist). Rodrigo wrote, &ldquo;The work consists of three movements; Allegro grazioso, Adagietto, and Rondo giocoso. The treatment of the theme is strictly an invention of the composer and makes constant allusions to the boleros, panaderos and zapateados, Spanish dances much in vogue in the middle of the past century. Thus the origin of its title, galante. Nevertheless the work is of our time with its modern harmonic language, up-to-date violoncellistic writing and dynamically sparkling orchestration.&rdquo; 
						</p>
						<p>
							The first movement struts along in cheeky fashion, the soloist supported wittily by the orchestra. A lyrical theme introduced by the cello provides appealing contrast. A buildup in volume and energy paves the way for a solo cadenza, further development of the themes, and a wistful coda. 
						</p>
						<p>
							The slow, dreamy second movement is based on an appealingly melancholy theme. Dance-like animation builds in the central section, only to cede to the return of the lovely opening subject. The recurring theme of the third movement offers a most attractive blend of elegance and humour. Rodrigo&rsquo;s virtuosic demands upon the soloist reach their summit here as well. 
						</p>
