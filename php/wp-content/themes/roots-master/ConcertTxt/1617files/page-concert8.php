						<p>
							<b> Asier Polo </b> 
						</p>
						<p>
							Asier Polo is considered by the music press to be one of the most important Spanish cellists of his generation. He has worked with many major international orchestras such as the Philharmonia Orchestra, the Orchestra Sinfonica Nazionalle della RAI, Dresdner Philharmonie, Orchestre de Paris, BBC Philharmonic, Bergen Filharmoniske Orkester, Israel Philharmonic Orchestra, Berliner Symphoniker, Orquesta Nacional de M&eacute;xico, Orquesta Sinf&oacute;nica de S&atilde;o Paulo, Louisiana Philharmonic, Orquesta Nacional de Espa&ntilde;a and the Basel Symphony Orchestra, and under the baton of distinguished conductors such as Pinchas Steinberg, Christian Badea, Rafael Fr&uuml;hbeck de Burgos, Claus Peter Flor, Carlos Miguel Prieto, G&uuml;nther Herbig, Juanjo Mena, Antoni Wit and Anne Manson. 
						</p>
						<p>
							He is a regular guest at important festivals such as those held in Schleswig-Holstein, Nantes, Ohrid, Biennale di Venezia, Rome, Lisbon, Morelia, Granada and the Quincena Musical in San Sebasti&aacute;n. 
						</p>
						<p>
							Asier Polo has appeared with great artists such as Silvia Marcovici, Nicol&aacute;s Chumachenco, Sol Ga&shy;betta, Isabelle van Keulen, Josep Colom, Eldar Nebolsin, Gerard Causs&eacute;, Cuarteto Jan&aacute;&#x10D;ek, Cuarte&shy;to Casals and Alfredo Kraus&mdash;who, in his last years, invited him to perform as soloist at his concerts, with engagements at the Maggio Fiorentino, Covent Garden in London, the Tonhalle in Zurich and the highly esteemed Musikverein in Vienna, as well as in a tour of the major concerts halls in Japan. 
						</p>
						<p>
							<b> Jeffrey Ryan </b> 
						</p>
						<p>
							Praised for his &ldquo;strong personal voice&rdquo; ( <em> Globe and Mail </em> ), &ldquo;masterful command of instrumental colour&rdquo; ( <em> Georgia Straight </em> ), and &ldquo;superb attention to rhythm&rdquo; ( <em> Audio Ideas Guide </em> ), Jeffrey Ryan writes music that runs the gamut from opera, art song, and choral music to chamber and orchestral works. 
						</p>
						<p>
							Recipient of SOCAN&rsquo;s 2014 Jan V. Matejcek New Classical Music Award for career achievement, Ryan&rsquo;s music has engaged audiences in concerts and broadcasts worldwide. His many commissions include works for the Cleveland Orchestra, Vancouver Symphony, Toronto Symphony, Montreal Symphony, Victoria Symphony, Manitoba Chamber Orchestra, Tokyo String Quartet, Arditti Quartet, Tafelmusik Chamber Choir, and Tapestry Opera. Major recent projects include <em> Seasons of the Sea </em> with First Nations storyteller Rosemary Georgeson for Vetta Chamber Music; Midnight Sun Songs for the Northern Arts and Cultural Centre (Yellowknife); and <em> Book of Love </em> for Kokoro Dance and Standing Wave. 
						</p>
						<p>
							Recordings of Ryan&rsquo;s music have garnered four JUNO nominations and five more from the Western Canadian Music Awards. His portrait CD <em> Fugitive Colours </em> , performed by the Vancouver Symphony with the Gryphon Trio, launched the Naxos Canadian Classics series and won the 2012 Western Canadian Music Award for Classical Recording of the Year. Ryan&rsquo;s growing discography includes recordings by musica intima, the Canadian Chamber Choir, clarinetist Cris Inguanti, pianists Rachel Kiyo Iwaasa and Yoko Hirota, the Thunder Bay Symphony, the Penderecki String Quartet, and Standing Wave. 
						</p>
						<p>
							Based in Vancouver, Ryan is Composer Advisor for Music Toronto, and was the Vancouver Symphony&rsquo;s Composer Laureate for 2008/09, after serving as the VSO&rsquo;s Composer-in-Residence from 2002 to 2007. He was an Affiliate Composer with the Toronto Symphony from 2000 to 2002. He holds degrees from The Cleveland Institute of Music (where he studied with the distinguished composer Donald Erb), Wilfrid Laurier University, and the University of Toronto. 
						</p>
						<p>
							<b> Andrew Balfour </b> 
						</p>
						<p>
							Of Cree descent, Andrew Balfour has written more than 30 choral, instrumental and orchestral works, including <em> Take the Indian </em> , <em> Empire &Eacute;trange: The Death of Louis Riel </em> , <em> Migiis: A Whiteshell Soundscape </em> , <em> Bawajigaywin </em> , <em> Gregorio&rsquo;s Nightmare </em> , <em> Wa Wa Tey Wak </em> (Northern Lights), <em> Fantasia on a Poem by Rumi </em> , <em> Missa Brevis </em> and <em> Medieval Inuit </em> . He has been commissioned by the Winnipeg, Regina and Toronto Symphony Orchestras, Ensemble Caprice, the Winnipeg Singers, the Kingston Chamber Choir and Camerata Nova, among others. His works have been performed and/or broadcast locally, nationally and internationally. 
						</p>
						<p>
							Andrew is also the founder and Artistic Director of the innovative, Winnipeg-based Camerata Nova. Founded in 1996, this 14-member vocal group presents an annual concert series as well as special performances. With Camerata Nova, Andrew specializes in creating &ldquo;concept concerts&rdquo; that explore a theme through an eclectic array of music, including new works, arrangements and inter-genre and interdisciplinary collaborations. 
						</p>
						<p>
							Andrew is passionate about music education and outreach, particularly in schools located in low-income areas of Winnipeg and northern communities. Since 2008 he has worked on behalf of organizations such as the National Arts Centre, Camerata Nova, the Winnipeg Symphony Orchestra and various Manitoba school divisions, offering young students empowering sessions in the joy and freedom of self-expression through music. 
						</p>
						<p>
							Andrew was Curator and Composer-in-Residence of the WSO&rsquo;s Indigenous Festivals in 2009 and 2010, and in 2007 received the Mayor of Winnipeg&rsquo;s Making a Mark Award, sponsored by the Winnipeg Arts Council to recognize the most promising mid-career artist in the city. 
						</p>
