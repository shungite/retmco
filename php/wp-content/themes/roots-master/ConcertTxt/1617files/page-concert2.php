						<p>
							<b>Kaluza Klein (2016)<br>Michael Oesterle</b> 
						</p>
						<p>
							Kaluza Klein <i>was commissioned by the MCO for Evelyn Glennie, with the financial support of the Canada Council for the Arts. She, Anne Manson and the MCO performed the premiere on 10 April 2012. The composer has provided the following note:</i> 
						</p>
						<p>
							In 1921, mathematicians Theodor Kaluza and Oskar Klein proposed the theory of a fifth dimension. Their collaboration produced a credible geometry: an elegant solution to this theory which was soon after referenced by other scientists, including Albert Einstein. The general consensus today is that this idea does not fit within the now widely accepted model of supersymmetry, but the possibilities opened up by a fifth dimension continue to lure physicists and mathematicians. Incidentally, the Kaluza-Klein theory of a compact curled dimension became central to the emergence of String Theory, an idea which is still to be proven. In any case, the Kaluza-Klein theory remains a byword for elegance in physics and math. 
						</p>
						<p>
							Although I didn&rsquo;t intend for my piece to have an audible reference to this mathematical theory, my idea was to write music that is about an analogous partnership between the vibraphone and strings; a collaboration in search of an harmonic identity for the piece. The violins introduce the initial pitch, the vibraphone proposes alternative ideas&mdash;both continuing to search for the &lsquo;right&rsquo; notes&mdash;leading to the final 25 measures of the piece during which a series of chords appear as an elegant solution. 
						</p>
						<p>
							<b>Chamber Symphony in C Minor, Op. 110a<br>Dmitri Shostakovich</b><br>&mdash;transcribed by Rudolf Barshai for string orchestra from String Quartet No. 8 
						</p>
						<p>
							Shostakovich&rsquo;s 15 string quartets are every bit as vital to an appreciation of his music as the 15 symphonies. He poured into them many of his most intimate thoughts, especially during those periods when it was dangerous, if not lethal, for a Soviet artist to make public display of personal emotions. 
						</p>
						<p>
							The powerful, expressive directness of No. 8 has made it the most frequently performed of his quartets. He composed it between 12 and 14 July 1960, and it was premiered by the Beethoven Quartet in Lenin&shy;grad on October 2. He wrote it during a visit to Dresden and publicly dedicated it &ldquo;[t]o the memory of the victims of fascism and war.&rdquo; This provided Soviet authorities with a politically correct inspiration, but he gave it a personal spin by his, at least inwardly, including himself among the victims. In a letter to a friend, Isaak Glikman, he revealed additional intentions: &ldquo;I was thinking about the fact that if I die sometime or other, it&rsquo;s pretty unlikely that someone will write a work in my memory. So I decided to write such a piece myself. The basic theme of the Quartet is DSCH (the German names for the notes d, e-flat, c, b) i.e. my initials. It also makes use of themes from my works and the revolutionary song <i>Tormented by Grievous Bondage</i>.&rdquo; 
						</p>
						<p>
							Another friend, Lev Lebedinsky, wrote that Shostakovich was contemplating suicide at that time, due to his recently giving in to long-standing pressure to join the Communist party. Lebedinsky stated that Shosta&shy;kovich considered the Quartet not only an autobiography but also his last will and testament. Lebedinsky convinced him not to take his own life. Fifteen years later, the Quartet was performed at Shostakovich&rsquo;s funeral. 
						</p>
						<p>
							Several transcriptions of Quartet No. 8 have appeared. Shostakovich gave his blessing to this highly effective version for string orchestra by Rudolf Barshai, who prepared it and renamed it <i>Chamber Symphony</i> shortly after the premiere of the original quartet version. He later created transcriptions of several additional Shostakovich quartets, and developed an intimate knowledge of Shostakovich&rsquo;s music, first as viola player in the Borodin Quartet, then as conductor. In 1969, he conducted the first performance of Symphony No. 14, which Shostakovich dedicated to Benjamin Britten. 
						</p>
						<p>
							The five movements of the Chamber Symphony are performed without breaks between them. The first is drenched in slow, desolate anguish. Next comes a <i>tour-de-force</i> of stark, driving power, then a bitingly satiric scherzo/waltz. The fourth movement, quoting the revolutionary song, alternates sharp dramatic outbursts with troubled meditations. The fugal finale exceeds the first movement in the depths of its ashen, dehumanized desolation. 
						</p>
						<p>
							<b>Praeludium &amp; Allegro in the Style of Pugnani<br>Fritz Kreisler</b> 
						</p>
						<p>
							As a violinist, the Vienna-born Kreisler was a familiar and beloved figure on the world&rsquo;s concert stages for half a century. With seemingly effortless grace and beauty, he illuminated all the great Classical and Romantic literature for the instrument. He appeared with all the major orchestras and conductors of his day, commissioned and premiered important new works (including the expansive Concerto by Sir Edward Elgar), and left a legacy of recordings that are still admired by music-lovers everywhere. 
						</p>
						<p>
							Kreisler also composed music, in forms large and small. His more substantial works included an operetta and a string quartet, but the pieces that keep his name alive on concert programs are the dozens of brief encore works. Many of them, such as <i>Liebesleid</i> (Love&rsquo;s Sorrow) and <i>Sch&ouml;n Rosmarin</i> (Beautiful Rosemary), paint sentimental portraits of the warmhearted musical style that was popular in central Europe during his youth. 
						</p>
						<p>
							At first he claimed that some of these miniatures were composed by violin masters of the Baroque era, which he had discovered in out-of-the-way libraries. He eventually confessed that he had written them himself. Some authorities were outraged, possibly from embarrassment at being &lsquo;taken in&rsquo; by Kreisler&rsquo;s expert powers of imitation! Audiences were quick to forgive him, and they have happily been enjoying these delightful bonbons ever since. 
						</p>
						<p>
							Kreisler published the appealing piece you will hear tonight in its original form for violin and piano in 1910. As its title indicates, it is an original work, written in tribute to the celebrated Italian violinist Gaetano Pugnani (1731-1798). 
						</p>
						<p>
							<b>Sonata in D Minor, Op. 5 No. 12&mdash;&lsquo;La folia&rsquo;<br>Arcangelo Corelli</b><br>&mdash;arr. for marimba, vibraphone &amp; strings by Karl Jenkins 
						</p>
						<p>
							Corelli&rsquo;s reputation and influence extended through much of Europe. He was one of the leading violin soloists of the Baroque era, as well as a composer of music that is both appealing and historically significant. For example, his sonatas for violin, helped establish this instrument as the most important non-vocal element in music. 
						</p>
						<p>
							<i>La folia</i> (the folly) is a type of wild Spanish or Portuguese folk dance. One particular melody used for it attained wide popular currency, beginning in the 16th century and extending through the 18th. Numerous composers have used it as the theme upon which to base sets of variations, including Vivaldi, Frescobaldi, Lully, Pergolesi, Geminiani, Bach, Cherubini, Liszt, Nielsen, Rachmaninoff, and Henze. The best-known is Corelli&rsquo;s, a dazzling single movement which makes up the last of the 12 sonatas for violin and continuo that he published in 1700 as Op. 5. 
						</p>
						<p>
							<b>Piccolo Concerto in C Major, RV 443<br>Antonio Vivaldi</b><br>&mdash;arr. for vibraphone by Evelyn Glennie 
						</p>
						<p>
							In Vivaldi&rsquo;s time, it was common practice for a composition to be played on several different instruments. Composers were happy just to have their music heard, and they weren&rsquo;t fussy about whether it was performed on a violin, flute or harpsichord. Given this attitude, as well as Vivaldi&rsquo;s well-known love of instrumental colour, it&rsquo;s quite likely that he would have welcomed the chance to hear one of his works sounding in an attractive (and what would have been new to him) medium such as the vibraphone. 
						</p>
						<p>
							He intended the delightful concerto that Dame Evelyn Glennie will perform at this concert for the smallest member of either the flute family, the piccolo, or the recorder family, the sopranino model. It&rsquo;s one of just three concertos that he composed for this instrument, perhaps under the inspiration of a virtuoso soloist. The lively outer movements are filled with high, bird-like trills that suit either piccolo or vibraphone admirably. In between comes a stately, almost melancholy slow movement which displays the solo instrument&rsquo;s lyrical side. (This piece was also performed by recorder player Lucie Horsch in the 2015/16 MCO season.) 
						</p>
