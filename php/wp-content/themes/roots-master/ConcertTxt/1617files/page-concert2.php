			<p>
				Audiences also tend to marvel that Glennie, the world&rsquo;s premier solo percussionist, is profoundly deaf, although she has a decidedly different attitude about the matter: &ldquo;For me, my deafness is no more important than the fact I am female with brown eyes,&rdquo; she says. 
