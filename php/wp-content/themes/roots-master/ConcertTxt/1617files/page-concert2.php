						<p>
							<b>Evelyn Glennie</b> 
						</p>
						<p>
							Evelyn Glennie is the first person in history to successfully create and sustain a full-time career as a solo percussionist. She performs worldwide with the greatest conductors, orchestras, and artists. She fondly recalls having played the first percussion concerto in the history of The Proms at the Albert Hall in 1992, which paved the way for orchestras around the world to feature percussion concerti. She had the honour of a leading role in the Opening Ceremony of the London 2012 Olympic Games. 
						</p>
						<p>
							Throughout her career, Evelyn has had the opportunity to collaborate with a diverse range of artists. &ldquo;Working with Bj&ouml;rk allowed me to break away from the written page and fall into a completely different arena, audience-wise,&rdquo; she says. &ldquo;Giving totally improvised performances with Fred Frith is always an exhilarating experience. We are asking the audience to listen in a completely different way.&rdquo; 
						</p>
						<p>
							Evelyn&rsquo;s solo recordings, which now exceed 30 CDs, are as diverse as her career onstage. <i>Shadow Behind the Iron Sun</i> and <i>Sound Spirits</i> continue to be bestselling albums that amply demonstrate her brilliant improvisational skills. Of <i>Shadow</i> Evelyn remembers: &ldquo;the freedom I had in choosing whichever instruments I wanted&mdash;playing what I wanted and how I wanted&mdash;was the most liberating experience I have ever had in a studio.&rdquo; 
						</p>
						<p>
							A leading commissioner of new works for solo percussion, Evelyn has more than 200 pieces to her name from many of the world&rsquo;s most eminent composers. She believes this has been crucial to her success as a solo percussionist: &ldquo;It&rsquo;s important that I continue to commission and collaborate with a diverse range of composers whilst recognising the young talent coming through.&rdquo; A triple GRAMMY award winner and BAFTA nominee, Evelyn is in demand as a composer in her own right and records high quality music for film, television and music library companies. The film <i>Touch the Sound</i> and her enlightening TED Talk remain key testimonies to her approach to sound-creation. 
						</p>
						<p>
							With over 80 international awards to date, including the Polar Music Prize, Evelyn continues to inspire and motivate people from all walks of life. Her masterclasses and consultations are designed to guide the next generation. &ldquo;Listening is the backbone to every aspect of our lives,&rdquo; she says. 
						</p>
						<p>
							Evelyn has a vision: to teach the world to listen. &ldquo;Life is full of challenges, but we can always find alternative ways of approaching our difficulties, which will often lead to new discoveries. My career and my life have been about listening in the deepest possible sense. Losing my hearing meant learning how to listen differently, to discover features of sound I hadn&rsquo;t realized existed. Losing my hearing made me a better listener.&rdquo; 
						</p>
						<p>
							Awarded an OBE in 1993 and Dame Commander of the British Empire in 2007, Evelyn lives in the beautiful countryside of Cambridgeshire in the east of England. 
						</p>
						<p>
							<b>Michael Oesterle</b> 
						</p>
						<p>
							Michael Oesterle is a Canadian composer living in Deux Montagnes, Quebec. Over the past ten years, he has written several works for the Manitoba Chamber Orchestra, including a cello concerto (<i>Ironman</i>, 2006), a violin concerto (<i>Unreasonable World</i>, 2008) and a concerto for vibraphone and strings (<i>Kaluza Klein</i>, 2011). He has also made a chamber orchestra version of Grieg&rsquo;s Piano Concerto in A Minor, Op. 16, and arranged Leo&scaron; Jan&aacute;ƒçek&rsquo;s two string quartets, all for the MCO. Currently he is composing the music for the MCO/Th&eacute;&acirc;tre Cercle Moli&egrave;re production of Rh&eacute;al Cenerini&rsquo;s play <i>Nanabozho et le tambour/Nanabush and the Drum</i>. 
						</p>
