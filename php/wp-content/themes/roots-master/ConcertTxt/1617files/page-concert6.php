						<p>
							<b> Violin Concerto No. 5 in A Major, K. 219&mdash;Turkish<br>Wolfgang Amadeus Mozart<br>&mdash;performed on the double bass </b> 
						</p>
						<p>
							Mozart, the greatest of all child prodigies in music, received from his father Leopold a thorough education in composition and performance. The latter included instruction in piano, harpsichord and violin. As an adult, Wolfgang concentrated his performing skills on the piano. In his early years, he appeared most often as a soloist on the violin, beginning with the concert tours his family made during the 1760s. His father once wrote to him that &ldquo;[y]ou yourself do not know how well you play the violin&thinsp;&hellip;&thinsp;when you play with energy and with your whole heart and soul, yes indeed, it&rsquo;s just as though you were the finest violinist in all of Europe.&rdquo; This was no small compliment, coming from a renowned authority on the instrument. He published his widely used violin method in the year of Wolfgang&rsquo;s birth. 
						</p>
						<p>
							Between June and December 1775, Wolfgang composed all but the first of the five violin concertos that can be unquestionably attributed to him. He completed No. 5 on 20 December 1775. It is not only the most accomplished of the series but also the most unusual. The first movement has the tempo marking Allegro aperto, for example, meaning quick and open. He appears to be the only composer to use this adjective. He applied it to just eight other works, the vocal numbers of which are either, in the words of author Jean-Pierre Marty, &ldquo;hymns to hope, to joy, to love, and to nature and to happiness,&rdquo; or liturgical songs of praise. This concerto movement evokes a similar mood. 
						</p>
						<p>
							The soloist&rsquo;s first entry is another example of the concerto&rsquo;s uniqueness. It is remarkable for being quite different in tempo and mood&mdash;quiet and dreamy&mdash;from the preceding, almost martial orchestral introduction. It&rsquo;s as if the violinist were saying to the orchestra, &ldquo;catch your breath while I introduce myself.&rdquo; And once the quicker tempo resumes, the soloist introduces a completely new theme. 
						</p>
						<p>
							The second movement is a true Adagio, slow and heartfelt, in contrast to the easy, flowing Andante that was typical of the era. Its lyrical intensity borders on the operatic. The finale, a rondo in the style of a minuet, is the source of the concerto&rsquo;s nickname. It begins in appropriately courtly fashion. In the delightfully startling minor key episode mid-way through, Mozart instructs the cellos and basses to strike their strings with the wood of the bow and asks the soloist for virtuoso pyrotechnics. These practices recall the Turkish military music that was all the rage in Austria at the time&mdash;but they evoke the folk music of Hungary to an equal degree. At that time, the word &lsquo;Turkish&rsquo; was a catch-all term that might be applied to any music that evoked the colourful, exotic folk tunes of people living east of Austria. 
						</p>
						<p>
							<b> Two Sketches on French-Canadian Airs<br>Sir Ernest MacMillan </b> 
						</p>
						<p>
							Sir Ernest MacMillan earned the status of Canada&rsquo;s elder musical statesman through his tireless and widespread activities as conductor (he was music director of the Toronto Symphony Orchestra from 1931 to 1956), composer, arranger, educator, broadcaster and administrator. His talent and devotion won him the only knighthood ever bestowed upon a Canadian musician. 
						</p>
						<p>
							His original compositions included theatre pieces, orchestral works, chamber, keyboard and choral music. He also helped preserve the country&rsquo;s rich heritage of folk music through his arrangements of homegrown material. He composed this appealing work in 1927. The occasion was the Canadian Pacific Railway&rsquo;s Qu&eacute;bec Folk Festival. He came across both melodies in Folk Songs of French Canada (1925), a collection edited by Marius Barbeau and Edward Sapir. 
						</p>
						<p>
							According to MacMillan, the first sketch, Notre Seigneur en pauvre (Our Lord in Beggar&rsquo;s Disguise) &ldquo;tells the well known legend (found in many variants), of Christ disguising himself as a beggar. He is brusquely refused by the rich miser but is welcomed by his pregnant wife who gives him food and shelter. The second sketch, &Agrave; Saint-Malo, pictures the Breton port of Saint-Malo from which so many of the early settlers in Canada, beginning with Jacques Cartier, originally set sail. The present tune is a great favourite, and while not highly distinctive, it is characterized by a briskness and gaiety which commend it to the hearer.&rdquo; 
						</p>
						<p>
							Serenade for Strings in E-flat Major, Op. 6<br>Josef Suk 
						</p>
						<p>
							Suk played a major role in the musical life of his native land (Bohemia, later renamed Czechoslovakia, then the Czech Republic). He was active as a composer, teacher, and a violinist for more than 40 years in the acclaimed Czech Quartet. He composed numerous piano works, songs and chamber pieces. His finest creations are his orchestral compositions, headed by the dramatic Asrael (Angel of Death) Symphony, and the delightful concert suite, A Fairy Tale. 
						</p>
						<p>
							He graduated from the Prague Conservatory in 1891, but he stayed on an extra year to study composition with the great composer Anton&iacute;n Dvoř&aacute;k. This proved crucial to his professional and personal lives alike. He became Dvoř&aacute;k&rsquo;s favourite pupil, and some of his music&mdash;early works such as this delectable Serenade in particular&mdash;is cast in the mould of his teacher&rsquo;s warm-hearted, folk-influenced style. Serious and sombre describe much of the music that Suk composed for Dvoř&aacute;k&rsquo;s class, however, including the Dramatic Overture he created for his graduation in 1892. At that time, Suk entered into a romantic relationship with Dvoř&aacute;k&rsquo;s daughter Otylka. They married in 1898. 
						</p>
						<p>
							Soon after Suk&rsquo;s graduation, Dvoř&aacute;k sent him off on holiday with the following suggestion: &ldquo;It&rsquo;s summertime now, so go and make something cheerful for a change, some respite from these eternal pomposities in minor keys.&rdquo; The eighteen-year-old Suk responded with this serenade. He composed the first three movements while visiting his hometown, Křečovice, and the finale later that autumn. The Prague Conservatory Orchestra gave the first complete performance on 25 February 1894. It so strongly impressed Johannes Brahms, who had already done much to promote the music of Dvoř&aacute;k, that he recommended it to his publisher. It was printed in 1896. 
						</p>
						<p>
							Suk&rsquo;s piece does show the influence of the Serenade for Strings that Dvoř&aacute;k had composed in 1875 (and which the MCO performed at the opening concert of the season). Yet it has enough individuality and creative assurance to stand as much more than a simple act of imitation. The first movement is relaxed and genial. Suk follows it with the sort of gentle, lilting waltz that both Dvoř&aacute;k and Tchaikovsky had included in their serenades for strings. The poignancy that wells up in the central section of the waltz comes to the fore in the third movement, the most substantial and serious of the four. The finale is vigorous and joyful, but Suk finds room in it for more thoughtful moments, too. 
						</p>
						<p>
							Dvoř&aacute;k didn&rsquo;t have a chance to hear Suk&rsquo;s Serenade until 1894 when he returned from his first visit to America. He expressed his approval, adding this playful comment: &ldquo;You will never get very far: you write too big notes&mdash;save paper! Still, Handel also wrote big notes and yet he was a great composer!&rdquo; 
						</p>
