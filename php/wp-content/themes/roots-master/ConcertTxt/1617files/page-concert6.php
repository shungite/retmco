			<p>
				In with the New! For all the famous, veteran soloists the MCO presents, we feature nearly as many young prodigies on the cusp of fame. Guest conductor Gemma New and double bassist Sam Casseday are surely among them. 
			</p>
			<p>
				With jaw-dropping artistry, 23-year-old Casseday will perform a double bass arrangement of Mozart&rsquo;s 5th violin concerto, otherwise known as &lsquo;The Turkish.&rsquo; 
			</p>
			<p>
				You know the tune. You&rsquo;ve heard it played on violin and as a cellphone ringtone &mdash; but have you ever heard it played on a big, bad double bass? 
			</p>
			<p>
				Around the same time Gemma New debuted at Carnegie Hall in Manhattan two years ago, the highest rated classical music radio station in the US named her one of the top five female conductors on the rise. As if to underscore their assessment, an orchestra no less prestigious than the St. Louis Symphony announced Gemma&rsquo;s appointment to the position of &lsquo;Resident Conductor&rsquo; effective from the beginning of the 2016/17 season. Celebrated for her insightful interpretations and dramatic performances, the 29 year-old will amply demonstrate to Winnipeggers why she&rsquo;s been causing such a stir. 
			</p>
			<p>
				<b> Gemma New, breaking the glass ceiling </b> 
			</p>
			<p>
				Gemma New, who is the Hamilton Philharmonic Orchestra&rsquo;s recently-appointed music director, is (alongside the MCO&rsquo;s Anne Manson) one of North America&rsquo;s few female conductors. New has never let the gender gap, a carry-over of the male-centric history of classical music, faze her. &ldquo;Just growing up, it never occurred to me to ask, &lsquo;Can I do this because I&rsquo;m a girl?&rsquo; It seemed laughable to me,&rdquo; she said. &ldquo;I strongly believed, and I was always told, that if you had a passion and a mindset and a career path you want to pursue, then you can do it.&rdquo; 
