						<p>
							<b> Gemma New </b> 
						</p>
						<p>
							Sought after for her insightful interpretations and dynamic performances, New Zealand-born conductor Gemma New currently serves as Music Director of the Hamilton Philharmonic Orchestra and Associate Conductor for the New Jersey Symphony Orchestra. A strong advocate for new music and innovative orchestral models, she is the Founder and Director of the Lunar Ensemble, a contemporary music collective in Baltimore as well as being the Principal Conductor for the New York City based ensemble Camerata Notturna. Ms New has been appointed Resident Conductor of the St. Louis Symphony Orchestra, effective at the start of this season. 
						</p>
						<p>
							As one of two Dudamel Conducting Fellows with the Los Angeles Philharmonic for the 2014/15 Season, Ms New led nine LA Phil concerts and covered frequently for their Music Director, Gustavo Dudamel, Conductor Laureate Esa-Pekka Salonen, and other guest conductors. For the month of September 2014, at the invitation of Maestro Kurt Masur, Ms New resided in Leipzig, Germany as a Felix Mendelssohn-Bartholdy Fellow, where she studied Mendelssohn&rsquo;s music under Maestro Masur and led the Leipziger Symphonieorchester in the historic Lindensaal of Markkleeberg. 
						</p>
						<p>
							In 2013, Ms New made her Carnegie Hall conducting debut, leading works by Adams, Norman and Ives on the American Soundscapes series. That same year she was selected as the David A. Karetsky Conducting Fellow at the American Academy of Conducting at the Aspen Music Festival. One year prior Ms New was awarded the Ansbacher Fellowship, in which she was selected by members of the Vienna Philharmonic to take up residence at the Salzburg Music Festival. 
						</p>
						<p>
							Under her leadership, the Lunar Ensemble has premiered almost 40 works since 2011 and has held composition residencies at several US universities, including the Peabody Conservatory in Baltimore, Frost School of Music in Miami FL and Tulane University in New Orleans. Another Lunar Ensemble highlight was the Pierrot Centenary Project, which united all fifty poems of Albert Giraud&rsquo;s Pierrot lunaire through commissioned works by selected composers, culminating in the release of Lunar&rsquo;s first album, featuring those works alongside Schoenberg&rsquo;s seminal Pierrot lunaire. 
						</p>
						<p>
							Passionate about music education, Ms New has enjoyed working with the NJSO Academy Orchestra and New Jersey All State Orchestra during her time as Associate Conductor for the NJSO. Between 2007 and 2009, Ms New conducted the Christchurch Youth Orchestra, which grew from 40 to 70 players under her leadership and performed upwards of nine concerts a year. 
						</p>
						<p>
							Ms New holds a Master of Music degree in orchestral conducting from the Peabody Institute in Baltimore, where she studied with Gustav Meier and Markand Thakar. She graduated from the University of Canterbury, New Zealand with a Bachelor of Music (Honours) in violin performance. 
						</p>
						<p>
							<b> Sam Casseday </b> 
						</p>
						<p>
							Sam Casseday, from Jacksonville, Florida, entered the Curtis Institute of Music&mdash;where he is the Mrs. Cary William Bok Fellow&mdash;in 2012 and studies with Harold Hall Robinson, principal bass of the Philadelphia Orchestra, and renowned double-bass play&shy;er, Edgar Meyer. Mr. Casseday has appeared on National Public Radio&rsquo;s From the Top and as a soloist with the Jacksonville Symphony Orchestra. He previously attended summer programs at Music Acade&shy;my of the West in Santa Barbara and at the Aspen Music Festival and School. At Music Academy of the West he competed in the final round of the festival&apos;s solo competition. Primarily an orchestral player, Sam has played with the Jacksonville Symphony Orchestra. He began double bass lessons at age five and studied previously with his father, Kevin Casseday. He resides in Philadelphia and is entering his fifth and final year at the Curtis Institute. 
						</p>
						<p>
							<b> Allan Gordon Bell </b> 
						</p>
						<p>
							Allan Gordon Bell was born in Calgary in 1953. He received a Master of Music degree from the University of Alberta, where he studied with Violet Archer, Malcolm Forsyth and Manus Sasonkin. He also did advanced studies in composition at the Banff Centre, where his teachers were Jean Coulthard, Bruce Mather and Oskar Morawetz. He is now Professor of Music at the University of Calgary, an associate Composer of Music at the University of Calgary, an Associate Composer and Past President of the Canadian Music Centre, a Fellow of the Royal Society of Canada, and a Member of the Order of Canada. 
						</p>
						<p>
							Bell has created works for solo instruments, chamber ensembles, orchestra, band, and electro-acoustic media, as well as an opera. His music has been performed by the Calgary Philharmonic Orchestra, Esprit Orchestra, the Orford String Quartet, eighth blackbird, the ensembles of Toronto New Music Concerts, Arraymusic, Soundstreams Canada, the Soci&eacute;t&eacute; de musique contemporaine du Qu&eacute;bec New Works Calgary and Lands End Chamber Music&mdash;as well as many other professional and amateur organizations in North America, Europe and Asia. Symphony orchestras that have performed Bell&apos;s compositions include those of Edmonton, the National Arts Centre, Nova Scotia, Montreal, Tokyo, Vancouver, Victoria and Winnipeg. In Winnipeg, he was Distinguished In 2002 and 2011, he was Distinguished Visiting Composer at the Winnipeg New Music Festival, and in 2014 he was the composer-in-residence at the Shattering the Silence Festival and a visiting composer at the Festival of the Sound. In 2014, Bell won a Juno award for his chamber work Field Notes in the Classical Composition of the Year category. 
