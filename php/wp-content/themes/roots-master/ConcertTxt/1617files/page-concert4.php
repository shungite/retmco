						<p>
							<b>Andante festivo<br>Jean Sibelius</b> 
						</p>
						<p>
							Side by side with Sibelius&rsquo;s epic symphonies and tone poems rests a series of shorter, lighter works. He devoted as much care to them as he did to his more ambitious creations; only the scale is different. 
						</p>
						<p>
							In 1922, the owners of a factory in a Finnish city, S&auml;yn&auml;tsalo, commissioned him to compose a celebratory cantata for the twenty-fifth anniversary of their building. He responded with the brief <i>Andante festivo</i> for string quartet instead. He prepared this version for string orchestra eight years later. 
						</p>
						<p>
							Its purity, nobility and passion have led some writers to call it the closest he came to making a religious statement in music. (Is the final cadence an Amen?) These qualities have led to its being performed on state occasions in Finland. 
						</p>
						<p>
							On New Year&rsquo;s Day, 1939, the seventy-three-year-old composer, who had not conducted an orchestra for more than a decade, led the Finnish Radio Orchestra in an especially intense performance of this piece. That was the last time he stood in front of an orchestra. Beamed by short-wave radio to the World&rsquo;s Fair in New York, it was recorded on a wax cylinder. It is the only recorded example of his conducting. 
						</p>
						<p>
							<b>Symphony No. 49 in F Minor&mdash;La Passione<br>Joseph Haydn</b> 
						</p>
						<p>
							When Haydn began composing symphonies in the late 1750s, little distinction existed between multi-sectioned opera overtures and symphonies written for concert performance. One of his primary accomplishments was to chart an independent course for the symphony. In his more than 100 examples of the genre, he raised it to the summit of its artistic value. 
						</p>
						<p>
							He composed his initial 15 or so symphonies during the three years he spent working for his first employer, Bohemian nobleman Count Karl Joseph Franz von Morzin. In 1761, after Morzin disbanded his orchestra due to financial difficulties, Haydn took up the position of Vice-Kapellmeister to an even wealthier and more influential family, the Esterh&aacute;zys of Hungary. His numerous responsibilities included composing operas, symphonies, chamber, and vocal music, and maintaining the court orchestra and library. Having a superb orchestra to work with provided a crucial tool in his quest to expand the contents and meaning of the symphony. What more could a composer ask than to have his new pieces played immediately by a crack ensemble? 
						</p>
						<p>
							Between approximately 1768 and 1772, he composed a number of urgent, dramatic works in minor keys, Symphony No. 49 among them. This is often referred to as his <i>Sturm und Drang</i> (Storm and Stress) period, mirroring an emotionally similar and slightly later movement in German literature. Symphony No. 49 dates from 1768. It would most likely have been premiered shortly after he completed it, under his direction, by the Esterh&aacute;zy court orchestra. 
						</p>
						<p>
							It may contain links to Easter. Its nickname, &lsquo;La Passione,&rsquo; which was applied anonymously at a later date, has several other possible sources, including an origin in incidental music for a stage drama, or simply its deeply serious character. 
						</p>
						<p>
							Its sequence of movements is unusual. It opens with a dark, grieving slow movement, one that the eminent Haydn scholar H.C. Robbins Landon feels may suggest &ldquo;the winding line of penitents before the Cross.&rdquo; Haydn follows it with three quick movements. For maximum effectiveness and to skirt any possibility of monotony, he places the stately Minuet (its gentle central Trio is the symphony&rsquo;s sole music in a major key) between two brisk, fiercely dramatic movements. 
						</p>
						<p>
							<b>Exsultate, jubilate, K&thinsp;165<br>Wolfgang Amadeus Mozart</b> 
						</p>
						<p>
							Mozart set out on what proved to be his third and final visit to Italy in October 1772. The sixteen-year-old composer bore with him the first sketches for <i>Lucio Silla</i>, an opera he had been commissioned to write for the 1772 Milanese carnival. The opera&rsquo;s premiere on 26 December at the Regio Ducal Teatro found great favour. 
						</p>
						<p>
							Much credit for this was due to the castrato singer Venanzio Rauzzini. Mozart admired the brilliance and agility of his voice, and gladly agreed to compose a work showcasing these vocal traits. &ldquo;I am about to write a motet for the <i>primo uomo</i>, which is to be produced at the Theatine Church tomorrow,&rdquo; he wrote in a postscript to a letter from his father to his mother. He composed the motet &lsquo;Exsultate, jubilate&rsquo; in January 1773. Rauzzini performed the premiere on January 17. The author of the motet&rsquo;s non-liturgical Latin text is unknown. 
						</p>
						<p>
							Nothing save brilliance and energy radiates from the outer sections. An orchestral introduction, delightfully coloured by oboe and horn flourishes, precedes the opening aria, &lsquo;Exsultate, jubilate&rsquo; (Exult, rejoice). Mozart reinforces the motet&rsquo;s resemblance to an instrumental concerto through a brief solo cadenza. After a compact recitative, the second and considerably longer aria, &lsquo;Tu virginum corona, tu nobis pacem dona&rsquo; (Crown of all virgins, grant us peace) is launched in its gentle, consoling way. Mozart aptly pares back the scoring to strings and organ. The full ensemble is restored to support the enchanting concluding Alleluja, whose style leans toward popular music. One of Mozart&rsquo;s best-known vocal compositions, it is frequently performed on its own. 
						</p>
						<p>
							The entire motet contains a good deal of vocal coloratura. To modern ears, such showy brilliance, inspired by Rauzzini&rsquo;s operatic prowess, might appear out of place in a church work. Audiences of Mozart&rsquo;s time would have found it neither surprising nor offensive given that no distinction was made between sacred and secular musical styles, and places of worship were typically decorated with completely secular paintings. 
						</p>
						<p>
							<b>Bachianas brasileiras, No. 9<br>Heitor Villa-Lobos</b> 
						</p>
						<p>
							The music of Bach is the very heart and soul of Brazil. Throughout his long, astonishingly active career, nurturing all things Brazilian remained his primary goal. He spent his early years travelling throughout his country&rsquo;s vast hinterland, playing the cello, studying native music on location, and composing original works in vast quantities. As a mature artist, he composed hundreds more pieces in every imaginable form, conducted orchestras, bands and choirs, became deeply involved in musical education, and generally served as Brazil&rsquo;s unofficial musical ambassador to the world. 
						</p>
						<p>
							Villa-Lobos idolized Johann Sebastian Bach, writing &ldquo;[t]he music of Bach is without question the most sacred gift to the world of art.&rdquo; While working on transcriptions of compositions by Bach (such as a selection of preludes and fugues from <i>The Well-Tempered Clavier</i>, which he arranged for chorus or an orchestra of cellos), Villa-Lobos noticed similarities between them and Brazilian folk music. This inspired him to create nine suites for various instruments in tribute to Bach. They combine original themes in Brazilian folk style with Bach&rsquo;s European Baroque composition methods. This is the origin of their collective name, <i>Bachianas brasileiras</i>. Villa-Lobos considered them &ldquo;the kind of music the Leipzig master might have written had he been born a twentieth-century Brazilian composer.&rdquo; Many of the movements bear two titles. The first is related to traditional European forms, and the second to the popular music of Brazil. 
						</p>
						<p>
							He initially composed <i>Bachianas brasil&shy;eiras No. 9</i> (1945) for what he called &ldquo;an orchestra of voices.&rdquo; When that version proved too challenging for most choirs, he produced the edition for string orchestra that you will hear at this concert. Consisting, &agrave; la Bach, of a prelude and fugue, <i>Bachianas brasileiras No. 9</i> is the most Baroque-orientated piece in the collection. Villa-Lobos strengthened the integration of the sections&mdash;the slow, stately Prelude, and the vigorous, rhythmically intriguing Fugue&mdash;by basing them on the same theme. 
						</p>
						<p>
							<b>Vocalise, Op. 34 No. 14<br>Sergei Rachmaninoff</b> 
						</p>
						<p>
							It is ironic that out of Rachmaninoff&rsquo;s more than 70 songs, the one that has become the most popular is the only one that does not have words: this haunting, intensely beautiful Vocalise. It comes from a collection of 14 songs composed between 1910 and 1912. As he often did, he tailored it to the voice of a singer he knew personally. In this case it was Antonina Nezhdanova, coloratura soloist with the Moscow Grand Opera. A vocalise is usually a piece designed strictly as a training exercise for the voice. Rachmaninoff conceived this example more as a wordless aria. He prepared an arrangement for orchestra in 1915, while other hands have prepared many further transcriptions. 
						</p>
						<p>
							<b>The Great Miracle<br>Vasyl Barvinsky</b> 
						</p>
						<p>
							Barvinsky received his musical education in Lviv and Prague, after which he taught music at the Lysenko Higher Institute of Music in Lviv from 1915 to 1939. From 1939 to 1941, and again from 1941 to 1948, he taught at the Lviv Conservatory. He was highly active in the promotion of Lviv&rsquo;s musical life and served as the President of the Union of Ukrainian Professional Musicians. In 1948, he was sentenced by the Soviet authorities to ten years&rsquo; imprisonment. Released in 1958, he spent most of his remaining years trying to reconstruct his works, which had been destroyed at the time of his arrest. He was posthumously &ldquo;rehabilitated,&rdquo; politically speaking, in 1964. His musical style is decidedly neo-romantic, with a touch of impressionism. He created some 30 works in all, instrumental and vocal alike. He also produced arrangements of many Ukrainian folk songs. The lovely Christmas carol, <i>The Great Miracle</i> (also known as <i>Oh, What a Miracle</i>), is one of his best-known works. 
						</p>
