						<p>
							<b>Mark Morash</b> 
						</p>
						<p>
							A former faculty member at the University of Toronto, Mark Morash is a conductor and pianist originally from Dartmouth, Nova Scotia. He currently serves as the Director of Musical Studies for the San Francisco Opera Center. He has also led productions and concerts with the Merola Opera Program and Western Opera Theater. In recent years, Morash has also led performances of <i>Rigoletto</i> with Opera Colorado as well as <i>Don Giovanni</i> and <i>The Turn of the Screw</i> for the Lincoln Theater in Yountville, California. His work with the San Francisco Opera Center has included such varied repertoire as <i>Cos&igrave; fan tutte</i>, <i>Die Fledermaus</i>, <i>The Merry Wives of Windsor,</i> Donizetti&rsquo;s <i>Rita</i>, Pasatieri&rsquo;s <i>The Seagull</i>, Pergolesi&rsquo;s <i>La Serva Padrona</i> and Ibert&rsquo;s <i>Ang&eacute;lique</i>. As a collaborative pianist, Morash&rsquo;s performances have taken him throughout North America, Japan and Russia. He has collaborated with such renowned artists as Michael Schade, Tracy Dahl and Sheri Greenawald, and he has accompanied numerous emerging singers in San Francisco Opera Center&rsquo;s esteemed Schwabacher Debut Recitals. Morash has also been involved with the Opera Center of Pittsburgh Opera, Wolf Trap Opera, the Banff Centre, and Hawaii Opera Theater. 
						</p>
