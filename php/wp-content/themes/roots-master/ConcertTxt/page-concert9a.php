		<h4><div>Reflecting on the unique legacies of the French, First Nations,<br>and M&eacute;tis in Canada through a spectacular theatrical presentation</div></h4>
		<p>Do you know Nanabush? He&rsquo;s the trickster figure of Anishnaabe mythology, a wry cultural hero who, according to legend, is responsible for dogs&rsquo; proclivity to sniff one another&rsquo;s tails and for rescuing the forest from Paul Bunyan.</p>
		<p>In <i>Nanabozho et le tambour&thinsp;/&thinsp;Nanabush and the Drum</i> he is envisioned as a central figure in the foundation of the M&eacute;tis. Nanabush (known to Francophones as Nanabozho) sets up playful obstacles for a French voyageur attempting to navigate the land and woo his more savvy Indigenous love interest.</p>
		<p>The production is unique among MCO collaborations, an immersive new work of art which brings together a group of celebrated artists: Th&eacute;&acirc;tre Cercle Moli&egrave;re, writer Rh&eacute;al Cenerini and composer Michael Oesterle. In the tradition of Stravinsky&rsquo;s <i>A Soldier&rsquo;s Tale</i>, <i>Nanabozho et le tambour&thinsp;/&thinsp;Nanabush and the Drum</i> is an enchanting piece of theatre that merges classical music and folk legends.</p>
		<p>While celebrating Canada&rsquo;s 150th birthday, the piece illuminates the diversity of our heritage. The work is bilingual, but accessible to Anglophones and Francophones equally, and appropriate for all ages. Matinee performances for school groups will take place both days.</p>
		<p><b>Th&eacute;&acirc;tre Cercle Moli&egrave;re</b></p>
		<p>Th&eacute;&acirc;tre Cercle Moli&egrave;re is the oldest theatre company with uninterrupted programming in Canada. Established in 1925, the company&rsquo;s founders had the objective of bringing the French language to life within and beyond St-Boniface&rsquo;s vibrant community. Today, the objective remains the same as the company strives to bridge language barriers with their subtitled performances and present inclusive and diverse programming to appeal to Winnipeg&rsquo;s ever-changing community.</p>
		<p>&ldquo;The company&rsquo;s future is brimming with new partnerships, co-productions with major players, Canadian tours and new works that reflect the francophone community today. We are thrilled about the new developments to come for this 90-year-old company, en route to our centennial&rdquo; &mdash;&thinsp;Genevi&egrave;ve Pelletier, Artistic Director.</p>
