      These three twentieth century works each capture very different but powerful emotional qualities. <i>Billy&rsquo;s Theme</i> by Howard Shore, featuring guitar and orchestral accompaniment, illustrates the feelings of an undercover police agent named Billy Costigan. <i>Musica Celestis</i>, by Aaron Jay Kernis, is inspired by medieval references to the singing of angels, and <i>La Jalousie Taciturne</i> by Gerald Barry is a title taken from a Couperin harpsichord suite and refers to different forms of &ldquo;taciturn jealousy.&rdquo;

      <p>
      When you listen to <i>Billy&rsquo;s Theme</i> consider:
      </p>

      <ul>
        <li>How does the repeated opening rhythm and the repeated melody note capture the tormented qualities of the film&rsquo;s character?</li>
        <li>How does melodic and rhythmic treatment in the guitar solo express struggle, doubt, and desperation?</li>
        <li>How does the lyric quality of the guitar&rsquo;s melodic treatment capture elements of that film&rsquo;s character?</li>
        <li>How does the orchestral background and ostinato (repeated pattern) effect create tension in this music?</li>
        <li>How is increased tension and despair created in the music towards the end of this piece? How does the orchestra accompaniment playing alone at the end of this work contribute to the feeling of despair?</li>
      </ul>

      <p>
      When you listen to <i>Musica Celestis</i> (Music of the Heavens) consider:
      </p>

      <ul>
        <li>How does the composer&rsquo;s use of high range and ascending expansive melodic lines contribute to the ethereal mood of this work?</li>
        <li>How do the flowing rhythmic treatments contribute to a feeling of praise &ldquo;without end?&rdquo;</li>
        <li>How are tempos and dynamics used to create meditative qualities?</li>
        <li>How does the use of harmonies and occasional modal lines in this work support a spiritual quality?</li>
        <li>How do the 7 variations of the theme suggest increasing emotional intensity, rapture, and joy as the piece evolves?</li>
        <li>How does Kernis use instrumental timbre and color to create contrasts of feeling and emotion?</li>
        <li>What is the effect of passages where instruments are muted or play without vibrato?</li>
        <li>How are expressive music elements (dynamics, tempo, legato, sustained rhythms and melodies) used to create a return to serenity at the conclusion of this work?</li>
      </ul>

      <p>
