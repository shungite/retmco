      <p><b>FRANZ JOSEPH HAYDN: CELLO CONCERTO IN D MAJOR, OP. 101</b></p>

      <p>Franz Joseph Haydn (1732-1809) composed his second Cello Concerto in D major around the year 1783 although it was at one time attributed to Anton Kraft, a renowned cellist whom Haydn knew well. Anton Kraft studied composition for awhile with Haydn and aslo premiered the work, but musicologists are now convinced that the work was written by Haydn. Haydn likely composed the work for Kraft as soloist since Kraft was such a prominent cellist of the time. A manuscript in Haydn’s writing of this cello concerto, rediscovered in the twentieth century, further confirms the authenticity of this Haydn work which is one of only two authenticated cello concertos by Haydn.</p>

      <p>Haydn may have learned much about composing for cello from Anton Kraft; certainly this famous virtuosic cello concerto has been delighting audiences since it was first composed and performed well over 200 years ago!</p>

      <p><b>When you listen to this Cello Concerto by Haydn, you will hear a work in three different sections called movements. You will hear a solo cello perform in virtuosic ways.</b></p>

      <div class="well">
