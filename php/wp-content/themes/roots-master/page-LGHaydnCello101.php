      <p><span class="badge">2</span> What adjectives might describe the mood that you felt when listening to this cello F? Can you identify what musical elements may have created that mood for you? When did the mood change and why?</p>

      <p><span class="badge">3</span> What musical elements did you enjoy or find interesting? Did you enjoy the melodies that you heard? Did the rhythms, dynamics, or tempos used by Haydn help make this work energetic? Did you hear parts of the cello concerto that made you feel peaceful or calm and if so, what part of the cello concerto were they in? Were there any parts that sounded playful to you? If so, which movement did you hear them in?</p>

      <p><span class="badge">4</span> Were there parts of the Cello Concerto that you did not enjoy? Why or why not? Can you identify which music elements made you enjoy or not enjoy the music?</p>

      <p><span class="badge">5</span> Different people often have different responses to the same music. Ask someone else who heard the same music about their response to Haydn’s second Cello concerto.</p>

