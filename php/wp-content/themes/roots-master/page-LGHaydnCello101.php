      <p><b>The following questions can help you listen to this work:</b></p>

      <p><span class="badge">1</span> Can you hear that this concerto is written in three different sections, or movements? They are called:</p>

      <ol>
        <li>Allegro moderato (moderately fast and lively)</li>
        <li>Adagio (slowly)</li>
        <li>Allegro (fast and lively)</li>
      </ol>

      <p>Each of the three sections or movements also has a particular form. The first movement Allegro moderato is written in sonata form. Can you hear the orchestra introduce the two main themes of this movement in the opening introduction (called ritornello)? Can you hear those two different themes then played by the solo cello?</p>

      <p>Can you hear how Haydn develops the two lyrical principal and secondary themes introduced at the beginning of the work in different ways throughout this movement, and then returns to the themes at the end of the movement? This kind of compositional development is characteristic of sonata form.</p>

      <p>If you have ever heard Mozart&rsquo;s opera Don Giovanni, listen to see if you can hear the similarity between one of the opera’s arias and the opening theme of the first movement!</p>

      <p>The second Adagio slow movement in A major is organized in five parts in a way similar to sonata-form. The different sections are all connected by the solo singing melody of the cello throughout.</p>

      <p>At the beginning of the slow movement, can you hear a beautiful melodic theme played by the cello accompanied by a few stringed instruments? Can you hear this main theme returning throughout the movement? Can you hear how the music played between the statements of the main theme contains variations or decorations of the theme? Is there one section that has a different mood to the others?</p>

      <p>Can you hear any similarities between the opening Adagio theme and the opening first movement Allegro theme?</p>

      <p>Do you hear the return of the main theme at the end of the Adagio with a final short cadenza played by the cello?</p>

      <p>The third Allegro movement is called a rondo. The rondo movement was a common form for the third movement in this time period. In rondo form, a main theme is repeated before and after contrasting sections. The repeated theme is called the A section. The contrasting sections are called B, C, D etc., depending on how many different sections the composer decides to compose. In this rondo the repeated theme is heard in three different episodes.</p>

      <p>Do you hear how the folk-like motif first heard in the cello is used to build the rest of the movement?</p>

      <p><span class="badge">2</span> What <b>instruments</b> can you hear in this concerto? How many performers are playing onstage? What string instruments do you hear (violin, viola, cello, double bass)? Can you pick out when you hear the oboes and horns? Can you hear a harpsichord playing? </p>

      <p>When do you first hear the cello? Do you hear the way that Haydn focuses on the cello and treats the cello as a special, solo instrument? Can you hear how Haydn gives the cello brilliant solo passages that feature the cello’s upper register in the opening movement?</p>

      <p>Can you hear when the cello gets to play a very difficult, virtuosic section all by itself towards the end of the first movement after the final return of the main themes? This showy section is called a <b>cadenza</b>.</p>

      <p>Can you hear when the orchestra plays by itself or when different instruments can be heard above the rest? Which instruments are given the predominant melodies or themes in this work?</p>

      <p><span class="badge">3</span> Although this concerto does not have the dramatic tension and contrast that can sometimes be heard in Haydn’s works, the composer does use <b>tempo</b> (the speed of the music), <b>dynamics</b> (the volume of the music), and <b>expression</b> (musical elements that express certain feelings or dispositions) to create different moods throughout the concerto.</p>

      <p>Can you identify the mood Haydn is trying to create in each movement? Can you hear a change in mood when Haydn writes a section of the second movement Adagio in a minor key?</p>

      <p>Haydn was known for the good humour that can be heard in some of his writing. Can you hear that cheerful, good humour in the final Allegro movement? Can you hear where Haydn gives the cello soloist opportunities to demonstrate virtuosic technique?</p>

      <p>Which movements use mostly fast music? Which movement uses mostly slow music? Can you hear when the tempo of the music changes and gets faster or slower? How does that affect the mood of the work?</p>

      <p>Was the music played at all the same volume (dynamics)? When do you hear music played loudly? Quietly? When do the dynamics change and what effect does that create? When does the music get louder (crescendo) or get quieter (decrescendo)?</p>

      <p>Do you hear musical sounds that are short sounding (staccato) or very smooth sounding (legato)? Which movement features legato sounds? Where do you hear suddenly loud sounds (accents)?</p>

      <p><span class="badge">4</span> How is <b>rhythm</b> used in this cello concerto? Do you hear sections that have a steady rhythm or beat that you could tap to? Did any particular rhythmic pattern stand out for you that you could listen for in the work? Was there a particular pattern that you could hear repeated anywhere in the work?</p>

      <p>The opening movement begins with a gentle rhythmic pattern that sounds like “Who’s knocking knocking please--come in.” Do you hear that pattern in the first movement? Can you hear the “knocking knocking” rhythm motif in the second theme of the first movement?</p>

      <p>Part of the first movement theme 1 rhythmic pattern is heard again in the opening of the second movement Adagio—can you hear the little “please—come in” motif in the cello?</p>

      <p>Can you hear any other repeated rhythmic patterns in other movements? Did any particular rhythmic pattern stand out for you that you could listen for in this symphony?</p>

      <p><span class="badge">5</span> How is <b>melody or pitch</b> used in this cello concerto?</p>

      <p>Do you hear high sounds or pitches? Do you hear low sounds or pitches? Can you pick out a particular melody played by the orchestra instruments or cello in each of the three movements? Can you hum or sing that melody? Could you draw the shape of the melody in the air? When does it move up higher and when does it move lower?</p>

      <p>Can you hear that the opening melody (theme) in the first movement is pitched in a high register and the second melody (theme) is pitched in a lower register? Do you hear a melody in a certain instrument that is then played by a different instrument?</p>

      <p>Can you hear a melody that is repeated over and over again in the third section or movement of the cello concerto? A piece where a melody keeps coming back after different or contrasting sections is called a rondo.</p>

      <p><span class="badge">6</span> Can you hear different kinds of <b>texture</b> in this work? Where do you hear lots of instruments playing together so that the sound is thick? Where do you hear just a few instruments playing? Can you tell which instruments are playing?</p>

      <p><span class="badge">7</span> What kinds of instrumental <b>timbre</b> do you hear? Timbre is the different qualities of sound that can be heard, for example the kinds of sounds that the string instruments make when they play together. When does the timbre of the music change because Haydn adds different instruments to the strings? Can you hear when the strings are playing alone and when they are playing together with the cello and other instruments?</p>

      <p>Can you hear different qualities of sound when the oboes, horns, and harpsichord play with the cello and orchestra?</p>

