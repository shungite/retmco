      <b>
        ANTONIO VIVALDI: CONCERTO FOR LUTE IN D MAJOR (RV93) ARRANGED FOR GUITAR
      </b>
      </p>

      <p>
      Antonio Vivaldi (1678-1741) was a highly prolific Italian composer of the Baroque period (roughly 1600-1750). He wrote choral music, opera, church music, music for a variety of instruments including the lute, and was renowned for his over 500 instrumental concertos. His famous set of concertos, <i>The Four Seasons</i>, has been heard around the world in concert halls, films, advertisements, and arrangements. 
      </p>

      <p>
      Vivaldi&rsquo;s concertos were innovative in their clarity of form (typically three movement), harmonic treatment, and rhythmic and melodic vitality. The Lute Concerto in D major (RV93) was written in the 1730s and is one of four lute works written by Vivaldi. The lute was an important instrument in the 16th and 17th century and like the guitar, is a plucked stringed instruments. The lute and guitar are similar in sound qualities and construction, although the lute has a pear shaped body and more strings. The modern guitar was not invented in Vivaldi&rsquo;s time; however, this chamber concerto is often now performed by guitar as in this performance by soloist Sharon Isbin.
      </p>

