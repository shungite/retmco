      <b>
        The following questions can help you listen to this work:
      </b>
      </p>

      <span class="badge">1</span> <b>Form</b> in music refers to the musical architecture or the way the music is structured. The Lute Concert in D Major follows Vivaldi&rsquo;s typical fast slow fast three-movement form for concertos. Can you hear that this work is in three large sections or movements called:

      <ol>
        <li>Allegro (lively and quick)</li>
        <li>Largo (very slow)</li>
        <li>Allegro (lively and quick)</li>
      </ol>

      <P>
      Each of the three sections has its own form or structure. The first movement features <b>ritornello</b> form. Ritornello means &ldquo;returning&rdquo; and refers to an instrumental section of music repeated throughout the concerto. The ritornello is a structuring device that serves a variety of functions. Ritornello form highlights the contrasting soloist sections and provides unity as recurring thematic material is played by the larger tutti ensemble. The ritornello also serves to reinforce the main key following modulations featured in the solo sections that contrast with the orchestral ritornello. 
      </P>

      <p>
      Can you hear that the opening and closing ritornello of the first movement includes both the large ensemble and the solo guitar? In between the opening and closing ritornellos, the whole orchestra (tutti) ritornello alternates with sections played by the solist and continuo. In total, it is possible to count 11 different sections in this movement. 
      </p>

      <p>
      The opening ritornello features two contrasting themes, one in major and a second, lyrical theme in a minor key. Can you hear the difference between these two themes? These themes are heard varied throughout the 11 different sections of this movement. 
      </p>

      <p>
      The slow second movement is not a ritornello form but it is in different sections: an A A B A (same, same, different, same) form that features the lyric melodies of the lute played against a string accompaniment.
      </p>

      <p>
      The final third allegro movement, lighter and more playful than the first movement, is in a lively 6/8 time signature that has a dance quality feeling of a tarantella, a fast, whirling Italian folk dance. This movement also features alternating, contrasting tutti chamber orchestra, solo guitar and continuo sections. 
      </p>

      <p>
      <span class="badge">2</span> What <b>Instruments</b> can you hear in this Lute Concerto by Vivaldi? Can you hear the guitar contrasted with the tutti (all instruments played together) thematic ritornello sections? Can you hear the conversations between the solo and tutti groups? Do you also hear a harpsichord that accompanies the lute? A harmony instrument such as a harpsichord or organ along with a bass melody instrument was known in Vivaldi&rsquo;s time as the <b>continuo</b>. 
      </p>

      <p>
      <span class="badge">3</span> How does Vivaldi use <b>expressive elements</b> (musical elements that express certain feelings or dispositions), varying <b>tempos</b> (the speed of the music), and <b>Dynamics</b> (the volume of the music) to create interest and excitement? What <b>tempos</b> (the speed of the music) can you identify in this work? Do you hear fast tempos in the first and third movements? 
      </p>

      <p>
      Do you hear contrasting slow tempos in the second movement? Can you hear how the slower tempo of the second movement allows the guitar to show off technical skill using ornamented and decorated notes?
      </p>

      <p>
      Does the concerto begin with loud or quiet dynamics (volume of sound)? How does Vivaldi feature the quieter sounds of the guitar? Can you hear how Vivaldi uses different dynamic levels for different parts, for example loud (forte) for the tutti (whole orchestra) and quiet (piano) for the guitar? 
      </p>

      <p>
      Can you hear how the dynamics change frequently throughout the first movement? When do you hear music played loudly? Quietly? When do the dynamics change and what effect does that create? When does the music get louder (crescendo) or get quieter (decrescendo)?  
      </p>

      <p>
      Can you hear how the dynamics are quieter overall in the second movement? Can you hear how the chamber orchestra is given a quieter accompanying role in this movement?
      </p>

      <p>
      Do you hear the mood contrasts between the joyful first movement, the reflective sounds of the second movement, and the playful dance-like third movement?
      </p>

      <p>
      Do you hear musical sounds that are short sounding (staccato) or very smooth sounding (legato)? Do you hear the long, sustained legato sounds of the strings that accompany the guitar in the second movement? Do you hear the sounds of plucked lower strings (pizzicato) in this accompaniment part? Do you hear ornamented sounds in the guitar part such as trills that are typical of this period of music writing?
      </p>

      <P>
      <span class="badge">4</span> How is <b>rhythm</b> used in this lute concerto? Do you hear sections that have a steady rhythm or beat that you could tap to? Did any particular rhythmic pattern stand out for you that you could listen for in the work? Can you hear the first movement opening three note scalic rhythmic pattern that sounds almost like a bird call in the quickly ornamented sounds? Then after two &ldquo;bird calls&rdquo; can you hear four notes repeated in the same rhythm? Can you hear the decorated bird call rhythms anywhere else in the first movement? Can you hear that the second theme of the opening ritornello has more even rhythms? 
      </P>

      <p>
      Can you hear the use of dotted rhythms featured in the second movement that give a rocking quality to this section? Can you hear longer held rhythms in the accompaniment part? Do you hear the fast rhythms that create rhythmic vitality in the third movement?
      </p>

      <p>
      <span class="badge">5</span> How is <b>melody</b> or <b>pitch</b> used in this Lute Concerto? 
      </p>

      <p>
      Do you hear the opening two stepwise three note &ldquo;bird call&rdquo; motifs in the first movement followed by repeated melodic notes? Can you hear those melodic patterns elsewhere in the first movement? Can you hear that Vivaldi likes to use concise melodic and rhythmic themes? 
      </p>

      <p>
      Can you hear a contrasting melody played with a quieter dynamic that includes a chromatic (moving by semitones) passage?  
      </p>

      <p>
      Where do you melody notes repeated or moving up or down in scale like patterns throughout the concerto? Can you hear when melody notes skip higher or lower in pitch? Can you hear when the melody is ornamented or decorated by the guitar? 
      </p>

      <P>
      <span class="badge">6</span> Can you hear different kinds of <b>texture</b> in this work produced by the two different groups of instruments &mdash; soloist and tutti orchestra? Where do you hear all the instruments playing together so that the sound is thick? Can you hear the thick, chordal (homophonic) sounds of the tutti parts in the first and third movements?
      </P>

      <p>
      Where do you hear a contrasting thinner texture where just the soloist and simple accompaniment of the continuo are playing?  
      </p>

      <p>
      Can you hear how there is less contrast of texture in the second movement? In this movement the texture is generally thinner with a simple quiet strings accompaniment throughout allowing the guitar to sound above the accompaniment. 
      </p>

      <p>
      <span class="badge">7</span> What kinds of instrumental <b>timbre</b> do you hear? Timbre is the different qualities of sound that can be heard, for example the kinds of sounds that the string instruments make when they play together. When does the timbre of the music change because certain instruments are added or taken away? How is the timbre of the tutti contrasted to the timbre of the guitar? 
      </p>

