      <p>
      <b>
        The following music elements and questions can help you listen to this work:
      </b>
      </p>

      <span class="badge">1</span> <b>Form</b> in music refers to the musical architecture or the way the music is structured. Although this string quartet transcription is structured in four movements, you will not hear the kinds of traditional forms that classical composers commonly used for their string quartets. Instead, the four movements could be compared to scenes in an opera, each telling a different story about Jan&aacute;&#269;ek&rsquo;s feelings for Kamila St&ouml;sslov&aacute; as revealed through his letters to her. Sometimes the different scenes are challenging to distinguish however, as there is continuous development of various rhythmic and melodic fragments of sound. 

      Jan&aacute;&#269;ek begins each of his four movements with these tempo markings:
      <ol>
        <li>Andante (moderately slow, walking tempo)</li>
        <li>Adagio (slow)</li>
        <li> Moderato (moderate pace)</li>
        <li>Allegro (lively and quickly)</li>
      </ol>

      However, the movements are difficult to distinguish by tempo markings alone, as Jan&aacute;&#269;ek changes tempos many times during each movement, for example:

      <ol>
        <li>Andante &mdash; Con moto &ndash; Allegro</li>
        <li>Adagio &ndash; Vivace &ndash; Andante &ndash; Presto &ndash; Allegro &ndash; Vivo &mdash; Adagio</li>
        <li>Moderato &ndash; Adagio &mdash; Allegro</li>
        <li>Allegro &ndash; Andante &ndash; Con moto &ndash; Adagio &ndash; Tempo I</li>
      </ol>

      <p>
      The form used within each of the four movements also does not follow conventional kinds of musical structure and development although the fourth movement could be described as a kind of sonata/rondo form that features two contrasting and recurring thematic motifs. The first motif is heard at the opening of the fourth movement and the second motif takes the form of a leaping, trilled note melodic fragment. 
      </p>

      <p>
      Throughout the string quartet/transcription, themes are generally not presented in their entirety to be developed in predictable keys and ways as in Haydn or Mozart&rsquo;s time. Jan&aacute;&#269;ek may present a theme or theme fragment and then immediately interrupt its development with sudden exclamations of sound from other instruments. Can you hear this kind of treatment in the string quartet? What is the effect of this kind of musical structure? 
      </p>

      <p>
      <span class="badge">2</span> The original String Quartet was written for 2 violins, viola, and cello but the performance by the Manitoba Chamber Orchestra is a transcription for string orchestra by Michael Oesterle on commission from the MCO. What <b>instruments</b> can you hear in this transcription for string orchestra? How many performers are playing onstage? 
      </p>

      <p>
      Can you hear the highly charged conversations taking place between various instruments and sections of the orchestra that create feelings of intense emotional dialogue and quality? When do you hear individual instruments or sections and when do you hear the orchestra playing together? Which instruments are given the predominant motifs that are heard in this work?
      </p>

      <p>
      Can you pick out the viola in this work? The viola has a special and prominent role in this string quartet, as Jan&aacute;&#269;ek used this instrument to personify Kamila St&ouml;sslov&aacute;. Jan&aacute;&#269;ek originally intended this part to be played by the romantic viola d&rsquo;amore known as the viola of love. However, the soft tone quality of its sound didn&rsquo;t blend well with the other instruments of the string quartet, so Jan&aacute;&#269;ek replaced the viola d&rsquo;amore with the traditional viola. What can you find out about the viola d&rsquo;amore? 
      </p>

      <p>
      <span class="badge">3</span> Jan&aacute;&#269;ek uses <b>expressive elements</b> (musical elements that express certain feelings or dispositions) to capture his intense emotions and passion for Kamila St&ouml;sslov&aacute;. The composer uses rapidly changing <b>tempos</b> (the speed of the music) and <b>accelerando</b> (increases in tempo) to suggest agitation, anguish, passion, and varying emotions. Can you hear a particularly expressive accelerando in the third movement? This accelerando is so intense it creates an almost dizzying effect. 
      </p>

      <p>
      Does the string quartet/transcirption begin quickly or slowly? Can you hear when the music changes tempo? Can you hear when the tempo of the music changes and gets faster or slower in other sections of the string quartet transcription? How does that affect the mood of the work? Can you hear when Jan&aacute;&#269;ek repeats the same motif at different tempos?
      </p>

      <p>
      <b>Dynamics</b> (the volume of the music) are also used to convey dramatic emotion, tension, and passionate feelings. When do you hear music or small motifs played loudly? Quietly? When do the dynamics change dramatically and what effect does that create? When does the music get suddenly louder (crescendo) or quieter (decrescendo)? Can you hear that the opening of this string quartet begins very loudly and then suddenly becomes very quiet just a short while later (bar 9)?
      </p>

      <p>
      Other expressive elements and special effects that create different moods throughout the work include sections marked <b>rubato</b> (expressive and rhythmic freedom) the use of <b>tremolo</b> (rapid back and forth movement of the bow on the same note) and <b>sul ponticello</b> (playing with the bow on or near the bridge). Can you hear the use of sul ponticello in the viola part in the opening of the first movement (bar 9)? Do you hear the use of tremolo in the viola and in other parts that creates frenzied, emotionally charged feelings?
      </p>

      <p>
      Jan&aacute;&#269;ek uses expressive effects in highly contrasting ways. Contrasts in tempo, dynamics, tone colour, and texture are dramatically juxtaposed, and there are frequent changes of tempo, texture, dynamics and tone colour. What effect and mood is created by these contrasts and changes? 
      </p>

      <p>
      Jan&aacute;&#269;ek stated that the first movement depicted the impression the composer had when he first saw Kamila; the second movement depicted his experience seeing Kamila over a year later; the third movement was intended to &ldquo;dissolve into a vision that resembles your [Kamila&rsquo;s] image;&rdquo; and the fourth movement was intended by Jan&aacute;&#269;ek to represent his great longing for Kamila and imagined fulfillment. How does the use of expressive elements help to create the contrasting and varying emotions that Jan&aacute;&#269;ek intended?
      </p>

      <P>
      <span class="badge">4</span> How is <b>rhythm</b> used in this string quartet transcription? Is this the kind of music that you can easily tap a steady rhythm or beat to? Can you hear that Jan&aacute;&#269;ek uses a very wide range and variety of rhythms that he repeats, contrasts and combines? Do you hear the way that Jan&aacute;&#269;ek uses rhythmic fragments to create the impression of speech patterns? Can you hear short rhythmic patterns (motifs) repeated at the opening of the work? Can you hear that the rhythmic pattern in the first four bars is repeated immediately and almost exactly in bars 5-8? 
      </P>

      <p>
      Do you hear the propelling rhythmic patterns rapidly repeated or repeated at different tempos to create feelings of great intensity? Can you hear an ostinato (a continually repeated melody or rhythm pattern) effect anywhere in this work? How are rhythms used to create a sense of drama and conflict in this work? Can you pick out dance sounding rhythms in the movement that begins Adagio?
      </p>

      <p>
      <span class="badge">5</span> How is <b>melody</b> or <b>pitch</b> used in this string quartet transcription?  
      </p>

      <p>
      Is this the kind of music that you can easily pick out a melody to hum or sing? Can you pick out short melodic motifs heard throughout the work? Can you hear the opening four bar melodic fragment repeated in bars 5-8? Can you pick out 3 different themes in the first movement? One theme, perhaps representing Jan&aacute;&#269;ek, is heard in the violins played over a trembling sound in the cellos; another theme is played by the sul ponticello viola representing Kamila; and a third leaping, arpeggio theme can be heard in the first violin. 
      </p>

      <p>
      Can you hear when these short melodic fragments are repeated, contrasted with each other, and combined in different ways throughout the movement? Can you hear when short motifs are rapidly repeated or repeated at changing tempos in this and other movements? Do you hear high sounds or pitches? Do you hear low sounds or pitches? Can you hear when the short motifs move higher or lower in pitch? Do you hear short melodic fragments played and traded by certain instruments or sections of instruments?  
      </p>

      <p>
      Can you hear two different themes played by the viola in the adagio movement? These themes are often considered to represent a love duet between Jan&aacute;&#269;ek and Kamila St&ouml;sslov&aacute;. Does the first melody played by the viola suggest sadness perhaps as a result of distance between Jan&aacute;&#269;ek and the woman he loves? Can you hear how this theme is transformed throughout the movement by different instruments and expressive treatment? Do you hear a sudden dance tune that might suggest an orchestra playing at the spa where Jan&aacute;&#269;ek and Kamila met in the setting suggested by this movement? What happens to the dance melody? Does it continue happily or do the sad sounding melodies of the opening movement return? What does this sad melodic ending suggest about the meaning of the second movement? 
      </p>

      <p>
      <span class="badge">6</span> What different kinds of <b>textures</b> do you hear in this work? Do you hear how Jan&aacute;&#269;ek uses the instruments to create a more minimalistic effect than the fuller orchestral sounds created in the Mozart Piano Concerto No. 26 also on this program? Jan&aacute;&#269;ek does not use traditional approaches to harmony or instrumental texture in this work but instead paints a mosaic of sound with various sound effects, tone colours, and melodic and rhythmic fragments, often interrupted by other instrumental sounds.   
      </p>

      <p>
      <span class="badge">7</span> What kinds of instrumental <b>timbre</b> do you hear? Timbre is the different qualities of sound that can be heard, for example the kinds of sounds that the string instruments make when they play together. When does the timbre of the music change because certain instruments are added or taken away? Can you hear different qualities of sound created by different instruments playing with the expressive techniques and effects such as <b>sul ponticello</b> described above? Can you hear the particular timbre of the viola (lower than the violin but higher than the cello) standing out over the other instruments at any time in this work?
      </p>

