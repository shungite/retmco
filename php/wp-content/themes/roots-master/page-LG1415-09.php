      <p>
      <b>
        LEO&#353; JAN&AACUTE;&#269;EK: STRING QUARTET NO. 2<br>&mdash;TRANSCRIBED FOR STRING ORCHESTRA BY MICHAEL OESTERLE 
      </b>
      </p>

      <p>
      Leo&#353; Jan&aacute;&#269;ek (1854-1928) composed his String Quartet No.2 in 1928 when he was around 74 years old. The string quartet was one of two string quartets commissioned by the Bohemian Quartet in 1923. Jan&aacute;&#269;ek subtitled String Quartet No. 1 &ldquo;after Tolstoy&rsquo;s Kreutzer sonata&rdquo; and further information about this work can be found in the Listening Guide for the February 18, 2015 Manitoba Chamber Orchestra concert. 
      </p>

      <p>
      Jan&aacute;&#269;ek also subtitled his second string quartet. He originally gave it the name &ldquo;Love Letters&rdquo; and then re-titled it &ldquo;Intimate Letters&rdquo; referring to hundreds of letters that Jan&aacute;&#269;ek wrote over many years, expressing his love to a young, married woman named Kamila St&ouml;sslov&aacute;. Jan&aacute;&#269;ek&rsquo;s wife Zdenka stated that St&ouml;sslov&aacute; was unimpressed with Jan&aacute;&#269;ek and although St&ouml;sslov&aacute; did not return Jan&aacute;&#269;ek&rsquo;s love and kept him at a distance for many years, the composer pursued St&ouml;sslov&aacute; obsessively and relentlessly for over a decade and she was the inspiration for several of Jan&aacute;&#269;ek&rsquo;s works, including the second String Quartet. 
      </p>

      <p>
      Jan&aacute;&#269;ek&rsquo;s letters to Kamila St&ouml;sslov&aacute; have been described as largely imaginative &mdash; his fantasized vision of what he hoped his relationship with Kamila could be. Jan&aacute;&#269;ek himself describes the relationship that inspires the string quartet as both real and imagined. In his letters to Kamila, Jan&aacute;&#269;ek calls the String Quartet No. 2 his &ldquo;first composition whose notes glow with all the dear things that we&rsquo;ve experienced together. You stand behind every note, you, living, forceful, loving &hellip; but everything&rsquo;s still only longed for &hellip;&rdquo; 
      </p>

      <p>
      Jan&aacute;&#269;ek described his second String Quartet as &ldquo;beautiful, strange, unrestrained, inspired, a composition beyond all the usual conventions&rdquo; (Arnold Whittall, 2003, p. 34).
      </p>

