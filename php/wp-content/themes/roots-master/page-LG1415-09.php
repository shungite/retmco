      <p>
      <b>
        The following ideas and questions can help you listen to this work:
      </b>
      </p>

      <p>
      <span class="badge">1</span> <b>Form</b> in music refers to the musical architecture or the way the music is structured. Can you identify the form of this piano concerto? Can you hear that it is written in three different sections, or movements? They are called: 
      </p>

      <ol>
        <li>Allegro (fast and lively)</li>
        <li>Larghetto (a fairly slow tempo)</li>
        <li>Allegretto (fairly fast and lively but slower than allegro)</li>
      </ol>

      <p>
      Each of the three sections or movements also has a particular form. The first movement Allegro is a variation of the typical classical symphonic form used for first movements known as sonata form. This sonata form variation is known by different names, such as concerto-ritornello form, or sonata form with double exposition. The sections of this first movement form are: 
      </p>

      <ol>
        <li><b>Exposition</b> (sometimes called ritornello or prelude) by the orchestra that states principal themes used in the concerto. Can you hear a first theme introduced by the orchestra that might sound appropriate for the coronation of Leopold II? Can you hear a second and contrasting legato, flowing theme?</li>
        <li><b>Exposition</b> by the solo piano. Can you hear where the piano begins by playing alone and can you hear the piano re-state the orchestra&rsquo;s opening theme? Can you hear where the piano has new virtuosic solo material not heard before?</li>
        <li><b>Development section</b> with orchestra and piano. Can you hear the orchestra and piano varying and combining some of the previously heard themes?</li>
        <li><b>Recapitulation</b> for orchestra and piano. Can you hear the re-statement of previous themes?</li>
        <li><b>Cadenza</b> for solo piano, a virtuosic, improvisatory section featuring the technical prowess of the pianist.</li>
        <li><b>Coda</b> or short concluding section for the orchestra.</li>
      </ol>

      <p>
      Although Mozart titled the first movement &ldquo;Allegro,&rdquo; the second and third movements of this piano concerto were given these tempo markings and titles by music publishers. Can you hear the second slow movement &ldquo;Larghetto&rdquo; introduced by the piano? 
      </p>

      <p>
      Can you hear three different sections in this slow, second movement? Can you hear that a contrasting second section follows the first section? Can you hear sounds from the first section then repeated again? This kind of three-part form is known as ABA or ternary form. 
      </p>

      <p>
      The third Allegretto movement is called a sonata-rondo form. The rondo movement was a common form for the third movement in this time period. In rondo form, a main theme is repeated before and after contrasting sections. The repeated theme is called the A section. The contrasting sections are called B, C, D etc., depending on how many different sections the composer decides to compose. In this rondo the repeated theme is heard in three different episodes and can be described as an ABACBA form. 
      </p>

      <p>
      <span class="badge">2</span> What <b>Instruments</b> can you hear in this concerto? How many performers are playing onstage? Mozart wrote this concerto for solo piano, 2 oboes, 2 bassoons, 2 horns, 2 trumpets, timpani (large drums) and strings. What string instruments do you hear (violin, viola, cello, double bass)? Can you pick out when you hear the other instruments? 
      </p>

      <p>
      Can you hear when the piano first plays? Do you hear the way that Mozart focuses on the piano and treats the piano as a special, solo instrument? Can you hear how Mozart gives the piano brilliant, virtuosic solo passages?
      </p>

      <p>
      Can you hear when the piano gets to play a very difficult, virtuosic, showy section all by itself towards the end of the first movement after the final return of the main themes? This showy section is called a cadenza.
      </p>

      <p>
      <span class="badge">3</span> Mozart uses <b>expressive elements</b> (music elements that express certain feelings or dispositions), <b>tempo</b> (the speed of the music), and <b>dynamics</b> (the volume of the music) to create different moods throughout the concerto. 
      </p>

      <p>
      Where do you hear music played quickly or slowly? Can you hear when the music changes tempo? Can you hear when the tempo of the music changes and gets faster or slower? How does that affect the mood of the work? Which movements use mostly fast music? Which movement uses mostly slow music?  
      </p>

      <p>
      <b>Dynamics</b> (the volume of the music) are also used to provide tension and drama, and to create particular moods. Was the music played at all the same volume (dynamics)? When do you hear music played loudly? Quietly? When do the dynamics change and what effect does that create? When does the music get louder (crescendo) or get quieter (decrescendo)?  
      </p>

      <p>
      What dynamics are used at the beginning of each of the three movements? Can you identify the mood Mozart is trying to create in each movement? Can you hear a change in mood in the second, slow movement?
      </p>

      <p>
      Do you hear musical sounds that are short sounding (staccato) or very smooth sounding (legato)? Which movement or themes features legato sounds? Where do you hear suddenly loud sounds (accents)? 
      </p>

      <p>
      <span class="badge">4</span> How is <b>rhythm</b> used in this piano concerto? Do you hear sections that have a steady rhythm or beat that you could tap to? Did any particular rhythmic pattern stand out for you that you could listen for in the work? Was there a particular pattern that you could hear repeated anywhere in the work?  
      </p>

      <p>
      <span class="badge">5</span> How is <b>melody</b> or <b>pitch</b> used in this piano concerto?  
      </p>

      <p>
      Do you hear high sounds or pitches? Do you hear low sounds or pitches? Can you pick out a particular melody played by the orchestra instruments or piano in each of the three movements? Can you hum or sing that melody? Could you draw the shape of the melody in the air? When does it move up higher and when does it move lower?  
      </p>

      <p>
      Can you hear when the piano plays rapidly ascending runs up the keyboard or descending runs down the keyboard? Can you hear when the piano plays trills (rapidly alternating notes) as part of the melodic line? Can you hear when stepwise melodies are contrasted with arpeggiated passages?
      </p>

      <p>
      Can you hear a melody  that keeps coming back again in the third section or movement of the piano concerto? A piece where a melody returns after different or contrasting sections is called a rondo.
      </p>

      <p>
      <span class="badge">6</span> Can you hear different kinds of <b>texture</b> in this work? Where do you hear lots of instruments playing together so that the sound is thick? Where do you hear just a few instruments playing? Can you tell which instruments are playing? Where does the piano play all by itself?
      </p>

      <p>
      <span class="badge">7</span> What kinds of instrumental <b>timbre</b> do you hear? Timbre is the different qualities of sound that can be heard, for example the kinds of sounds that the string instruments make when they play together. When does the timbre of the music change because Mozart adds different instruments to the strings? Can you hear when the strings are playing alone and when they are playing together with the piano and other instruments?
      </p>

      <p>
      Can you hear different qualities of sound when different instruments play at different times? 
      </p>

