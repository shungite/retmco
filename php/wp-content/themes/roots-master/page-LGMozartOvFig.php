      <p>
      <b>
        The following ideas and questions can help you listen to this work:
      </b>
      </p>

      <p>
      <span class="badge">1</span> <b>Form</b> in music refers to the musical architecture or the way the music is structured. The opera <i>The Marriage of Figaro</i> is structured in 4 acts with an opening, introductory, instrumental <b>overture</b>. The <b>overture</b> played before the curtain opens sets the stage for the action and music to follow and helps the audience emotionally prepare for the opera. In Mozart&rsquo;s time the overture did not always contain reference to melodic themes heard in the rest of the opera although Mozart&rsquo;s late operas did often include themes heard in the opera.  However, <i>The Marriage of Figaro</i> is a stand-alone work and does not contain any thematic references found in the rest of the opera, although the character of the music does suggest the frenetic action and qualities of the characters in the opera.
      </p>
      <p>
      The <b>form</b> of the <b>Overture</b> to <i>The Marriage of Figaro</i> is an abridged sonata form structure. The three section sonata form that Mozart uses for the overture to <i>The Magic Flute</i> and <i>Don Giovanni</i> is shortened here to just two main sections. A smaller, shorter sonata is often called a <b>sonatina</b>.
      </p>
      <p>
      The first section is called an <b>exposition</b> that typically presents 2 themes.  In sonata form, a middle <b>development</b> section typically follows where the themes are varied and developed, but there is no development section in this overture. A final <b>recapitulation</b> section re-states the main themes, followed by an extended <b>coda</b> (conclusion) to balance the structure since there is no development section.
      </p>
      <p>
      Unlike the overtures to <i>The Magic Flute</i> and <i>Don Giovanni</i> which both feature a slow introduction, the overture to <i>The Marriage of Figaro</i> launches immediately into the first theme of the overture, a whispered <b>pianissimo</b> (very quiet), very fast and agitated passage, suggesting perhaps, the conspiratorial whispers and intrigue to come. The hectic <b>presto</b> (very fast) tempo marking of the overture conveys the frenetic, breathless pace and buoyant mood of the opera as a whole.
      </p>

      <p>
      The second part of the first theme begins with a long note followed by an <b>arpeggio</b> pattern (notes of a chord sounding a fast succession). Other thematic fragments are then introduced as part of this first multi-theme group, and about halfway through the exposition, you will hear a contrasting second multi-theme group that can be identified by a more static melody, the sound of sudden, loud accents, and quick <b>ornaments</b> (notes added to decorate or embellish the main melodic notes).  Can you hear that all the themes or theme fragments are mostly introduced quietly which adds to the feeling of intrigue and secrecy? The effervescent themes then bubble up throughout the overture creating a feeling of wit, anticipation, and buoyant good humour.
      </p>

      <p>
      <span class="badge">2</span> What <b>Instruments</b> can you hear in this arrangement of Mozart&rsquo;s overture to <i>The Marriage of Figaro</i>? Mozart originally scored this overture for: strings; two each of flutes, oboes, clarinets, bassoons, horns, trumpets; and timpani.  In this arrangement for wind octet can you hear two each of oboes, clarinets, bassoons, and horns?
      </p>
      <p>
      Can you distinguish the different instruments of the octet playing the different themes and rhythms of the overture? Can you hear the different instruments taking turns on various parts or calling back and forth to each other?
      </p>
      <p>
      <span class="badge">3</span> What is the overall <b>tempo</b> (the speed of the music) of this work? Can you hear how the Presto tempo always seems to be moving forward at a breathless pace to set up the frenetic mood of the opera action to follow?
      </p>

      <p>
      How are <b>dynamics</b> (the volume of the music), and <b>expression</b> (musical elements that express certain feelings or dispositions) used to convey the sense of intrigue and the qualities of comic opera?  Do you hear sudden accents, contrasts between loud and quiet, and long-drawn out crescendos that create an exciting sense of anticipation? Can you hear a very long, extended, controlled crescendo at the end of this overture?  How does this crescendo add to the sense of building tension? This kind of crescendo is called a <b>Mannheim crescendo</b>.
      </p>

      <p>
      Do you hear any unexpected dynamic changes? The dynamic surprises hint at surprises in the action of the opera. Can you hear the many contrasts between loud and quiet in this overture? How might the sudden changes in dynamics or the contrasts between loud and quiet, fast and slow, also suggest different characters in the opera?
      </p>

      <p>
      Do you hear musical sounds that are short sounding (staccato) or very smooth sounding (legato)?  What effect do these sounds create, especially when they are contrasted against each other?
      </p>

      <p>
      <span class="badge">4</span> How is <b>rhythm</b> used in this overture? Do you hear the steady pulsing, and persistent rhythms throughout this work? Do any particular rhythmic patterns stand out for you that you could listen for in the work? Is there a particular pattern that you could hear repeated anywhere in the work?  Do you hear running patterns of notes throughout this overture?
      </p>

      <p>
      <span class="badge">5</span> How is <b>melody</b> or <b>pitch</b> used in this overture?
      </p>

      <p>
      This overture abounds in various catchy melodies and melodic fragments. Can you pick out any particular melody or part of a melody? Can you hum or sing any? Could you draw the shape of any melody in the air? Can you hear the rising melodies that create a feeling of buoyancy and energy?  Can you hear the quickly ascending and descending scalic melodies? Can you hear where Mozart uses high, middle, and low notes? How does Mozart use melody to create both a sense of mystery and a sense of playfulness?
      </p>

      <p>
      Mozart is a brilliant master of writing melody and some consider Mozart to be one of the greatest melody writers ever. The overture to <i>The Marriage of Figaro</i> certainly highlights Mozart&rsquo;s genius for writing melodies. There are no less than 9 distinct fast-paced melodic themes or theme fragments found in this overture.  Some of the melodic ideas or themes are introduced only to be quickly abandoned or interrupted as Mozart introduces yet another, different melodic theme just a few beats or bars later. However, all 9 or more melodic themes and thematic fragments can be grouped into two main multi-part theme groups that are contrasting in style as described above in the section on form.
      </p>

      <p>
      <span class="badge">6</span> How would you describe the <b>texture</b> of this work?  Do you hear the instruments all playing together at once to create thick sounding chords or do you hear the different voices of the instruments interacting together in more linear ways?
      </p>

      <p>
      <span class="badge">7</span> What kinds of instrumental <b>timbre</b> do you hear? <b>Timbre</b> is the different qualities of sound that can be heard. For example, the timbre of the oboe is different to the timbre of the clarinet, bassoon, or horn.  Can you pick out each of those instruments when they are playing just by listening to their individual timbres?
      </p>

