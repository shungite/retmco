      <p>
      <b>
        The following ideas and questions can help you listen to this work:
      </b>
      </p>

      <p>
      <span class="badge">1</span> <b>Form</b> in music refers to the musical architecture or the way the music is structured. The opera <i>Don Giovanni</i> is structured in 2 acts with an opening, introductory, instrumental <b>overture</b>. The <b>overture</b> played before the curtain opens sets the stage for the action and music to follow and helps the audience emotionally prepare for the opera. In Mozart&rsquo;s time the overture did not always contain reference to melodic themes heard in the rest of the opera but in this overture, the Commendatore&rsquo;s theme is heard in the opening introduction to the overture. Other musical elements in the overture are also heard again at different points of the opera.
      </p>
      <p>
      The <b>form</b> of the <b>Overture</b> to <i>Don Giovanni</i> is the classic three section sonata form structure. The first section is called an <b>exposition</b> that typically presents 2 themes; however, in this particular overture you will hear 3 distinct themes in the exposition, none of which are borrowed from the opera. A middle <b>development</b> section follows where the themes are varied and developed, and a final <b>recapitulation</b> section re-states the main themes.
      </p>
      <p>
      The sounds of the overture portray the contrasting moods of the <i>drama giocoso</i>. Can you hear the slow dramatic introduction that Mozart uses to preface the exposition of the overture? The ominous, foreboding opening chords of the introduction set the stage for the opera&rsquo;s dark, tragic, dramatic elements which are in strong contrast with the following energetic giocoso allegro which some listeners believe are meant to portray the character of Don Giovanni. The contrast between the opening foreboding sounds and the following lively allegro underline the qualities of the <i>drama giocoso</i> and the opposition between drama and comedy.
      </p>

      <p>
      The overture importantly foreshadows the dramatic action involving the Commendatore as it introduces different motifs associated with the Commendatore in Act Two. The opening fearsome D minor chord of the overture has been called the &lsquo;Don Giovanni&rsquo; chord since it is heard later in the opera associated with the Don Giovanni character as his statue comes to life.
      </p>

      <p>
      Listen for the long slow sounds of the opening chords, then a silence that some listeners call deathly, then sounds in the same rhythmic pattern but sounding four notes lower. Then can you hear repeated notes and a long short long short long rhythmic pattern?  Following that you will hear a short ascending pattern alternating with two lower sounding notes. These imposing and sinister sounds are heard again when the Commendatore appears in the dining room at the end of the opera.
      </p>

      <p>
      Then can you hear a series of sinister sounding up and down scale passages followed by music that changes character and becomes much more lively and impetuous?  This lively music contains the first theme of the exposition. The second theme begins with contrasts between a loud long chord followed immediately with light quiet but playful fast descending staccato scales, again suggesting contrasts between the dramatic and comedic characters. You can identify the beginning of the third theme by a long note followed by four descending notes played loud and staccato (short, separated, detached notes) followed by a quick light, repeated note passage played more quietly.
      </p>

      <p>
      After a short rest, the development section mainly features the third theme of the exposition, which makes it challenging to distinguish between the development section and the end of the exposition. However the recapitulation is much easier to identify. Can you hear all three themes repeated in the recapitulation section?
      </p>

      <p>
      This kind of compositional development is characteristic of sonata form, which was typically used by Mozart for his later overtures.
      </p>

      <p>
      <span class="badge">2</span> What <b>Instruments</b> can you hear in this arrangement of Mozart&rsquo;s overture to <i>Don Giovanni</i>? Mozart originally scored this overture for: strings; two each of flutes, oboes, clarinets, bassoons, horns and trumpets; three trombones; and timpani.  In this arrangement for wind octet can you hear two each of oboes, clarinets, bassoons, and horns?
      </p>
      <p>
      Can you distinguish between the different instruments of the octet playing the different themes and rhythms of the overture? Can you hear the different instruments taking turns on various parts or calling back and forth to each other?
      </p>
      <p>
      <span class="badge">3</span> What <b>tempos</b> (the speed of the music) can you identify in this work? Can you hear a slow tempo at the beginning of the overture followed by an energetic Allegro (fast and lively)?  How are <b>dynamics</b> (the volume of the music), and <b>expression</b> (musical elements that express certain feelings or dispositions) used to convey the contrasting nature of the <i>drama giocoso</i>? Can you hear the opening chords associated with the Commendatore played loudly and strongly and the quieter, more playful sections that suggest the playful and comedic aspects of the opera? Can you hear other contrasts of loud and quieter music?
      </p>

      <p>
      When do the dynamics change and what effect does that create?  When does the music get louder (crescendo) or get quieter (decrescendo)?
      </p>

      <p>
      Do you hear musical sounds that are short sounding (staccato) or very smooth sounding (legato)?  What effect do these sounds create? Can you hear suddenly loud sounds (accents)? What kinds of contrasting characters or moods do these dynamic and expressive effects suggest?
      </p>

      <p>
      <span class="badge">4</span> How is <b>rhythm</b> used in this overture? Do you hear sections that have a steady rhythm or beat that you could tap to? Did any particular rhythmic pattern stand out for you that you could listen for in the work?  Was there a particular pattern that you could hear repeated anywhere in the work?
      </p>

      <p>
      Do you hear silences (rest) between the opening chords of the overture?  Do you hear the long short long short rhythmic pattern that follows?  This pattern is repeated over and over again; a repeated note pattern is called an <b>ostinato</b>. Do you hear other repeated note rhythmic patterns in the overture?  Can you hear different rhythmic patterns repeated in the overture?
      </p>

      <p>
      <span class="badge">5</span> How is <b>melody</b> or <b>pitch</b> used in this overture?
      </p>

      <p>
      Do you hear high sounds or pitches?  Do you hear low sounds or pitches? Can you pick out a particular melody played by certain woodwind instruments? Can you hum or sing that melody? Could you draw the shape of the melody in the air?  When does it move up higher and when does it move lower?
      </p>

      <p>
      Can you hear that after the first rest at the beginning of the overture, the melody of the next chords is four notes lower then the opening chords? Can you hear the repeated note melody in the next long short long short pattern? Can you hear that the choice of melodic notes used in the up and down scalic passages of the slow introduction create a menacing sound?
      </p>

      <p>
      In the main theme of the exposition can you hear repeated melodic notes? In the second theme can you hear descending melodic passages that might sound like someone tip toeing down the stairs?  In the third theme can you hear a short low descending scale fragment that might sound like very heavy feet stomping five times?  Then can you hear a much higher melody contrasting melody with repeated notes?
      </p>

      <p>
      Can you hear this third theme varied in the development?  Can you hear the melodies in the three themes return again in the recapitulation?
      </p>

      <p>
      <span class="badge">6</span> Can you hear different kinds of <b>texture</b> in this work?  Where do you hear all the instruments playing together so that the sound is thick? (Think of the opening chords of the overture).  The slow opening chords of the overture are written in the key of D minor.  Can you hear where the texture changes with a lighter treatment of instrumentation?  Where the music becomes more playful the key also becomes brighter and changes from D minor to D major.
      </p>

      <p>
      Where do you hear a thinner texture where just a few instruments are playing or taking turns?  Can you tell which instruments are playing? Can you hear that the texture becomes thicker when all the instruments are playing together at the same time?
      </p>

      <p>
      <span class="badge">7</span> What kinds of instrumental <b>timbre</b> do you hear? <b>Timbre</b> is the different qualities of sound that can be heard. For example, the timbre of the oboe is different to the timbre of the clarinet, bassoon, or horn.  Can you pick out each of those instruments when they are playing just by listening to their individual timbres?
      </p>

