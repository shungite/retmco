      <p><b>WOLFGANG AMADEUS MOZART: OVERTURE TO DON GIOVANNI</b></p>

      <p>Wolfgang Amadeus Mozart (1756-1791) composed his 2 act opera <i>Don Giovanni</i> in 1787. It was an immediate success with audiences and remains one of the most popular operas of all time. Mozart called <i>Don Giovanni</i> an opera buffa (comic opera) although it was billed as a &ldquo;drama giocoso&rdquo; to indicate that it contained elements of both drama and comedy.  The opera features a character named Don Giovanni, a licentious Spanish nobleman with a reputation for seducing and abusing women. The opera chronicles Don Giovanni&rsquo;s exploits of attempted rape, murder, and deceit, his relations with the characters Donna Anna, Donna Elvira, and Zerlina, and his eventual downfall. </p>
      <p>
      In the first scene of the opera, Don Giovanni kills Donna Anna&rsquo;s father, Don Pedro, the Commendatore and in the final scene of the opera, a marble statue of the Commendatore comes to life to punish Don Giovanni, ushering him into hell.
      </p>
      <p>
      As was typical for the time, <i>Don Giovanni</i> opens with an instrumental overture. Mozart famously composed the overture the night before the opera&rsquo;s premiere. The approximately 6-7 minute overture that you will hear is an arrangement of Mozart&rsquo;s symphonic overture and is played by a wind octet of oboes, clarinets, bassoons, and horns.
      </p>
      <p>
      You can find out more about Mozart by viewing Haley Rempel&rsquo;s comments on the MCO website: http://www.themco.ca/concert/manson-barnatan/
      </p>
      <p>
      <b>
        When you listen to this overture by Mozart, you will hear the original Don Giovanni symphonic overture arranged for wind octet.
      </b>
      </p>

