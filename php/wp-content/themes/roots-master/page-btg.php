	<h4>About Terezin, Czech Republic</h4>
	<p>In the late 18th century, Terezin was founded as a garrison town during the reign  of Emperor Joseph II and named after his mother, Empress Maria Theresa. However, in 1941, the &lsquo;Ghetto Theresienstadt&rsquo; was established and between the years of 1941 and 1945 the Nazis transferred about 140,000 Jewish people to the town. At its largest,  there were approximately 55,000 people concentrated in Terezin, a town that before  the war had housed about 5,000 residents.</p>
	<p>Children deported to Terezin were not kept with their families, but were watched over by counselors. Education was strictly forbidden, but the counselors found ways around  this rule by telling stories, singing songs, and acting out plays to covertly teach history, religion, reading, writing and mathematics. One of the most tragic statistics is that of the  10,000 children who made their way through Terezin, only a tiny percentage survived. Some place the figure at 100, others at 1,000, but either way it is horrifically small.</p>
	<p>For more information on Terezin, visit the following websites:</p>
	<p><a href="http://www.jewishvirtuallibrary.org/jsource/Holocaust/terezin.html"><b>http://www.jewishvirtuallibrary.org/jsource/Holocaust/terezin.html</b></a></p>
