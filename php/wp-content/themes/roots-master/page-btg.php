		<h4>Curricular connections</h4>
		<p><b>Aesthetics and art criticism</b></p>
		<ul>
			<li>After reviewing the Study Guide and attending the Manitoba Chamber Orchestra&rsquo;s production of <em>Brundib&aacute;r</em>, ask students to write a review of the production, noting how the music directly affects the emotional impact for the listener.</li>
		</ul>
		<p><b>English, reading and writing</b></p>
		<ul>
			<li>After seeing the Manitoba Chamber Orchestra&rsquo;s production of <em>Brundib&aacute;r</em>, ask students to write a journal entry or review of the show as a reflection.</li>
			<li>Write a sequel to <em>Brundib&aacute;r</em> about what happens to the characters or life in the town 5 years after the action in opera.</li>
			<li>Using persuasive writing styles, create a new ending for the opera, using known character information to produce absolute change within one or more of the characters.</li>
			<li>Ask the students to consider whether there are other stories that remind them of <em>Brundib&aacute;r</em>. Ask the students to choose a fairytale and write an opera libretto. Remind them that they may have to streamline and/or simplify the story. It takes longer to sing something than to say it. The libretto will consist almost entirely of  dialogue. After they have written the libretto, have them reflect on what they had to do to take a written story and make it work as a dramatic or musical one. They  can use poetry or not, as they wish.</li>
		</ul>
		<p><b>Science</b></p>
		<p>The voice is a combination of a wind instrument and a string instrument &mdash; air passing  through the vocal cords creates a vacuum, pulling the vocal cords closed, The cords then vibrate together and create sound. Pitch is determined by the tension of the vocal cords, just like a violin or guitar. You can demonstrate this with a rubber band: wrap a rubber  band around your fingers. Pluck it a few times. Can you see and feel the vibrations? The harder you pluck the rubber band, the more it will vibrate, creating a louder sound. If you stretch the rubber band, making it longer and thinner, what do you hear? (It will be a higher pitch.) Have your students place their hands on their throats while speaking or singing at different pitches so they can feel the vibrations in their throats and chests. Explore sound waves.</p>
		<p><b>Create, present, perform</b></p>
		<ul>
			<li>Obtain copies of the translation of the libretto for <em>Brundib&aacute;r</em>. Have students read the parts in a dramatic reading, infusing as much feeling and power into the words as possible. The language may be awkward or embarrassing. Ask them to translate the libretto into their own way of speaking and perform that way. Then return to  the original libretto. How does their perception of the language change &mdash; or not? Do they think that music can affect how the lines sound?</li>
			<li>Have students work in groups to write their own &lsquo;operas&rsquo; using popular songs, stringing them together with dialogue. These could be performed for the class.</li>
		</ul>
