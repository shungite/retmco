						<strong>LE PAPILLON DE L&rsquo;APPRENTISSAGE</strong>
					</p>
					<p>
						Les s&eacute;ances en matin&eacute;e d&rsquo;une heure de l&rsquo;OCM et les Guides d&rsquo;apprentissage interactifs et bilingues connexes donnent vie au programme Musique Manitoba pour les &eacute;l&egrave;ves de la maternelle &agrave; la 12e ann&eacute;e, de m&ecirc;me que leurs enseignants. Les Guides d&rsquo;apprentissage &eacute;tablissent des liens entre nos concerts et une gamme de composants cl&eacute;s du programme et utilisent les quatre ailes du papillon de l&rsquo;apprentissage (Faire, Cr&eacute;er, &Eacute;tablir des liens et R&eacute;agir) pour approfondir l&rsquo;exp&eacute;rience d&rsquo;apprentissage.
					</p>
					<p>
						<strong>&Eacute;tablir des liens</strong>&thinsp;&mdash;&thinsp;Par une vari&eacute;t&eacute; d&rsquo;exp&eacute;riences interactives (sons, questions, images, vid&eacute;os, etc.), les &eacute;l&egrave;ves acqui&egrave;rent une compr&eacute;hension des gens et des pratiques de la musique. 
					</p>
					<p>
						<strong>R&eacute;agir</strong>&thinsp;&mdash;&thinsp;Par l’écoute, l’observation, la description, l’analyse et l’interprétation, les élèves sont invités à réfléchir aux expériences musicales de façon critique et à y répondre. 
					</p>
					<p>
						<strong>Faire</strong>&thinsp;&mdash;&thinsp;Les &eacute;tudiants apprennent les termes et les pratiques n&eacute;cessaires pour jouer de la musique par des m&eacute;thodes interactives d&rsquo;&eacute;coute et de prestation d&rsquo;un r&eacute;pertoire de concert et d&rsquo;&eacute;l&eacute;ments musicaux.
					</p>
					<p>
						<strong>Cr&eacute;er</strong>&thinsp;&mdash;&thinsp;Les &eacute;l&egrave;ves sont encourag&eacute;s &agrave; explorer et &agrave; cr&eacute;er leur propre musique, inspir&eacute;e d&rsquo;&eacute;l&eacute;ments musicaux du r&eacute;pertoire de concert.
