						<strong>THE LEARNING BUTTERFLY</strong>
					</p>
					<p>
						The MCO&rsquo;s one-hour matinees and accompanying interactive bilingual Learning Guides bring the Manitoba Music curriculum alive for K-12 students and teachers. The Learning Guides connect our concerts to a variety of core curricular areas, and employ the four wings of the Learning Butterfly (Making, Creating, Connecting and Responding) to deepen the learning experiences. 
					</p>
					<p>
						<strong>Connecting</strong>&thinsp;&mdash;&thinsp;Through a variety of interactive experiences (sounds, questions, pictures, videos, etc.), students develop understandings about people and practices in music. 
					</p>
					<p>
						<strong>Responding</strong>&thinsp;&mdash;&thinsp;By listening, observing, describing, analyzing, and interpreting students are invited to critically reflect and respond to music experiences. 
					</p>
					<p>
						<strong>Making</strong>&thinsp;&mdash;&thinsp;Students develop language and practices for making music through interactive ways to listen to and perform to concert repertoire and music elements. 
					</p>
					<p>
						<strong>Creating</strong>&thinsp;&mdash;&thinsp;Students are invited to explore and create their own music inspired by music elements of the concert repertoire. 
					</p>
