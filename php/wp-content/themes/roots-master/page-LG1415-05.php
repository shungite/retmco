      <p><b>The following questions can help you listen to this work:</b></p>

      <p><span class="badge">1</span>&nbsp;Can you identify the form (organizational structure) of this piano concerto?</p>

      <p>Can you hear that it is written in three different sections, or movements? They are called:</p>

      <ol>
        <li>Allegro con brio (fast, lively)</li>
        <li>Adagio (slowly)</li>
        <li>Rondo Molto Allegro (a music form called a rondo played very fast)</li>
      </ol>

      <p>Each of the three sections or movements also has a particular form. The first movement Allegro con brio is written in sonata form. Can you hear a main theme introduced by the orchestra at the beginning of the work that Beethoven develops in different ways throughout this movement, and then returns to at the end of the movement? This kind of compositional development is characteristic of sonata form.</p>

      <p>The second Adagio slow movement is a three part form called ABA or ternary form. Can you hear themes in the first part of this movement, then a contrasting section, and then the sounds repeated from the first section again?</p>

      <p>The third movement is called a rondo. In rondo form, a main theme is repeated before and after contrasting sections. The repeated theme is called the A section. The contrasting sections are called B, C, D etc., depending on how many different sections the composer decides to compose. In this rondo the repeated theme and contrasting sections create an ABACABA form. This means that the main theme (A) is heard 4 times with two different sections (B and C) heard in between the A sections.</p>

      <p>Beethoven also includes a musical joke in the rondo for his audience and musicians of the time. Just before the last A theme is heard, Beethoven writes the music in a different key to what the musicians and audience would have expected at that time in Europe. Can you hear where the sounds of the rondo in the final A section sound like they change as Beethoven &lsquo;corrects&rsquo; the final key of the rondo for his audience and orchestra?</p>

      <p><span class="badge">2</span>&nbsp;What <b>Instruments</b> can you hear in this concerto? How many performers are playing onstage? What string instruments do you hear (violin, viola, cello, double bass)? Can you pick out when you hear the flute, oboes, bassoons, and horns?</p>

      <p>When do you first hear the piano? Do you hear the fast notes that sound very close together when the piano is first heard?</p>

      <p>Do you hear the way that Beethoven treats the piano as a special, solo instrument? Can you hear when the piano is given an important part to play? Can you hear when the piano gets to play a very difficult section all by itself towards the end of the first movement? This showy section is called a cadenza. Can you hear when the orchestra plays by itself or when different instruments can be heard above the rest? Which instruments are given the predominant melodies or themes in this work?</p>

      <p><span class="badge">3</span>&nbsp;Can you identify the mood Beethoven is trying to create in each movement? How does Beethoven use tempo (the speed of the music), dynamics (the volume of the music), and expression (musical elements that express certain feelings or dispositions) to create different moods?</p>

      <p>Which movements use mostly fast music? Which movement uses mostly slow music? Can you hear when the tempo of the music changes and gets faster or slower? How does that affect the mood of the work?</p>

      <p>Was the music played at all the same volume (dynamics)? When do you hear music played loudly? Quietly? When do the dynamics change and what effect does that create? When does the music get louder (crescendo) or get quieter (decrescendo)?</p>

      <p>Do you hear musical sounds that are short sounding (staccato) or very smooth sounding (legato)? Do you hear suddenly loud sounds (accents)?</p>

      <p><span class="badge">4</span>&nbsp;How is <b>rhythm</b> used in this piano concerto? Do you hear sections that have a steady rhythm or beat that you could tap to? Did any particular rhythmic pattern stand out for you that you could listen for in the work? Was there a particular pattern that you could hear repeated anywhere in the work?</p>

      <p><span class="badge">5</span>&nbsp;How is <b>melody or pitch</b> used in this piano concerto? Do you hear high sounds or pitches? Do you hear low sounds or pitches? Can you pick out a particular melody played by the orchestra instruments or piano in each of the three movements? Can you hum or sing that melody? Could you draw the shape of the melody in the air? When does it move up higher and when does it move lower?</p>

      <p>Do you hear a melody in a certain instrument that is then played by a different instrument?</p>

      <p>Can you hear a melody that is repeated over and over again in the third section or movement of the piano concerto? A piece where a melody keeps coming back after different or contrasting sections is called a rondo.</p>

      <p><span class="badge">6</span>&nbsp;Can you hear different kinds of texture in this work? Where do you hear lots of instruments playing together so that the sound is thick? Where do you hear just a few instruments playing? Can you tell which instruments are playing?</p>

      <p><span class="badge">7</span>&nbsp;What kinds of instrumental timbre do you hear? Timbre is the different qualities of sound that can be heard, for example the kinds of sounds that the string instruments make when they play together. When does the timbre of the music change because Beethoven adds different instruments to the strings? Can you hear when the strings are playing alone and when they are playing together with the piano and other instruments?</p>

