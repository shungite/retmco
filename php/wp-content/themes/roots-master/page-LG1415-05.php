      <p><span class="badge">2</span>&nbsp;What adjectives might describe the mood that you felt when listening to this piano concerto? Can you identify what musical elements may have created that mood for you? When did the mood change and why?</p>

      <p><span class="badge">3</span>&nbsp;What musical elements did you enjoy or find interesting? Did you enjoy the melodies that you heard? Did the rhythms, dynamics, or tempos used by Beethoven help make this work exciting? Did you hear parts of the piano concerto that made you feel peaceful or calm and if so, what part of the piano concerto were they in? Were there any parts that sounded playful to you? If so, which movement did you hear them in?</p>

      <p><span class="badge">4</span>&nbsp;Were there parts of the Piano Concerto that you did not enjoy? Why or why not? Can you identify which music elements made you enjoy or not enjoy the music?</p>

