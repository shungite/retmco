      <p><span class="badge">7</span>&nbsp;Is there other music by Beethoven that you could listen to and compare to the sounds and experience of the Piano Concerto No. 2?</p>

      <p><b>MOZART SYMPHONY NO. 40</b></p>

      <p>Mozart wrote at least 41 symphonies and there is evidence that he probably wrote even more. He finished writing his 40th symphony in July 1788. This symphony is one of Mozart&rsquo;s most performed and admired symphonies and it exists in different versions. You can read Haley Rempel&rsquo;s comments about this work on the website to find out more about Mozart and the story of this work <a href="http://www.themco.ca/concert/manson-barnatan/"><b>here</b></a>.</p>

      <p><b>When you listen to this symphony by Mozart, you will hear musical elements that give this work a strong emotional quality. Perhaps you might even feel there is a sense of tragedy, grief, or passionate feelings expressed in this work. The following questions can help you listen to this work.</b></p>

      <p><span class="badge">1</span>&nbsp;Can you identify the form (organizational structure) of this symphony?</p>

      <p>Can you hear that it is written in four different sections, or movements? These movements use forms typical for a symphony written in this classical time period. They are:</p>

      <ol>
        <li>Molto allegro (Very fast and lively)</li>
        <li>Andante (a slower movement)</li>
        <li>Menuetto Allegretto ( A minuet dance form)</li>
        <li>Allegro Finale (a fast, lively finale)</li>
      </ol>

      <p>Each of the four sections or movements also has a particular form. The first, second, and fourth movements are written in sonata form. Can you hear a main theme introduced by the orchestra at the beginning of these movements that Mozart develops in different ways throughout the movement, and then returns to at the end of the movement? This kind of compositional development is characteristic of sonata form.</p>

      <p>The third movement is a three part dance form called a minuet and trio. Can you hear themes in the first part of this movement, then a contrasting section, and then the sounds repeated from the first section again? This particular dance minuet was not intended for people to dance to. Could you dance to this minuet? Why or why not?</p>

      <p><span class="badge">2</span>&nbsp;What <b>instruments</b> can you hear in this symphony? How many performers are playing onstage? What string instruments do you hear (violin, viola, cello, double bass)? Can you pick out other instruments? Which ones?</p>

      <p><span class="badge">3</span>&nbsp;Can you identify the mood Mozart is trying to create in each movement? How does Mozart use <b>tempo</b> (the speed of the music), <b>dynamics</b> (the volume of the music), and <b>expression</b> (musical elements that express certain feelings or dispositions) to create different moods?</p>

      <p>Which movements use mostly fast music? Which movement uses mostly slower music? Can you hear when the tempo of the music changes and gets faster or slower? How does that affect the mood of the work?</p>

      <p>Was the music played at all the same volume? When do you hear music played loudly? Quietly? When does the volume of the music change? When does the music get louder (crescendo) or get quieter (decrescendo)?</p>

      <p>Do you hear musical sounds that are short sounding (staccato) or very smooth sounding (legato)? Do you hear suddenly loud sounds (accents)?</p>

      <p><span class="badge">4</span>&nbsp;How is <b>rhythm</b> used in this symphony? Do you hear sections that have a steady rhythm or beat that you could tap to?</p>

      <p>There are many interesting rhythm patterns in this symphony. The opening movement begins with a repeated three note pattern that sounds like Òopen up, open up, open up now!Ó Do you hear that pattern in the first movement?</p>

      <p>Can you hear any repeated rhythmic patterns in other movements? Did any particular rhythmic pattern stand out for you that you could listen for in this symphony?</p>

      <p><span class="badge">5</span>&nbsp;How is <b>melody</b> used in this symphony? Do you hear high sounds or pitches? Do you hear low sounds or pitches? Can you hear when a melody suddenly jumps much higher? The opening of the first movement is sometimes known as the rocket theme because in the opening rhythmic pattern &lsquo;open up open up now,&rsquo; the note on the word &lsquo;now&rsquo; is suddenly higher than the rest.</p>

      <p>There is another rocket sound at the very beginning of the fourth movement that Mozart creates by writing a series of notes that quickly go higher and higher. This was known as a &lsquo;Mannheim rocket.&rsquo; Can you hear the Mannheim rocket in the finale of Mozart&rsquo;s symphony?</p>

      <p>Can you pick out other melodies or themes in each of the four movements? Can you hear melodies or themes that are repeated throughout each movement and sound quite different to each other? Can you hear melodies or themes that are louder and more dramatic than others? Do you hear themes are that calmer and gentler? Do you hear where Mozart has lets different instruments take turns playing the same melody or parts of the melody? The contrast of these different themes helps create a sense of drama in this symphony.</p>

      <p><span class="badge">6</span>&nbsp;Can you hear different kinds of <b>texture</b> in this work? Where do you hear lots of instruments playing together so that the sound is thick? Where do you hear just a few instruments playing? Can you hear which instruments are playing?</p>

      <p><span class="badge">7</span>&nbsp;What kinds of instrumental <b>timbre</b> do you hear? Timbre is the different qualities of sound that can be heard, for example the kinds of sounds that the string instruments make when they play together. When does the timbre of the music change because Mozart adds or takes away different instruments?</p>

