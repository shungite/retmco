		<p>The <em>Souvenir de Florence</em> is a string sextet scored for two violins, two violas and two cellos, composed in the European summer of 1890. He dedicated the work to the St. Petersburg Chamber Music Society in response to his becoming an Honorary Member.</p>
		<p>The work, in the traditional four-movement form, received its name after the composer sketched one of the principal themes while visiting Florence, Italy, where he composed <em>The Queen of Spades</em>. The work was revised between December 1891 and January 1892, before being premiered in 1892 (Wikipedia).
